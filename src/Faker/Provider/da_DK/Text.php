Følger Kvindens Skrift, kaldet Huskors.


Første Kapitel.

Det første vi fornam Forfærdelse i vort Hus, var en Nat, som min
salige Husbonde Hans Bartskiær og jeg lå i vor Seng; da kom der
under vores Hoved som en Høne, der klukker sine Kyllinger tilsammen.
Hvoraf vi bleve ganske forfærdede og rædde, og vidste ikke hvad vi
skulde tænke, og beklagede os for Godtfolk. Somme sagde, det skulde
være en Hugorm. Så toge vi lange Stænger og kastede Halmen op i
Sengen. Men vi fornam intet. Noget derefter om en Aftens Tid imellem
ni og ti, der jeg vilde trine udaf Døren i Gården, kom der en
forfærdelig Skrubtudse gående fra vor Port på to høie, smalle Been
som en Høne, og var ganske forskrækkelig til at see på. Jeg løb
hastig ind og råbte vore Folk ud, at de skulde see det underlige
Syn, men der de kom ud, forsvandt det.

Om Natten derefter kom et stort Skrig på det mindste Barn. Så
kaldte vi den ene Pige op at tænde et Lys. Hun blev længe ude. Der
hun kom ind, såe hun ganske forfærdelig ud og var slet forvendt. Vi
spurgte, hvorledes det var fat med hende. Hun svarede, at der var
ondt hos hende. Og blev derpå så meget syg, at vi på et halvt års
Tid måtte holde en Pige til hende, som måtte bære hende ud og ind.
Dog blev hun ved Guds Hielp omsider tilpas igjen.

Der dette var skeet, begav det sig, at vor liden Datter, som var ved
otte år gammel, der hun om en Aften gik og legte på Gulvet, kom hun
grædende til hendes Fader og mig og sagde, at hun ikke turde blive i
Stuen for den store Mand, der kommer til hende om Aftenen. Thi, sagde
hun, når det begynder at mørkes, så kommer der en stor Karl til
mig og haver herlige Klæder på og en Kappe med et Fløyels Opslag og
haver to store Knebelbarte og to store slemme Øine; jeg er så ræd
for ham! -- Vi toge det ikke for andet end for Skrømt. Men om Aftenen
derefter, der hun skulde gå iseng, græd hun bitterligen og sagde, at
hun turde ikke ligge i Kammeret; thi, sagde hun, imorgen når min
Fader går fra mig, så kommer den store Karl til mig og tager mig
bort. Så snibbede vi ad hende og lagde hende i Sengen. Om Morgenen
derefter, som hendes Fader stod op fra hende, begyndte hun
hierteligen at græde og skrige. Der vi kom ind til hende, var hun
ganske forvendt. Gud veed da, hvad Sorrig vi fik, at vi såe sådan
Ynk på vort kiære Barn og ikke vilde tro hende, den Tid hun
beklagede sig for os. Hvad vi gav hende i tre Nætter og Dage, det gik
altsammen ud af hendes Mundviger igien. Men vi med alt Folket i Huset
råbte til den alsommægtigste Gud om Hielp og Råd, hvilken også
som en nådig Fader, der ikke forlader sine Børn, kom os til Hielp,
så at Barnet blev så smukt igien, at der kiendtes intet ondt på
hende.


Andet Kapitel.

Noget derefter reiste salig Hans Bartskiær til Tyskland. Fiorten Dage
derefter kom en stor Rædsel i Huset, og hvor vi kom, da var det fælt.
Nu havde vi en liden Dreng hos os, som var ved tolv år gammel, og
var samme Dreng salig Hanses Morbrors Søn. Som han om Aftenen skulde
gå tilsengs, begyndte han at klage sig og græde. Og sagde, at han
ikke turde ligge på Loftet, thi der var så meget ondt hos ham. Så
lagde jeg ham i vort eget Kammer. Der han nu havde lagt sig, begyndte
han lidet derefter ynkeligen at skrige og give sig. Vi løbe
hasteligen til ham; da rystede alle fire Sengestolpene med ham, og
hans Øine stode, ligesom de kunde været opspilede, og ingen kunde
lukke dem sammen. Og hans Mund var så hårdt sammenknyttet, at
ingen kunde vriste den op. Som vi nu længe havde stået over ham, kom
han til sit Mål igjen og kaldte. Vi spurgte ham ad, hvor det var fat
med ham. Han svarede: Det må Gud vide; jeg veed det ikke. Der vi nu
havde våget over ham indtil Klokken var to om Natten, faldt han i
Søvn og sov til om Morgenen Klokken var ti. Der han stod op, såe han
så fælt ud, at man måtte ræddes for ham.

Om Aftenen, som vi fik Nadvere, stod han tilbords og skulde få sig
Mad. Da sagde jeg til ham: Jacob, få dig Mad og gak i din Seng og
læg dig og befal dig Gud i Vold. Vil du så begynde, som du giorde i
Nat, da forfærder du os allesammen, så vi må løbe ud af Huset. --
Som jeg det sagde, sloges Stuedøren og Stegersdøren på Væg. Og blev
han hasteligen henrykt fra Bordet hen udi Gården og blev opdragen
halvanden Alen fra Jorden. Og hængte så i Veiret med oprakte Arme,
åbne Øine og sammenknyttet Mund. Hans Hage sprang op og ned, ligesom
han skulde have mistet den. Vi toge på begge hans Been og Axler og
droge af største Magt og vilde have ham til Jorden. Men vi kunde dog
ikke rokke ham nogensteds. Så faldt vi da alle på Knæ i Gården og
råbte til den evige Gud, at han vilde tænke på Nåde og
Barmhiertighed. Som vi nu havde giort vor Bøn til Gud, blev han løs
og kom på Jorden at stå, men hans Mund var endnu sammenknyttet, og
kunde han slet intet tale. Så toge vi en Sølvskee med et slet Skaft
og vristede Munden op; så sukkede han og hans Tunge løsnedes og kom
til sit Mål igien. Vi spurgte ham ad, hvor det var med ham. Da
svarede han og sagde: Jeg håber til Gud, det bliver nu godt. Thi den
Tid i faldt på Knæ i Gården, da veg han bort og foer af Brønden på
Vedhammeren.

Jeg spurgte ham, hvad det var, der veg bort. Han svarede, at det var
Satan. Da bleve vi ganske forfærdede, at sådan en Giæst var kommen
til os. Jeg spurgte ham videre ad, om han ikke havde besvoret sig med
ham. Da svarede han mig, græd hiertelig og sagde: Kiære Moder, i
haver stor Synd, at i tænker mig dertil; mine kiære Forældre have
ikke dertil optugtet mig. Som han det sagde, kom der et stort Bulder
i Gården, ligesom det havde været en Røst, hvilken dog ingen kunde
forstå. Men Drengen sagde, han talte med ham. I det samme kom der en
gloende Ild og skiød sig fra den ene Side af Gården til den anden.
Da sagde Drengen, at den onde Fjende skiød Ild af sin Hals. Så fik
vi ham ind i Stuen og læste for ham indtil Klokken var to om
Morgenen. Da kom han i Rolighed indtil Klokken var elleve om Dagen.

Derefter kom han atter til ham; og blev det så Dag fra Dag jo
længere jo større Forfærdelse udi det ganske Huus. Her giver jeg
enhver ærlig Mand og Kvinde at betænke mine Vilkår, hvordan de
haver været. Jeg med mine små Børn måtte stedse leve i stor Frygt
og Rædsel; min salig Mand var ikke hiemme, men forreist til Tyskland,
og jeg turde ingen lade vide min store Nød, førend han selv kom hiem
igien.

Ellers sagde Satan til Drengen, at han havde gået efter hans
Husbonde i fiorten år og ikke måttet for den store Mand (Gud i
Himmelen) gjøre ham noget ondt, fordi han ligger ham i Ørene Dag og
Nat. Men nu haver jeg Lov af den store Mand, at plage dig for ham.
Så græd han og vænede sig meget og bad Gud bedre sig, at han nogen
Tid skulde have kommet til Danmark i sine Dage.

Derefter fristede Satan Drengen jo hårdere og hårdere. Undertiden
korsfæstede han ham, at ingen kunde bøie ham: nedbøiede hans Hoved
til den ene Side og lagde hans Fødder tilsammen, ligesom vor Herre
Christus står naglet til Korset. Det hvide af hans Øine vendte han
ud, ligesom han havde været død.


Tredie kapitel.

Der salig Hans kom hiem, mødte ham denne sørgelige Tidende. Så lod
han giøre Bøn på Prædikestolen i vor egen Kirke, såvelsom i alle
omliggende Kirker på Landsbyerne. I vort eget Hus holdt vi tre
Søndage Bededage, at ingen måtte æde eller drikke, enten Mennesker
eller Kvæg.

Der dette skete den første Søndag, og Drengen sad på en Stol, kom
Satan og hakkede på Vinduet ligesom med et Næb: hvorfor han skreg og
gav sig ynkelig og sagde, han vilde have ham udaf Vinduet. Og blev
han seet af somme som en Ravn, af somme som en Gås, og førte et
stort Mørke for Huset.

Som han nu var fristet en lang Tid og ikke havde sovet en Måned, kom
han omsider i Søvn. Og der han vågnede, var han så smuk, som han
havde intet skadt, og samme ganske Nat var det smukt i Huset, hvor
man kom.

Om Morgenen sagde han til os: Jeg takker Gud, som haver denne Nat
ved sine hellige Engle mig bevaret og forkyndt mig, at jeg får
bedre, Gud være lovet! Jeg vil gjerne med Tålmodighed lide, hvad Gud
mig vil pålægge. Jeg veed, det får en god Ende, og at jeg får min
Helbred igien. Thi han må ikke skade nogen Lem på mig. Da sagde vi
til ham: Det bliver nu godt med dig med Guds Hielp, det skal du
visseligen tro. Nei, sagde han, jeg haver nu Frist for ham idag
indtil i Aften Klokken er elleve; så kommer han igien og plager mig
en Tid lang. Men det bliver ikke dermed giort: her bliver alt mere
af. Den gode Gud kan hielpe nådeligen! det vil alt gå hårdere til.
Gud hielpe Moder, den fattige Kvinde, med hendes små Børn.

Om Aftenen, som han havde sagt, kom den onde Giæst igien med stor
Forfærdelse og blev så hos ham indtil Påske. Og kunde vi ikke noget
Sted i Huset være fri for Spøgeri. For somme var han at see som en
Præst, såsom det enten kunde have været Hr. Hans Kiuse i Karlslund
eller Hr. Matz i Herfølge. For somme som en Kiøbmand, for somme som
en Hund, for somme som et Svin. Somme blæste han op i Ansigtet og på
Hænderne, at de hovnede og bleve så store, at man neppelig dem
kiende kunde. De små Børn skrege og græde og gave sig jammerlig,
når han lod sig see for dem. Vi havde et lidet Drengebarn på andet
år. Det rev ud Hår af sit Hoved og sin Skiorte af sit Liv, og ingen
kunde få Råd dermed. Og pegede der som Satan stod og råbte: Der!
der! see! see! at det var stor Ynk at see derpå.

Men den Dreng, som før er talt om, der fristet blev, når han fornam,
at den onde ånd skulde komme, bad han, de skulde tage Børnene bort
af Huset og sagde: Ret nu kommer han til mig.

Engang kom Jakob Meiers Pige ind og havde deres lille Barn med sig,
da bad strax Drengen hende, hun skulde gå hiem med samme Barn eller
hun foer vist ilde. Men hun vilde ikke. Og blev hun så opblæst, at
hendes Ansigt og Hænder bleve så store, at man neppeligen hende
kiende kunde, og såe stor Ynk på hende; dog fik hun bedre på
tredie Dag derefter.

Engang havde vi lånt en fattig Kvinde Hus; som vi nu vare gåede
tilsengs, begyndte hun at råbe og skrige og sagde: Hvor er jeg
kommen her ind? her er en underlig Kompan hos mig, han er ligesom
Simeon Fryndt{3}, og er en stor Forfærdelse hos ham. -- Sådant og
andet råbte hun gruelig og rev sit Hår af sit Hoved. Samme Tid
havde vi en liden hvid Hund, der lå i Stuen; han blev optaget om
Hovedet og slaget op og ned på Gulvet. Men man kunde ingen see, der
ham havde fat. Der den nu omsider krøb under Bænken at skiule sig,
blev den uddragen igien efter Hovedet og Nederparten slæbte på
Gulvet. Om Morgenen blev Hunden forbistret, så at han vilde skamfere
alle, der ind kom, hvorfor vi måtte lade slå ham ihiel.

 {3} Navnet på en Borger i Byen.

En Løverdag, som vi sade og beklagede os, vidste ikke, hvor vi skulde
bære os ad udi dette vort Huskors, da kom Thomes Hiulmand ind at
besøge os. Han blev så anfægtet, at han sagde: Dersom jeg ikke
tilforn vidste, at Satan var til, da fik jeg det nu at vide.

Som vi nu atter beklagede os, begyndte Drengen at række sig op i
Sengen, som han lå, og fægtede imod Satan med Guds Ord og sagde: Vig
nu bort du arrige Fiende Satan, som haver så længe pint mit Legeme.
Nu skal du pines igien i den Sted, som dig er bered i den evige Pine,
som aldrig får Ende. Tvi vorde dig Satan til evig Tid; du som en Tyv
har stiålet dig her ind at plage mit Legeme. -- Så læste han mange
smukke Stykker af den hellige Skrift og af Davids Psalmer og satte
sig på sine Knæ i Sengen og læste Budordene, Troens Artikler, Fader
Vor, Dåbens og Alterens Sakramentes Ord; og ingen han passede på.
Så lode vi hente Folk ind, som skulde høre derpå. Der han nu
skulde skilles fra den onde Fiende, da lagde han sine Hænder sammen,
rakte dem op iveiret og sagde: Ære være Gud, som haver forløst mig
fra Satan. -- Så vendte han sig om til den ene Side, kyssede sin
Hånd og gav den fra sig og sagde: Vær nu velkommen, du sande Guds
Engel, som står hos Guds høire Hånd. Vig ikke bort fra mig, bliv
hos mig enten jeg far tillands eller vands; med mange andre Ord, som
på den Tid blev talet. Kyssede så atter på Hånden, rakte hende
fra sig og lagde sig ned i Sengen under Klæderne, at vi intet såe af
ham uden Kronen af Hovedet. Og da hørte vi ligesom et lidet Barn med
klein Røst havde sunget: Ære være Gud i det høie, Fred på Jorden og
i Menneskene en Velbehagelighed.

Så kastede han Klæderne af sig og stod op af Sengen, som han intet
havde skadet. Tilforn kunde han intet, uden vi skulde hielpe ham og
var så kroget, at hans Ansigt bøiedes ned til Jorden, og man ikke
kunde rette ham op. Men efter den Tid blev han karsk og sund igien.
Da bleve vi meget glade og tænkte, at alting var nu bleven godt.
Hvorfor vi lod også takke i Kirken.


Fierde Kapitel.

Men nu kom den allermægtigste Gud, hvis Råd og Villie er
urandsagelig, og tilstædte endnu større Hiertesorg at komme os på.
Thi efter at det var blevet godt med Drengen, blev min kiære Husbonde
anfægtet Nat og Dag, så ingen kan det tro eller tænke, uden jeg, som
var ham næst; og dette varede hver Dag fra elleve til to Eftermiddag,
desligeste om Natten fra elleve til to.

Det første vi påny fornam noget i Huset, var en Søndag under
Høimesse, og var jeg selv hiemme hos et af vore Børn, som var sygt.
Som vi nu alle vare i Stuen, kom os sådan en Rædsel på, og der blev
så grumt i Huset, at vi allesammen faldt ned, somme på Bænke, somme
på Gulvet, som døde Mennesker. Men Gud kom os til Hielp, at vi kom
til os igien. Vi faldt på Knæ og giorde vor Bøn til den gode Gud;
så blev det godt, og det onde forsvandt. Om anden Dagen derefter
skulde vor Pige Anna gå på Loftet. Der hun kom ned igien, var hun
ganske forfærdet og sagde, der var en underlig Kompan på Loftet
skabt som en Abe.

Anden Dagen derefter gik vores yngste Datter ved Navn Lisabeth på
Loftet og skulde hente sig Pennefiedre, som hun skulde have med sig i
Skole. Der hun nu kom derop, gav hun et stort Skrig af sig. Vi løbe
hastelig op til hende. Da klagede hun sig, græd og sagde: Den slemme
Kompan er nu kommen igien, han havde fat i min Trøie og sagde, at han
skulde have mig; han var så slem og havde to Øine som to Brikker, og
hans Fødder vare som Hønsekløer. Den Tid vi kom ned med hende, gik
jeg ind til hendes Fader og gav ham det tilkiende. Hvoraf han blev
meget bedrøvet. Og derforuden havde han gået udi tre Uger meget
selsom og underlig, og ingen vilde han lade vide, hvad ham skadte.
Dog blev det Dag fra Dag værre med ham; ja, det blev så fælt i det
ganske Hus, som tilforn ikke havde været.

Så bad jeg ham, han dog for Guds Skyld vilde sige mig, hvad ham
skadte, og hvor det var med ham. I det samme segnede han ned på
Bænken, som han sad, og besvimede. Der han nu kom til sig igien,
sagde han: Gud, som er en nådig Fader og altid hielper sine Børn,
som af Hiertet råbe til ham, han tænke på Nåde og Barmhiertighed
og hjelpe os med vore små Børn! Jeg fristes så hart af den arrige
Fiende Satan, som lægger sig omborde med mig Dag og Nat, at mig synes
Himmelens og Jordens Tynge ligger på mig. Du haver ikke længe Mand i
mig, og dine Børn blive snart faderløse. Den gode Gud, som haver
lovet at være Enkers og faderløses Forsvar, han hielpe eder. -- Så
blev han Dag fra Dag jo mere og mere anfægtet, og bekiendte han, at
den onde Fiende lå hver Dag fra Klokken elleve til to på hans Ryg
som en stor Kornsæk. Og var han og ellers knyttet undertiden i hans
Side som et Hønseæg. Han sagde til mig: Du veed ikke andet, end du
haver Gud og din kiære Husbonde hos dig, da haver du Satan hos dig.
Gud forløse mig og det ganske Hus.

Derpå var jeg begiærende, at der måtte giøres Bøn i Kirken af
Prædikestolen både i vor By, såvelsom i Landsbyerne deromkring. Jeg
agede selv hen til Hr. Jakob i Norderup og bad, han vilde giøre Bøn
for os og skrive hen til andre Præster, de vilde giøre det samme. Der
jeg nu skulde tilbage igien og kom i Skoven, da blev Vognen så tung,
at Hestene neppe kunde drage afsted, og var dog intet til Læs uden
Vognmanden og jeg. Den Tid vi agede fra Hr. Jakob var Klokken to
Eftermiddag, der vi kom i Vesterstrædet, slog Klokken et om Natten. I
det samme begyndte Træerne i Haven at knage, og kom os sådan Rædsel
på, så vi troede ikke at komme derfra med Livet. Thi der blev
opreist for os et stort, grueligt Tingest høit op i Veiret som et
stort Tårn, som flyttede sig og havde en skrækkelig Røst. Dog kunde
vi ingen Ord forstå. Da råbte vi til Gud og sang: Vor Gud han er
så fast en Borg, etc. Dermed kom vi Gud skee Lov derfra.

Som jeg nu kom hiem, gik jeg i Seng og lagde min høire Arm over mit
Hoved. Og idet jeg faldt isøvn, kom en forfærdelig Rotte og fik min
ene Finger i sin Mund. Jeg råbte og skreg og drog af min største
Magt vel et halvt Kvarter. Min salig Husbonde bad mig være tilfreds
og sagde: Han må dog intet ondt giøre dig. -- Mig syntes ligesom min
Finger skulde været af Leddet. Men der jeg kom tilrette igien, da var
den hverken brun eller blå, ei heller hoven, og ingen kunde see, at
den skadte noget. Dog kunde jeg ikke røre den i en Måned derefter.

Anden Nat derefter, som vi lå i Sengen, kom os sådan en Hede på,
som vi skulde brænde op i Sengen, og som det havde været en stor
Torden-Hede; så havde vi ingen Ro den ganske Nat. Om Morgenen vi
stod op, bleve vi var, at under os på Lagenet og Sengedynen fra
Hovedet ned til Fødderne var ligesom det var overstrøet med store
Rotteskarn og alt to og to tilsammen, ligesom de havde hængt i en
Tråd, og så mangfoldig, at man neppe kunde see det underste Lagen
derfor. Hvilket og Naboerne såe med stor Forundring. Anden Dagen
derefter blev min salig Mand så hart anfægtet, at han ingen Ro få
kunde. Ingeborg Meiers besøgte os på samme Tid, og som hun kom ind i
Stuen, hørte hun en stor Latter og såe ingen. Der hun turde ikke
længer være inde og var bortgangen, blev min salig Mand atter så
hart anfægtet, at han dånede på Bænken, som han sad. Der han nu kom
noget tilrette igien, fik jeg ham i Seng. Så tog jeg Børnene og
Folkene til mig og satte os uden for Døren i Bislaget, på det han
kunde få nogen Rolighed og komme isøvn.

Som vi havde siddet nu ved en Time, bad jeg min ene Pige Johanne gå
ind så sagtelig og høre om han sov. Hun kom igien og sagde, at han
sov; thi hun kunde ikke høre ham. Derpå sad vi endda noget, indtil
Klokken var ni, thi det var om Sommeren. Der vi nu kom ind, og jeg
vilde gå iseng til ham: borte var han. Gud veed da, hvad mit Hierte
var. Vi ledte allevegne, og ingensteds vi kunde finde ham indtil
længe derefter, da kom vi på et lidet Kammer; der lå han og gav sig
såre. Og holdt det så på samme Sted den ganske Nat med stor Jammer
og Uro. Ligeså anden Nat på en liden Sal, så tredie Nat i Stuen og
fierde Nat i Sengekammeret.

Som vi der lå begge sammen i Sengen, kom der en forfærdelig Rotte
springende op i Sengen og løb på alle fire Sengestolper, og omsider
hen under hans Hoved og løftede to Puder og Hoveddyner og en
Sengedyne op, tog et Stykke bort af den underste Sengedyne, så stort
som et Spunds på en Tønde, så at Fieren fløi op i Sengen og omkring
i Sengekammeret. Den Forfærdelse glemmer jeg aldrig. Da sagde den
salig Mand: Far nu bort, du fordømte ånd, til den Sted, som du est
kommen fra, til den evige Død, som dig og dine er beredt til evig
Tid. Ære være Gud, som mig haver forløst! Der haver du alt det du
haver vundet. -- Så blev han fri efter den Dag og ikke mere fristet.


Femte Kapitel.

Der vi nu havde ladet takke på Prædikestolen og tænkte alting var
godt, så kommer den almægtige og gode Gud og tilsteder endnu mere
Hiertesorrig. Vi havde en liden Dreng, som var på niende år. Han
blev så underlig, at vi ikke kunde tænke, hvad ham skadte. Han
sagde, det løb i hans Bug og stak ham. Vi tænkte at det havde været
Skiæver{4}. Hvorfor vi lod ham bade og brugte adskillig Råd dertil,
men det blev jo længer jo værre. Så sendte vi Bud efter
Bartskiæreren, at han skulde forfare, hvad ham skadte. Han vidste
ingen Råd, men eftersom her på samme Tid var kommen til Byen en
Doktorske, sagde han, vi skulde høre hendes Betænkning derom. Hende
lod vi også hente; hun sagde, at han var besat med den onde ånd og
vidste ingen Råd uden en hellig og alvorlig Bøn til Gud i Himmelen.
Gud veed, hvad Sorrig vi da fik, at sådan en Giæst var kommen igien.
Som jeg nu stod i Stuen og dette Barn lå i en Kurveseng, blev Sengen
optagen halvanden Alen fra Jorden og begyndte at springe op og ned.
Jeg løb til salig Hans og kaldte ham ind. Der vi kom ind, var han
tagen af Sengen; han stod på Hovedet og Benene i Veiret med
udstrakte Arme, og var med stor Nød, at vi kunde få ham ned i Sengen
igien. Efter den Dag såe vi stor Ynk på ham. Den onde ånd løb op
og ned i ham som en Gris og reiste hans Bug op, at det var grueligt
at see, skiød Tungen ud af hans Hals og trillede den tilsammen som et
Klæde, og Blodet flød af Munden. Han smadskede i hans Bug som en
Gris og knytted hans Lemmer så hart tilsammen, at fire føre Karle
vare ikke stærke nok til at skille dem fra hinanden. Han galede som
en Hane, giøede som en Hund, førte ham op på vore Bielker i Stuen,
desligeste på Vedhammeren i Gården. Og når han havde ført ham
derop, forlod han ham. Så sad Barnet der og græd og kunde ingensteds
komme. Han kastede ham og over Plankerne ind i Jakob Meiers Gård.
Han drog hans Øine ind i hans Hoved, desligeste også hans Kinder, og
giorde ham så stiv som en Stok, så at den det ikke vidste, skulde
ikke andet sige, end at det var et Træ. Vi reiste ham op til Væggen.
Der stod han uden al Bevægelse som et Træbillede. Hvo, som ellers
ikke letteligen kunde græde, den måtte jo da græde.

 {4} En Børnesygdom.

Engang var jeg gået i Aftensang. Den Tid jeg kom hiem igien, da var
Stuen fuld af Folk. Og havde han da anfægtet min Moder, at det måtte
sig Gud derover forbarme. Hendes Mund havde han vredet op til den
ene Side, hendes Ben sprang op og ned, at ingen af største Magt kunde
holde dem til Jorden. Der sad den arme Kvinde, græd og jamrede sig,
klagede ynkeligen over mig og sagde, at jeg havde taget hendes Sko af
hendes Fødder og ilde slaget hende. Det kunde intet hielpe, at jeg
sagde nei, og hvad gode Ord jeg gav hende, da hialp det intet. Om
Morgenen derefter lod jeg hente nogle Kvinder, som skulde tale med
hende, men hun blev hart ved de samme Ord. Dog kom hun strax tilrette
igien. Der han nu kom til Drengen igien og talede af hans Mund, sagde
han til salig Hans: Jeg giorde din Kone en stor Harm dermed, at jeg
skabte mig i hendes Lignelse og pinte hendes Moder, at hun ikke andet
vidste, end det var hendes Datter. Hvorfor hun klagede hart over
hende. Der vare og nogle, som troede det var Sanden, at hun slog sin
Moder. Men havde jeg måttet for den store Mand, jeg skulde så
handlet med hende, at den ganske By skulde have grædt for hende. --

Om Aftenen, når vi sang: Vor Gud han er så fast en Borg, eller
læste, vrinskede han som en Hest og giorde den allerstørste Spot
deraf, som han kunde; spyttede den salig Mand op i Øinene, at det
flød ned i hans Skiæg og satte dragne Knive for mit Bryst. Når jeg
bad ham slå til i Jesu Navn, så faldt Kniven. Han sagde til salig
Hans: Du må ligeså gierne være tilfreds; forhast dig ikke, du får
mig ikke herud, førend de få Skam, som mig haver her indført. Den
store Mand vil det så have. Endog jeg er en løgnagtig ånd, så må
jeg alligevel undertiden sige Sanden; thi den store Mand tvinger mig
dertil. Jeg var tilfreds at min Tid var kommen at rømme denne Bolig.
Da sagde salig Hans: Når kommer den Tid? Da svarede Satan af Barnets
Mund: Det veed vel den store Mand og ikke jeg. Når han siger: pak
dig, så må jeg rømme.


Sjette Kapitel.

Der den salige Mand havde nu udstået sådan stor Bedrøvelse og
Fristelse både på sit eget Legemes såvelsom ganske Huses Vegne,
så kom den gode Gud og forløste ham fra al Jammer og Elendighed og
kaldte ham fra denne onde Verden til sit evige Riges Herlighed og
Glæde.

Men jeg med mine små Børn måtte blive her tilbage i den store
Sorrig og havde inden mine lukte Døre den arge Fiende Dag og Nat. Den
alvidende Gud, som ingen Ting er forborgen, allene veed den store
Sorrig jeg måtte lide. Og var Under at høre på de Ord, som Satan
talede af Barnets Mund.

Nogen Tid derefter, som min salig Mand var død, skulde jeg age hen
til Hårløv og der kræve nogen Giæld ind hos Præsten sammesteds, Hr.
Samuel. Og Erik Brygger i Vesterstræde kiørte selv for mig. Som vi nu
kom en halv Mil fra Byen, blev jeg så sælsom, som jeg sad i Vognen,
at jeg havde ingen Ro. Hvorfor jeg måtte stå af og sætte mig ned
på Jorden. Der jeg nu havde siddet ved et halvt Kvarter, blev jeg
tilpas igien og vilde stige i Vognen; men borte var Mand, Heste og
alting. Jeg kunde see Fugle flyve, men Mand, Heste og Vogn kunde jeg
ikke see. Som jeg nu længe havde gået omkring på Marken og intet
Menneske kunde komme tilords med, og det led hen over Middag, og jeg
så gik og vænede mig, for jeg vidste ingen Råd ei heller Vei at
komme til, blev jeg henført til et Morads, som var fuldt af mange
små Buske. Mig syntes, jeg vilde gå på dem og tog mit Forklæde fra
mig og vilde have skåret det isønder og bundet det tilsammen, at jeg
kunde dermed opkilte mig. Og så antvordede jeg mine Børn og mit
ganske Hus den evige Gud ivold, thi jeg tænkte aldrig at see dem
mere. Der jeg nu vilde skiære Forklædet isønder til et Kiltebånd,
syntes mig langt borte ligesom der havde været to Krager tilsammen,
hvoraf jeg blev endda mere bedrøvet. Men det var mine Heste, som jeg
omsider med Vognen blev var. I det samme hørte jeg, at Erik kaldte
på mig, og jeg kaldte igien på ham, indtil vi kom tilsammen. Og
vare vi begge så bange, at vi neppe kunde tale. Han sagde, han havde
spurgt om mig, men ingen kunde sige at have seet mig. Så gik han til
den Mose, jeg vilde gået over, og tog et langt Træ og vilde måle,
hvor dybt det var. Men han kunde ikke nå Bunden. Jeg må vel sige,
at Christus Guds Søn var hos mig og hialp mig. Så kom vi dog omsider
vel hiem til Huset igien. Men førend vi kom hiem, havde den fule ånd
af Barnets Mund sagt, hvor langt han havde fulgt os, og hvorledes han
vilde have omkommet mig, havde han måttet for den store Mand; men
dog glædede han sig derved, at han havde så forfærdet os.


Syvende Kapitel.

En liden Tid derefter skulde jeg drage til Kiøbenhavn, og forberørte
Mand, Erik Brygger, kiørte for mig. Da havde den arrige Satan truet,
at han skulde følge mig til Kroen. Som vi nu kom ud ved Ølsemagle ved
Stranden, begyndte Hestene at fnyse, og Vognen løb trindt omkring som
et Hiul, og Klæderne og hvad i Vognen var fløi af den. Jeg havde tre
eller fire Par Høns, som jeg vilde forære bort. De bleve alle løse og
sade på Marken, dukkede og vare meget rædde. Som vi nu havde samlet
Klæderne og Hønsene op igien og aget et Stenkast bort, blev Erik var,
at han havde tabt sin Sæk, som var noget Havre udi. Der han løb
tilbage at hente den igien, bar han så tungt på den, så ham
syntes, at han aldrig bar så tung en Byrde. Jeg troer vist, sagde
han, at Dievlen er i Sækken, thi han er så tung, alligevel her ikke
er uden en Skiæppe Havre udi.

Han bandt Sækken bag på Vognen, men som vi havde lidet aget: borte
var hans Sæk igien. Jeg bad ham, at han vilde lade Sækken fare, jeg
vilde gierne betale ham den. Erik svarede, han ikke vilde miste samme
Sæk, thi han havde om Løverdagen tilforn givet fire og tyve Skilling
for den. Så tog han ham og bandt ham for i Vognen og satte sig selv
derpå. Men det hialp intet; thi den trimlede imellem hans Ben og
faldt ud på Jorden.

I det samme kom en Mand agende; ham bad jeg, han vilde kiøre for i
Veien, jeg vilde betale ham derfor. Hvilket han også giorde; men det
kunde intet lide dermed. Hvorfor samme Mand drog sin Vei til
Kiøbenhavn. Der han var borte, kom vi til en Bæk, hvor vi bleve
siddende, og Vandet gik ind i Vognen; Hestene kunde ei heller
nogensteds komme. Så måtte han bære mig og min Datter først til
Land og siden hvad som var på Vognen og endda have Hielp at han
kunde få Hestene op og Vognen med. Det holdt os så, at vi med stor
Møisommelighed kom til Kroen. Siden kom vi ubehindret frem. Der vi
agede fra Kiøge, var Klokken imellem fire og fem om Sommeren årle om
Morgenen; der vi kom til Kiøbenhavn, slog Klokken syv om Aftenen. Før
jeg kom hiem, havde Satan talt af Barnets Mund og sagt, hvor langt
han havde fulgt os og hvad Skielmeri han havde giort.


Ottende Kapitel.

Således holdt jeg Hus med den arrige Fiende to år efter min salig
Mands Død med stor Hiertesorrig. Hvilket den gode Mand, Magister
Niels Glostrup, min Sogneherre, gik til Hierte såvelsom alle andre
mine gode Venner, og kom Mag. Niels tidt og ofte og selv besøgte mit
Hus og talede den onde Fiende til; giorde og Bøn af Prædikestolen for
os.

Som han kom engang at besøge os, sagde Satan til ham: Måtte jeg for
den store Mand, jeg skulde så handle med dig, at du skulde få Skam.
Du beder så inderlig til den store Mand for dette Barn og det ganske
Hus og piner mig dermed. Idag sad jeg i Sømmen på din Kiortel, men
den Tid du bad for den lille Dreng, da faldt jeg ned og slog mig et
Fandens Slag, så jeg fik Skam. Mag. Niels svarede: Du haver Skam
nok, du fordømte ånd. -- Da svarede Satan: Det veed jeg vel selv. --
Mag. Niels spurgte ham ad: Når vil du fordømte ånd rømme denne
Bolig, hvor du har stiålet dig ind, og forlade dette fattige Barn,
som du piner Nat og Dag? -- Den onde ånd svarede af Barnets Mund:
Vil Du have mig herud? -- Dertil svarede Mag. Niels: Den
allermægtigste Gud skal drive dig herud og hen til den Sted, som er
dig bered i den evige Ild. -- Satan svarede: Når den store Mand
siger: pak dig, så må jeg rømme. Så talede Mag. Niels Latin til
ham. Satan svarede af Spot, at han ikke gad brudt sit Hoved dermed.

Men han sagde til ham: Må jeg være din Giæst til Aften, da vil jeg
komme til dig? Da sagde Mag. Niels: Gud skal bevare mig fra dig!
Satan svarede: Jeg kan komme ind der, som et lidet Sandkorn ikke kan
komme igiennem; gak hiem og giv den lille Dreng et Stykke af den
Steg, som din Kone haver på Ilden. Da svarede Mag. Niels: Gud
forbande dig Satan, du løgnagtige ånd, til evig Tid. Hvad veed du af
den Steg? Det er Sanden, sagde Satan, at jeg er en løgnagtig ånd,
men det er vist, at du haver en Steg på Ilden. Jeg var i Kiøbenhavn
til dit Bryllup og vilde så gierne have giort Perlament og sad i
Døren, men jeg måtte ikke for den store Mand. Dog var det nær, at de
havde kommet op at slås om Dandsen. -- Sådant og andet lod han
høre.

Men den alsommægtigste Gud, som råder over alting havde dog forbudt
ham at skade noget Ledemod på dem, som han af Guds Tilladelse havde
plaget og anfægtet. Og der den nådige Gud nu ikke længere vilde
tilstede ham at bruge sin Ondskab imod os, så måtte han endelig
rømme, og det ganske Hus blev så rent som det tilforn var. Hvorfor
Guds hellige Navn være lovet, som ikke forlader sine Børn, som af
Hiertet råber til ham.

Dette haver jeg skrevet til en Ihukommelse om det min kiære og salige
Husbonde Hans Bartskiær og jeg med vores små Børn og ganske Hus have
af Dievelen og hans Lemmer udstået. Så sandt som Gud skal hielpe
mig og er en nådig Gud og jeg et syndigt Menneske, da er dette
forskrevne Sanden. Hvilket også vitterlig er de hæderlige og
vellærde Mænd Mag. Niels Glostrup, nu Bisp til Opslo, på den Tid vor
kiære Sognepræst; Hr. Bertel Hansen i Duebrødre; Hr. Peder Mand i
Jersie; Hr. Jakob i Nordrup; Hr. Jesper i Roeskilde; Hr. Lauritz i
Liemark og Hr. Niels i Vallensby; af hvilke nogle leve endnu, nogle
ere hensovede. Hr. Peder Mand af stor Medynk tog Barnet til sig og
havde det hos sig nogle Ugers Tid. Siden jeg blev Enke havde Mag.
Niels Drengen hos sig på et halvt års Tid. Og som han var bleven
tilpas igjen, og de fik deres Løn, som sådant ondt havde kommet
afsted og indført Satan i vort Hus, levede han indtil den store Pest
kom, som var år 1620. Så kom den gode Gud og kaldte ham fra denne
onde Verden af al Jammer og Elendighed til sit evige Rige og
himmelske herlige Glæde. Gud skee Lov og Tak fra nu og til evig Tid.
Amen.

Hvo som står, han bede Gud Dag og Nat at han ikke falder.

Man kan ikke sige, hvad et Menneske kan tilkomme førend man lægger
det i Graven.

       *       *       *       *       *

Hidindtil Kvindens egne Ord, såsom de i de troværdigste Exemplarer
befandtes. I somme af de andre lægges en Drøm til, som ikke er meget
at lide på, ikke heller hører synderlig til Sagen. Hvilken vi dog
ville her for deres Skyld fremføre, som kunde begiære den at vide, og
hvis den ikke her fandtes, skulde klage, noget var af Historien
borte. Og henføres samme Drøms Beretning til Enden af det tredie
Kapitel og lyder således:

Søndagen næst efter den Løverdag, som Drengen Jakob blev den onde
ånd kvit, der Folkene kom af Høimesse, kaldte han mig -- (forstå:
Kvinden i Huset,) -- til sig, bad jeg vilde høre, hvilken Drøm han
havde havt denne Nat. Han sagde: Mig tyktes, at en gammel Mand med en
hvid Kiep i sin Hånd kom og tog mig af min Seng og satte mig på
Marken. Så tykte mig, at den gode Engel, som havde været hos mig i
min Sygdom, kom og tog mig op og førte mig i Luften. Så kom den onde
Engel Satan, som og havde været hos mig i min Sygdom og tog mig fra
den gode Engel og førte mig til en stor bred Port, som skulde være
Helvedes Port. Der jeg kom derind, da tykte mig, at der vare mange
grumme og slemme Trolde derinde, og Belsebub sad på et gloende
Hynde, bunden i en Jernlænke og sad for sig selv allene. Og mig
syntes, sagde han, at der var en lang Skive, derved sade mange slemme
Trolde og havde mange store Bøger og Penne for sig og skrev. Og andre
Trolde, som ikke skulde være så meget agtede, de stode og tiente de
andre og gjorde Regnskab for hvad de havde giort her i Verden imod
Menneskene, såsom om Mord, Manddrab og anden Ulykke. Så førte han
mig derfra igien og udi et stort Hus. Derudi stod et langt Trug,
deraf stod en gloende Lue. Derom stode mange Mennesker, som vare
kosteligt klædte. Så sandt som Gud skal hielpe mig, sagde han, jeg
kiendte de Mennesker deriblandt, som jeg har kiendt her i Verden for
nogle år siden. Og idet de stode så udstafferede, da kom disse
Trolde med gloende Brande og sloge på disse Mennesker og kastede dem
ned i den gloende Lue, at de skrege meget ynkeligen. Så førte han
mig derfra og i et stort Hus. Der stode Snedkere og giorde Stole. Jeg
kiendte de Mennesker, hvis Stole Satan viste mig, at de vare nær
færdige. Så førte han mig derfra til et andet Hus, som var meget
Sølv og Guld udi. Og sagde til mig: Alt dette vil jeg give dig, om du
vil være hos mig. Da tykte mig, at jeg svarede nei. Så førte han mig
ud af den brede Port igien, og så kom den gode Engel og tog mig fra
den onde og førte mig udi Luften som tilforn og satte mig på den
grønne Sted. Så kom den gamle Mand med sin hvide Kiep og førte mig i
min Seng igien. --

Så vidt Drømmen, hvilken enhver for sit Værd kan skattere. Men at
Kvinden den skulde have skrevet, er hel uvist. Thi både er der mange
troværdige Exemplarer, i hvilke den ikke findes, så er der og udi et
af dem disse efterskrevne Ord i fortalevis antegnede: Der ere nogle,
som have denne Historie skreven og noget mere indført, end som den
Dannekvinde selv har optegnet. Men denne nærværende er en rigtig Kopi
af det hun selv med sin egen Hånd har skreven. -- Hvoraf er let at
slutte at sådant ikke af Kvinden selv er giort, men snarere af
andre, der have ladet dem forlyste at lægge noget dertil. Og dette
må så være om Kvindens egen Beretning.




Følger:

Videre Forklaring om samme Sag, til mere Forsikkring derom.


Hidindtil om Kvindens egne Ord, hvilke i Førsten ene var agtet til
Trykken at befordres. Men siden er videre Forklaring derom bekommen,
endel af Kiøge Tingbøger, som de fornemme Mænd Mag. Ole Løxdorff,
fordum Sognepræst der i Byen, og Rasmus Christensøn Skiøler,
Borgemester samme Sted, have meddelt at læse og dem derfor bør at
takkes; -- endel af gamle troværdige Folk der i Byen, som derom
vidste at sige. Hvoraf den gode Læser til Underretning skal meddeles
hvad forekommer.

Og synes det altsammen bedst at kunne afdeles i #Personen#, #Stedet#,
#Tiden#, #Begyndelsen#, #Fremgangen# og #Enden#.

Belangende #Personen#, denne Handel fornemmeligen angår, da er
Mandens Navn Hans Bartskiær. Hans Næring var Kiøbmandskab, for hvis
Skyld han og udi Kiøge Tingbøger kaldes gemenligen Hans Kræmmer. Hans
Kvinde og Ægtehustru, som hed Anna, og den forrige Beretning skreven
haver, døde han fra to år før dette Hus-Kors fik Ende, efter at han
ved Rettergang tvende Troldkvinder, som hans Hus havde fortryllet,
havde ladet dømme og henrette med Bål og Brand, som de havde
forskyldt. Af hans Børn, som år 1674, da denne Bog blev først trykt,
var endda i Live og boede i Kiøge: Lisbet Hans Bukkes, en Enkekvinde
på halvfiersindstyve år, som hun selv sagde for mig, der dette
beretter, og sig at være født år 1603, havde i forrige Tider været
Borgemester-Kvinde der i Byen; men var så kommen tilagters, at hun
levede næsten af Folks Almisse. Hendes Moder, som hun berettede,
havde Hans Bartskiær i sytten år. Sad så Enke i fire år. Havde
siden Kort Richter i nitten år. -- #Steden# anlangende, da har dette
svare Hus-Kors været i den Hiørnegård på Torvet, hvor Nørregade
begynder, i hvilken år 1674 boede den gode Mand Jakob Pomeyer,
Rådmand der i Byen, som havde i forrige Tider tient Kort Richter og
vidste meget af denne Handel at sige, hvis Velvillie til at meddele,
hvad ham bevidst var, jeg for mange Stykker i denne Beretning har at
takke.

#Tiden#, på hvilken det er begyndt, er omtrent år 1608 og 1609; thi
år 1612, da det allerede nogle år havde varet, lod Hans Bartskiær
søge disse Troldfolk med Lov og Ret, så de to fornemste af dem samme
år bleve brændte. Men han levede ikke længe derefter, og synes han
at være død enten i Enden på samme år eller i Begyndelsen på det
efterfølgende. Hvorfor han og i Tingbogen år 1613 d. 5. Juli nævnes:
salig Hans Kræmmer, såsom han da var død. Men Hus-Korset varede to
år efter hans Død, som i Kvindens Beretning står i Begyndelsen på
det ottende Kapitel. Og skal derfor uden Tvivl være endt ved år
1615. Vor vidtberømte ~Dr.~ Th. Bartholinus, i hans Skrift ~de morbis
biblicis~, melder om noget sådant ved hans Barndomstid i Kiøge
passeret, og at en af Satan på en underlig Måde er bleven fristet,
så at han endogså undertiden førte ham om i Luften. Og ingen af
Præsterne turde besøge ham, uden han havde levet et ustraffeligt
Levnet. Thi den besatte bebreidede dem, hvad de lønligen havde
beganget. Så vidt den gode Mands Ord, hvilke han for mig selv
således har forklaret, at han har hørt sådant i hans Barndom for
vist at fortælles. Skal derfor ikke feile meget, at de, det har
berettet, jo har havt Henseende til noget i denne Historie.

#Begyndelsen# til al den hele Handel er denne, at en Troldkvinde ved
Navn Johanne Thomesis fattede en svar Vrede og Had til Hans
Bartskiær. Hvortil fortælles disse årsager, nemlig at bemeldte
Johanne Thomesis, som pleiede i Førsten at have god Kundskab med Hans
Kræmmer og ofte kiøbslå med ham, blev af en anden Troldkvinde ved
Navn Kirsten Snedkers udlagt for at have Gemenskab med sådanne Folk,
og der det kom i Rygte, vilde han som en Mand, der haver Afsky til
sådant Pak, ikke mere kiendes ved hende, og om han nogen Tid mødte
hende på Gaden, gik han hende forbi, som han ikke vidste, hvem hun
var. Hvorover hun skal være bleven meget forbittret på ham,
fornemmeligen for der også ved samme Tid en Gård blev tilkiøbs, som
bemeldte Johanne hel gierne vilde have, men Hans Kræmmer fik før ja
på den og kiøbte den bort for hende. For sådanne og andre årsagers
Skyld fatter hun, som sagt er, sådan Had til ham og derfor med andre
Troldfolk, som hun dertil brugte, lagde sig efter at fortrylle ham og
hans Hus. Og har så opmanet Dievlen til at føre den ind i hans
Gård. Hvilket efter deres egen Bekiendelse til Tinge skal være skeet
to Gange: den ene Gang i Johanne Thomesis egen Gård, hvorom i
Tingbogen findes d. 14. Juni og 5. Juli 1613 antegnet, at en af dem
ved Navn Annike Christoffers Datter har bekiendt og derpå gået
tildøde, at de havde været tilsammen i Johanne Thomesis Gård og ved
hendes Brønd opmanet Dievelen, så den er opkommen i en brun,
stakket-stiertet Rottes Lignelse. Den anden Gang uden for Byen ved en
Bæk, som ligger strax udenfor Broporten på den høire Hånd, [når]
man kommer ud af Byen og kaldes Banghors-Bæk. Der har de slemme
Mennesker ladet sig finde tilsammen at mane den onde ånd. Derom
findes i Tingbogen antegnet den 11. September 1612, at en af dem ved
Navn Kirsten Lauritzdatter, som havde tient Johanne Thomesis og blev
samme Dag dømt fra sit Liv, bekiendte, hun havde hørt af samme
Johanne Thomesis, at både hun selv og en anden Troldkvinde ved Navn
Mette Banghors, samt andre, som vare udlagte, vare med at opmane den
onde af Bækken og førte ham i Hans Kræmmers Hus.

Videre berettes, at der de manede den onde, har han i Førsten stillet
sig som han nødig vilde komme og abet dem med dette Giensvar, at han
havde Horn på Hovedet, men de ikke, derfor vilde han ikke komme;
hvorpå den slemme Mette Banghors har hentet en Gryde, sat den på
sit Hovede og sagt til den onde, han skulde herop, thi havde han to
Horn, så havde hun tre. Derpå skal han være opkommen i en Rottes
Lignelse og i sådant Gestalt på en Søndag ad Aften i Hans
Bartskiærs Hus indført.

Derpå er det onde begyndt at tee sig i de fromme Folks Hus og fået
så sin #Fremgang#, hvilket, hvor det er passeret, i det forrige
Skrift med Kvindens egne Ord fortælles. Hvortil man kan lægge dette,
som i Tingbogen findes antegnet d. 5. Oktober 1612, at fire
Dannekvinder, hvis Navne der opregnes, have med Ed vundet, de havde
været i Hans Kræmmers Hus d. 22. Sept. næst forleden og der seet,
hvorledes Drengen, som sagdes at være besat, sprang af Sengen op på
Bordet og fra Bordet op under Bielkerne og hængte sig ved Benene som
en Kat. Tilmed at den onde sagde af Barnets Mund: Han hed Pibere.
Item Mette Gårdløs, og at hans Frue hed Mette Banghors, med mange
flere og slemme Ord, som ikke er fornøden at omtale. Såvidt
Tingbogen.

Anlangende #Enden# og #Udgangen#, da foruden det i Kvindens egen
Relation fortælles, er dertil at henføre, hvorledes disse
Umennesker, (at jeg så skal tale) som det onde med Trolddom i
Mandens Hus indført have, ere omsider en efter anden åbenbarede,
fast tagne og med Lov og Ret til Bål og Brand forfulgte. Den første
af dem skal være en Boel Peders i Eghøi, om hvilken der fandtes
skreven i et Exemplar af Hus-Korset, at samme Boel Peders med sin
Datter er bleven brændt og har udlagt det andet Selskab. Så meldes
og i Tingbogen d. 24. August 1612 at denne Boel Peders har været deri
delagtig.

Den, dernæst blev fasttagen, var bemeldte Johanne Thomesis. Og er hun
bleven røbet ikke alleneste af den Boel Peders, men endogså af en
Taterkvinde. Hvorom der står i Kiøge Tingbog skreven d. 22. Juni
1612, at for fire år tilforn er en Taterkvinde ved Traminde ved
Lybek kommen til Hans Kræmmers Tiener Jakob, som fra Kiøge på sin
Husbondes Hans Kræmmers Ærinde did var forseilet. Denne Kvinde har
sagt ham alt det ham i Hans Kræmmers Hus var vederfaren. Desligeste
at Johanne Thomesis havde ført Dievelen ind i samme Hans Kræmmers Hus
og giort det, for han havde kiøbt Gården fra hendes Husbonde. -- Så
vidt af Tingbogen.

Ellers berettes der, at så ofte samme ugudelige Menneske gik Hans
Kræmmers Vindue forbi, råbte den onde ånd af Drengens Mund, han
havde besat: Der går min Frue, der går min Frue. Desligeste stod
der i et Exemplar af Hus-Korset skreven, at Satan råbte af Barnets
Mund i Hans Kræmmers Hus: Lader mig få min Frue, Johanne Thomesis i
Byesgård, brændt. Over hvilket og andet deslige hun kom i stor
Mistanke, fornemmeligen for hun og tilforn for Trolddom og Gemenskab
med Troldfolk var berygtet.

Men eftersom hun var af Formue, turde ingen vel lægge hende det til,
førend der blev forhvervet Kongebrev, hun skulde på sådant Rygte
gå i Kiælderen.

Derpå blev hun sat ungefær først i Mai 1612, og strax d. 8. Juni er
der sexten Nævningsmænd tilnævnet, som skulde forfare i Sagen. Hvorom
således læses i Tingbogen på samme Dag at være passeret: Matz
Jensen, Borger her i Kiøge, på Hans Kræmmers Vegne med Fuldmagt, som
idag inden Tinge blev læst og påskreven, opkrævede Nævningsmænd, som
skulde granske, udlede og udi ret Sandhed forfare om Johanne
Andersdatter her i Kiøge, boende i Byesgård, kan findes udi
Trolddoms Konst skyldig at være og med Trolddom haver medværet at
opmane Dievelen og indført udi Hans Kræmmers Hus her i Kiøge, som
legemlig kan have besat hans Dreng, den ene efter den anden, som hun
er besigtet for, og offentlig for Dom og Retssag given, så hun
derfor bør at lide etc. Så vidt af Tingbogen, hvori siden opregnes
sexten Mænd ved Navn, som dertil på samme Tid ere forordnede. Og
står videre: At de inden Tinge derom skal giøre deres Afsigt til
Laugetid efter Recessen, som de for Gud allermægtigste og Menneskene
ville ansvare.

Siden fremføres Vidnesbyrd imod hende, hvilket skete d. 22. Juni og
6. Juli. Iblandt dem var dette, som Taterkvinden til Hans Kræmmers
Tiener havde sagt til Traminde og tilforn er fremført. Derforuden at
hun har lovet adskillige ondt, og de havde så fået. Så også at
hun i Byfogdens Hus har engang falden på Knæ for Hans Kræmmer, som
da var kommen derind, og bedt, han hende vilde benåde. Ydermere at
en Aften har en Kvinde ved Navn Anna Simonsdatter stået ved
Fængselsdøren og hørt, at Johanne talte høit derinde, klagede sig og
sagde: Du har forført mig fra mit Gods og Biergning og ført mig i
dette skidne sorte Hul. Tvi vorde dig, jeg vil intet have med dig at
giøre; Mag. Laurs skal vel forsvare mig. Disse ere hendes Ord. Den
Mag. Laurs, hun ommelder, skal være Præsten i Byen på de Tider,
efter hvilken kort derefter kom Mag. Niels Glostrup at være Præst der
samme Sted. End er der vidnet hende på, at hun kort før denne Tid
har stået i Døren til Kirsten Snedkers (som før disse Tider, der hun
for Retten blev tiltalt, udlagde denne Johanne Thomesis), og der
denne Kirsten beklagede sig for Johanne og sagde: Jeg er så meget
bange, at jeg veed ret aldrig, hvort jeg vil; jeg tænker, jeg vil
rømme min Kås, eller jeg bliver sat. Da sagde Johanne: Hvad i giør,
da rømmer ikke. Dersom i det giør, da tager i Sagen med eder. Bliver
i sat og bliver hart ved nei, da kan de ikke komme længer med eder.

Omsider har hun og selv bekiendt, at hun i al den Tid hun sad i
Kiælderen intet åd. Derfor står der, at d. 18. Juni har hun sagt
for Retten, at siden hun i Kiælderen er kommen at sidde, da haver hun
ikke ædt nogen Smule Brød eller nogen Mad, dog hende haver hver Dag
været buden både Mad og Øl i timelig Tid, og det som godt er.
Desligeste står der d. 7. Juli antegnet: Johanne Thomesis bekiender
endnu som tilforn, at nu på ottende Uge hun haver siddet i Fængsel,
haver hun ikke ædt nogen Krumme Brød eller anden Mad i hendes Liv. Og
som hun blev tilspurgt, om hun ikke desimidlertid har havt sit
legemlige Behov, vilde hun slet intet svare dertil, enten ja eller
nei. Tilmed at hendes Mand, Jens Nielsen, havde bekiendt, at: Ihvor
længe hun sidder, da æder hun intet af Hans Kræmmers Mad, om hun end
sad et halvt år. Men dersom jeg eller hendes Børn måtte give hende
af vor egen Mad, da skulde hun vel æde strax.

Så vidt af Tingbogen. Men at hun ganske intet vilde æde af den Mad,
hende blev tilbåren, skete, som Folk berette og Udgangen lærer,
deraf, at Dievelen hende havde indbildt, Folk skulde deraf tænke, hun
var uskyldig. Hvorfor hun og siges at have sagt, hendes Gud skulde
vel opholde hende, alligevel hun intet åd, og derved vise, hun
usandfærdig for sådan Gierning var beskyldt. Ikke desmindre, som
godt Folk i Kiøge, der levede på den Tid dette Skrift blev trykt af
Førsten, vidste at sige, blev der imidlertid allevegne i Byen Stege
og anden Mad borte. Og det således, at ingen kunde vide, hvor de
skulde være henkommen. Blandt andet er det synderligt som fortælles
om en fornem Mand, hvis Navn vi ville forbigå, der engang havde
budet adskillige godt Folk til Giæst, hvilke han blandt andre Retter
vilde foresætte en hel skiøn Kalkunshane. Denne bliver, idet der
rettes an i Kiøkkenet, mellem Folkenes Hænder borte, hvoraf bliver
en stor Forundring, som omsider gik derud oppå, at en af Pigerne fik
Hug derfor. Imidlertid begynder den onde Fiende i Hans Kræmmers Hus
udi den besatte Dreng overlydt at lee og sagde, han lo ad det Pigen
fik Hug for den Steg, hans Frue i Kiælderen med sine Tænder slider.
Dette kommer for Øvrigheden, som strax lader visitere og befinder så
at være, som den onde sagt havde. Således kunde troværdige Folk
derom berette. Skal udentvivl i Pennen have været forfattet i nogen
af de skriftlige Vidnesbyrd om samme Sag, som d. 22. Juni vel meldes
at være for Retten læst og påskreven, men dog nu ingensteds fandtes.
Og vare disse først nogle Præstemænds Vidnesbyrd, nemlig
Herreds-Provstens, Mag. Clauses i Herfølge, Hr. Knud Koldings i
Roeskild, Hr. Peders i Ringsted. Dernæst et Bevis, som en Soldat ved
Navn Georgius Subbert, som lå udi Hans Kræmmers Hus, havde givet
beskreven. For det tredie Steen Hansens skriftlige Vidnesbyrd.
Dernæst Falkvord Pundførs Vidnesbyrd. Og for det sidste Christen
Povelsøns Vidnesbyrd.

Ellers blev og samme 22. Juni Landsdommers Dom og Vidnesbyrd mod
samme udædiske Menneske på Kiøge Ting fremført, læst og påskreven.
Men hvad der i den indeholdtes, står der ikke. Dog at det ikke har
været godt, er deraf at slutte, at der hos læses, at hendes Mand Jens
Nielsen da i fire Ting havde begiæret hans Hustrues Skudsmål, og
ingen har turdet understå sig at give hende noget Skudsmål imod
Landsdommers Dom og Vidnesbyrd.

Den 20. Juli er antegnet, hendes Husbonde Jens Nielsen at være for
otte Dage tilforn given kongelig Varsel at møde samme Dag til Tinge
på hans Hustrues Vegne at svare til det, de samme sexten Mænd vilde
fremføre. Den 27. Juli står der, at samme sexten Mænd ere af samme
Jens Nielsen indstævnet for Landsdommeren, og at Stævningen til Tinge
samme Dag er læst og påskreven. Ikke desmindre have samme sexten
Mænd den 3. August fuldkommet deres Tag og: Endrægtelige (så lyder
Ordene i Bogen) hver efter anden lagt Hånd på Bog og bedet dem så
sandt Gud hielpe og hans hellige Ord, som de udi denne Sag ikke
rettere udi Sandhed kan forfare, end Johanne Thomesis jo har været
årsag udi den onde Medfart, som er vederfaret Hans Kræmmers Dreng,
som siges at være besat, og at de kunde ikke rettere sige, end hun jo
skyldig er udi Trolddoms Gierninger og Konster.

Den 24. August bliver hun atter stillet for Retten, såsom den, der
samme Dag skulde lide for sine Gierninger. Og meldes, at hun da som
tilforn har bekiendt det, som hun var beskyldt for, desligeste udlagt
Voldborg Bødkers, Maren i Rinsbierg, Mette Banghors og en Pige ved
Navn Kirstine, som havde tient hende, og at de vare så slemme i
Trolddom og end værre, end hun var, og derpå vilde hun gå til sin
Død.

Derforuden meldes, at de sexten Mænds Tag over hende samt
Vidnesbyrdene ere til Landsting dømt for fulde, og at Landsdommers
Dom derom er inden Tinge læst og påskreven.

Ydermere at bemeldte Matz Jensen på Hans Kræmmers Vegne samme Dag
til Tinge har været begiærende Dom og Ret over hende, og at hun
derfor er dømt fra hendes Liv og for hendes onde Gierninger at
straffes på Bål og Brand efter Loven.

Samme Dag giver og Matz Jensen på Hans Kræmmers Vegne Mette Banghors
fuldkommelig Sag, og: At hun (så lyder Bogens Ord) med Boel Peders i
Eghøi, fornævnte Johanne Thomesis og flere have været med at opmane
Dievelen og ført det onde i Hans Kræmmers Hus og i så Måde årsag i
den Medfart, hans Dreng, som siges er besat, er med beladen, eftersom
de nu tvende derpå ere gåede tildøde; og var han begiærende en
nøiagtig Borgen for hende etc.

Den 11. September meldes Johanne Thomesis at være henrettet.
Desligeste står der, at den Kirsten (eller Kirsten Larsdatter), som
hende tient havde og var udlagt af hende, er samme Dag stillet for
Retten at dømmes og lide for sine Gierninger, som hun havde tilforn
bekiendt og da også for Retten kiendte, nemlig at hun havde giort
den uhørlige Gierning med Fonten i Kiøge Kirke at besmitte, og at en
Troldkvinde ved Navn Skiel-Annike flyede hende en Skammel, som hun
tog i en af Stolene; den stod hun op på, mens hun det giorde. --
Dommen får hun, at miste sit Liv og at straffes på Bål og Brand.

Og dermed begynder Vidnesbyrd at fremføres mod Mette Banghors.
Iblandt hvilke ere disse, at bemeldte Kirsten for Retten samme Dag
bekiendte, hun havde hørt af Johanne Thomesis, at både Mette
Banghors og de andre, hun havde udlagt, var med at opmane den onde og
at indføre ham i Hans Kræmmers Hus.

Den 21. Septbr. vidnes hende på, at der hun af Kathrine Markusdatter
blev tilspurgt, om hun havde hørt noget om Johanne Thomesis Handel og
om hun havde skikket den onde ind til Hans Kræmmers, undskyldte hun
hende og sagde, Johanne deri var uskyldig. Ikke desmindre, der
Kathrine spurgte, hvem det da havde giort, svarede hun og sagde,
somme vare levende og somme vare døde. Til hvilket Vidne Mette, som
da stod for Retten, sagde nei, og at hun ikke så har sagt. Dog
benægtede hun ikke, at Johanne var ligeså uskyldig som hun er i
denne Sag.

Den 5. Oktober fremførtes det foromtalte Vidne af fire Dannekvinder,
som såe, at den besatte Dreng sprang op af Bordet og hængte sig
under Bielken ved Benene som en Kat og hørte den onde sagde af ham,
hans Frue hedder Mette på Banghors.

Den 26. Oktober meldes, hun på Rådhuset har udi Borgemester og
Råds Nærværelse adskilligt bekiendt efter en skriftlig Fortegnelse,
som derom blev oplæst. Desligeste at hun samme 26. Oktober denne
Bekiendelse var gestændig og sagde, hun af ganske Hierte fortryder,
hun havde givet sig Fanden ivold.

Samme Dag efter Matz Jensens Begiæring på Hans Kræmmers Vegne
forordnes sexten Nævningsmænd, som skulde granske, udlede og udi ret
Sandhed forfare, om hun er skyldig i det samme, Matz Jensen har givet
hende Sag for. Samme sexten Mænd opregnes derefter ved Navn.

Den 9. November bekiender hun for Retten, at: Voldborg Bødkers var
med at indføre (så lyde Ordene) det Dievelskab og onde Medfart, som
den lille Dreng til Hans Kræmmers er beladt med, og at være ligeså
god som hun er. -- Dog at samme Voldborg ikke var med at opmane den
onde, men at hun var med at bære den onde ind i Hans Kræmmers Hus, og
hendes Apostel hedder Smukker, og at Mette, Johanne Thomesis og
Voldborg Bødkers, de vare samtlige om at bære den onde ind i Hans
Kræmmers Hus.

Den 30. Novbr. fuldkomme de sexten Mænd over Mette Banghors deres
Tag. Hvorom står antegnet, at de samme Dag lagde Hånd på Bog og
endregteligen svore Eed og Jevneed og afsagde, at fornævnte Mette
Banghors er skyldig i den onde Medfart og Hus-Kors, som Hans Kræmmers
Dreng er med beladt, og at være skyldig udi Trolddomskonst, så hun
derfor bør at lide.

Den 7. Decbr. meldes der, at samme Tag er af Landsdommeren kiendt ved
Magt, og at hun derfor samme 7. Decbr. er sagt fra hendes Liv, at
straffes med Bål og Brand. Dermed lod Hans Kræmmer Sagen fare.
Derfor står der antegnet Mandagen den 13. Decbr., at efterat Mette
Banghors var af sexten Mænd kiendt skyldig i det hun blev beskyldt
for, har Matz Jensen på Hans Kræmmers Vegne sagt sig fra Sagen og
intet mere at ville have dermed at bestille.

De andre Dievels Lemmer søgtes af Øvrigheden i Byen og andre, dem
mistænkt havde. Og begyndtes så Proces mod Voldborg Bødkers og
Annike Christoffersdatter. Og toges år 1613 den 22. Februar
Nævningsmænd over Annike og den 22. Marts over Voldborg. Samme 22.
Marts, såvel som den 29. dito og den 10. Mai fremføres og Vidnesbyrd
mod denne Voldborg, iblandt hvilke ere disse de mærkeligste:

Hans Pottemager vidnede at Voldborg var engang kommen ind til hans og
der faldt tre Gange i Knæ for hans Hustru og vilde have Barnet at
lege med; men der hun ikke fik det, begiærte hun at besee nogle
Potter og fik dem ikke heller at see; gik så vred på Døren. Og
derefter er strax en ganske Ovn fuld af Arbeide sønderslaget og
fordærvet, alligevel ingen Mennesker rørte derved. Så og er hans
Hustru bleven langsommelig Tid syg. For hvilken Skade og Sygdom han
gav hende fuldkommelig Sag.

Peder Lademager og andre adskillige vidnede om en Guldsmedsvend, som
havde ligget en Nat til Voldborgs, at han har beklaget, samme Nat var
ham en ond Nat, og at Voldborg og hendes Pige have ført ham over
Trætoppe ind på Gammel-Kiøge Kirkegård og der gravet en Tørv, som
de vilde have ham derned. Og at han i nogles Påhør har sagt til
Voldborg: Gud forlade eder for Nat, i have giort mig fattige Karl. I
have giort mig en ond Nat; i og eders Pige ere lige gode. I burde at
være brændt på en Ild for ti år siden. -- Desligeste at han samme
Nat barbenet og i Skiorten med et draget Værge i Hånden er kommen ud
af Voldborgs Gård og til hendes Nabo Peder Lademager og bedet for
Guds Skyld, at de vilde lade ham ind; ellers, sagde han, tage de
Livet af mig.

Rasmus Rafn og Lars Bødker med oprakte Fingre vandt efter Loven, at
de i Peder Karmagers Hus har seet, at som de bedst talede tilsammen,
kom Peder Karmager at sidde i et Kar som var ved fiorten Tønder Vand
udi, og ingen såe eller vidste, hvor han kom derudi. Og havde de
ikke strax fået fat på ham og taget ham op af Karret, da havde han
deri druknet. Videre at Bartskiæren M. Hans og hans Søn Casper vandt,
at der de efter Begiæring kom at forbinde samme Karmagers Kvinde,
viste hun noget levende i sit Lår, som løb op og ned som en spæd
Gris. Og der M. Hans vilde skiære et Hul derpå og mente det skulde
være af Sygdom, gav det sig strax op i Hovedet, så Hovedet slog op
og ned, at nogle Kvinder det måtte holde.

Desligeste at der han atter kom derhen, har hun spyttet ad ham og
med en sælsom Skik revet Plasteret af og vilde ingen Salve lide.

Så vandt og Thomas Biørnsøn og Matz Jensen med oprakte Fingre, at de
have engang været i Peder Karmagers Hus og samme Tid seet og hørt
mange underlige Ord af hans Kvindes Mund, såvel som og underlige
Fagter, såsom hun var besat. Og der den onde blev tilspurgt, hvad
han var for en, svaredes af Kvindens Mund: Jeg er Lucifer. Min Frue
Voldborg Bødkers kom mig ind i dette Menneske at jeg skal pine hende;
at samme Voldborg var hans med Krop og Siæl til evig Tid. -- Det
samme meldes og flere at have vidnet. Iblandt andre Gregers Guldsmed,
som og lagde dette dertil, at den onde sagde, Voldborg havde besvoret
sig med ham, for hun kunde få Sølv og Penninge nok.

Den 29. Marts stilles Annike Christoffersdatter for Retten og selv
bekiendte, som hun havde giort mange Gange tilforn, at hun var
skyldig i det, hun blev anklaget for. Udlægger og samme Voldborg
såvel som Else Holtugs, Birthe Rokkemagers, Karen Eriks og Kirsten
Væverkvinde. Siger og at Kirsten Væverkvinde i Fængslet har været
begiærende, hun vilde årsage hende. Samme Kirsten, som stod der til
Vedermæle, brød omsider ud og begiærede den hellige Kirkes Forbøn
for sig, at hun kunde komme til en sand Bekiendelse og Poenitentze.

Men Annike får samme Dag af sexten Mænd dette Tag, at hun var
skyldig i Trolddomskonst og haver medværet at opmane Dievelskab, som
udi Hans Kræmmers Hus er indført, så hun bør derfor at lide.

Den 14. Juni stilles hun atter for Retten, og meldes de sexten Mænds
Tag over hende den 2. Juni næst forleden til Landsting at være dømt
ved Magt efter Landsdommersdom, som blev læst og påskreven. Annike
bliver og i sin forrige Bekiendelse bestandig, og foruden de andre,
hun havde udlagt, udlægger og Maren Bysvends og siger, at Karen Eriks
har begiæret af hende i Fængslet, hun vilde undskylde og årsage
hende.

Blev så dømt fra hendes Liv, på Bål og Brand, til Høi og hedensk
Jord.

Voldborg Bødkers meldes den 10. Mai, den 7. og 14. Juni at være rømt.

Ikke des mindre bliver den 7. Juni over hende af sexten Mænd afsagt,
at hun i Trolddoms Konst og Gierninger skyldig er og derfor bør at
lide.

Den 14. Juni røber omsider Karen Eriks sig selv for Retten og bad
Byfogden for Guds Skyld, hende måtte benådes hendes Liv; hun vilde
gierne forsværge Landet.

Den 5. Juli siges Annike at være henrettet; antegnes og Vidnesbyrd
imod Maren Ringsbierg, der og skal være udlagt. Iblandt andet
bevises, hun har ladet sig bruge til at vise igien og med adskillige
Personer brugt allehånde Kogleri og Signeri, som her ikke giøres
Behov at opregne, så og spået om dem, og at hun har sagt om ens
Sygdom, som hun blev tilspurgt om, at han var ikke forgiort af onde
Folk, og at han står aldrig op af den Syge og døer lidt efter
Påske, som og skeete.

Samme Dag beskikkes sexten Mænd over Karen Eriks, hvilke den 30.
August dømte hende at være skyldig i Trolddoms Konst og Gierninger,
og at hun derfor bør at lide.

Den 13. Marts begyndes Proces mod Anne Olufs for Trolddom og
fremføres Vidnesbyrd iblandt andet, at Kirsten Pedersdatter
beklagede, der hun havde fået engang at drikke af Anne Olufs Øl, fik
hun ondt, så det begyndte at løbe i hendes venstre Læg og så langs
opad og stædtes i hendes venstre Skulder, som det havde været en Mus,
hvilket hende holdt ved fiorten Dages Tid og skyldte hun Anne Olufs
derfor.

Anne Niels Ibsens vidnede, at efterat Anne Olufs havde engang lovet
hende ondt, blev hun angreben med en underlig og heftig Sygdom og
krøb det omkring i Livet på hende, og rev og sled, så hun ingen Ro
havde, og var det ligesom det havde været en levende Snog og somme
Tid som en Mus både omkring Navlen, Skuldrene og Nakken.

Den 26. Juni meldes Anne Olufs at være henrettet. Samme Dag er og
sexten Mænd forordnede at kiende: Om Else Holtugs (så lyde Bogens
Ord) kan befindes at være skyldig udi det Dievelskab, som var hængt
på Lars Krogs Tag, som kunde give eller være årsag udi Lars Krogs
Ulykke og i hans Tieners Jørgens langvarige Sygdom.

Derforuden andre sexten Mænd over Birgitte Rokkemagers, om hun kan
findes med Trolddom at have forgiort hendes forrige Husbonde, og udi
hvis mere hun på adskillige Tider er beskyldt og udlagt for.

Den 17. og 24. Juli føres Vidnesbyrd mod Else Holtugs og Birgitte
Rokkemagers. Iblandt andet en skriftlig Fortegnelse på Maren
Murmesters Bekiendelse, som hun var gået tildøde på. Men hvad der
stod i den meldes ikke. Derforuden at Ellen Hendrik Snedkers vandt og
høitideligen beklagede både for Gud i Himlen og for Verden, at
hendes Mand engang var kommen i Skienderi med Birgitte Rokkemagers,
og at hun truede ham og derefter manede den onde ind i deres Hus, så
at de slet ingen Ro kunde have derinde Nat eller Dag, og de nødtes
til at sælge Boligen derfor, og at hendes Mand siden blev syg og
grueligen pintes og brændte så såre som af gloende Ild og pintes
så i otte år til han døde, og i sit yderste gav Birgitte Skyld
derfor.

Didrik Snedker vandt desligeste sådant om Hendrik Snedker, og at han
havde sådan Hede og sådan Brynde indvortes, at når man lagde koldt
Smør på hans Bryst, da stod det og sydede, som det kunde stået på
en Ild.

Peder Pedersen vidnede, at Birgitte Rokkemagers havde på en Søndag
hentet Øl for en Skilling hos ham, og om Mandag Morgen derefter kom
han gående ud i sit Bryggers, og ret som han lod Døren op, kom der
en Ting løbende fra Urtekarret og imod ham, ligesom en Hare, og havde
to Øine som en gloende Glød, og siden den Dag kunde han aldrig få en
god Brygning Øl, og derfor skyldte han Birgitte fuldt og fast.

Den 21. August bleve de begge kiendt skyldige i Trolddom.

Den 18. Septbr. får Birgitte Rokkemagers sin Dom til Bål og Brand,
til Høi og hedensk Jord.

Den 19. Oktober opkræves sexten Mænd over Mette Navnes, som af
Birgitte Rokkemagers var udlagt.

Den 6. November føres Vidne imod hende. Så siger hun selv, at hun
kunde døve Ildebrand ved to eller tre Ord, og det kunde hun giøre,
sagde hun, for hun var et Søndagsbarn og sagde, det var ikke
Trolddom.

Den 20. November dømmes således af sexten Mænd, at efterdi hun
af adskillige Troldkvinder er udlagt at være medvidende udi
Trolddomskunster, og haver forgiort hendes egen Mand, hvorpå nogle
af dem ere gåede tildøde, så har hun og i mange år havt et ondt
Rygte for Trolddom, og intet godt Skudsmål hende af nogen bliver
meddelt, er også bevist, hun haver lovet ondt og Ulykke, og de, hun
har lovet det, har så fået, da kunde de ikke skiære eller purgere
hende for Trolddoms Sag; men hun udi samme onde Dievels Konst og
Trolddom skyldig er, så hun derfor bør at lide.

       *       *       *       *       *

Så vidt om denne forskrækkelige og grusomme Dievelens Vold og
Tyranni og det øvrige af Historien; thi videre Kundskab om noget, der
værd er at fortælle, var ikke at bekomme. Og dette er så Enden og
Udgangen på denne Handel.

                Gud os nådelig fra alt ondt bevare!
