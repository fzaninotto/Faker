Capítulo 2: El charco de lágrimas

—¡Curiosismo y curiosismo! —exclamó Alicia (estaba tan sorprendida, que
por un momento se olvidó hasta de hablar correctamente)—. ¡Ahora me estoy
estirando como el telescopio más largo que haya existido jamás! ¡Adiós, pies! —
gritó, porque cuando miró hacia abajo vio que sus pies quedaban ya tan lejos
que parecía fuera a perderlos de vista—. ¡Oh, mis pobrecitos pies! ¡Me pregunto
quién os pondrá ahora vuestros zapatos y vuestros calcetines! ¡Seguro que yo no
podré hacerlo! Voy a estar demasiado lejos para ocuparme personalmente de
vosotros: tendréis que arreglároslas como podáis... Pero voy a tener que ser
amable con ellos —pensó Alicia—, ¡o a lo mejor no querrán llevarme en la
dirección en que yo quiera ir! Veamos: les regalaré un par de zapatos nuevos
todas las Navidades.

Y siguió planeando cómo iba a llevarlo a cabo:
—Tendrán que ir por correo. ¡Y qué gracioso será esto de mandarse regalos
a los propios pies! ¡Y qué chocante va a resultar la dirección!
Al Sr. Pie Derecho de Alicia
Alfombra de la Chimenea,
junto al Guardafuegos
(con un abrazo de Alicia).

¡Dios mío, qué tonterías tan grandes estoy diciendo!
Justo en este momento, su cabeza chocó con el techo de la sala: en efecto,
ahora medía más de dos metros. Cogió rápidamente la llavecita de oro y corrió
hacia la puerta del jardín.

¡Pobre Alicia! Lo máximo que podía hacer era echarse de lado en el suelo y
mirar el jardín con un solo ojo; entrar en él era ahora más difícil que nunca. Se
sentó en el suelo y volvió a llorar.
—¡Debería darte vergüenza! —dijo Alicia—. ¡Una niña tan grande como tú
(ahora sí que podía decirlo) y ponerse a llorar de este modo! ¡Para
inmediatamente!

Pero siguió llorando como si tal cosa, vertiendo litros de lágrimas, hasta
que se formó un verdadero charco a su alrededor, de unos diez centímetros de
profundidad y que cubría la mitad del suelo de la sala.
Al poco rato oyó un ruidito de pisadas a lo lejos, y se secó rápidamente los
ojos para ver quién llegaba. Era el Conejo Blanco que volvía, espléndidamente
vestido, con un par de guantes blancos de cabritilla en una mano y un gran
abanico en la otra. Se acercaba trotando a toda prisa, mientras rezongaba para
sí:
—¡Oh! ¡La Duquesa, la Duquesa! ¡Cómo se pondrá si la hago esperar!
Alicia se sentía tan desesperada que estaba dispuesta a pedir socorro a
cualquiera. Así pues, cuando el Conejo estuvo cerca de ella, empezó a decirle
tímidamente y en voz baja:
—Por favor, señor...
El Conejo se llevó un susto tremendo, dejó caer los guantes blancos de
cabritilla y el abanico, y escapó a todo correr en la oscuridad.
Alicia recogió el abanico y los guantes, Y, como en el vestíbulo hacía mucho
calor, estuvo abanicándose todo el tiempo mientras se decía:
—¡Dios mío! ¡Qué cosas tan extrañas pasan hoy! Y ayer todo pasaba como
de costumbre. Me pregunto si habré cambiado durante la noche. Veamos: ¿era
yo la misma al levantarme esta mañana? Me parece que puedo recordar que me
sentía un poco distinta. Pero, si no soy la misma, la siguiente pregunta es ¿quién
demonios soy? ¡Ah, este es el gran enigma!
Y se puso a pensar en todas las niñas que conocía y que tenían su misma
edad, para ver si podía haberse transformado en una de ellas.
—Estoy segura de no ser Ada —dijo—, porque su pelo cae en grandes rizos,
y el mío no tiene ni medio rizo. Y estoy segura de que no puedo ser Mabel,
porque yo sé muchísimas cosas, y ella, oh, ¡ella sabe Poquísimas! Además, ella
es ella, y yo soy yo, y... ¡Dios mío, qué rompecabezas! Voy a ver si sé todas las
cosas que antes sabía. Veamos: cuatro por cinco doce, y cuatro por seis trece, y
cuatro por siete...
¡Dios mío! ¡Así no llegaré nunca a veinte! De todos modos, la tabla de
multiplicar no significa nada. Probemos con la geografía. Londres es la capital
de París, y París es la capital de Roma, y Roma... No, lo he dicho todo mal, estoy
segura. ¡Me debo haber convertido en Mabel! Probaré, por ejemplo el de la
industriosa abeja."

Cruzó las manos sobre el regazo y notó que la voz le salía ronca y extraña y
las palabras no eran las que deberían ser:
¡Ves como el industrioso cocodrilo
Aprovecha su lustrosa cola
Y derrama las aguas del Nilo
Por sobre sus escamas de oro!
¡Con que alegría muestra sus dientes
Con que cuidado dispone sus uñas
Y se dedica a invitar a los pececillos
Para que entren en sus sonrientes mandíbulas!
¡Estoy segura que esas no son las palabras! Y a la pobre Alicia se le
llenaron otra vez los ojos de lágrimas.

—¡Seguro que soy Mabel! Y tendré que ir a vivir a aquella casucha horrible,
y casi no tendré juguetes para jugar, y ¡tantas lecciones que aprender! No, estoy
completamente decidida: ¡si soy Mabel, me quedaré aquí! De nada servirá que
asomen sus cabezas por el pozo y me digan: «¡Vuelve a salir, cariño!» Me
limitaré a mirar hacia arriba y a decir: «¿Quién soy ahora, veamos? Decidme
esto primero, y después, si me gusta ser esa persona, volveré a subir. Si no me
gusta, me quedaré aquí abajo hasta que sea alguien distinto...» Pero, Dios mío —
exclamó Alicia, hecha un mar de lágrimas—, ¡cómo me gustaría que asomaran
de veras sus cabezas por el pozo! ¡Estoy tan cansada de estar sola aquí abajo!
Al decir estas palabras, su mirada se fijó en sus manos, y vio con sorpresa
que mientras hablaba se había puesto uno de los pequeños guantes blancos de
cabritilla del Conejo.

—¿Cómo he podido hacerlo? —se preguntó—. Tengo que haberme
encogido otra vez.
Se levantó y se acercó a la mesa para comprobar su medida. Y descubrió
que, según sus conjeturas, ahora no medía más de sesenta centímetros, y seguía
achicándose rápidamente. Se dio cuenta en seguida de que la causa de todo era
el abanico que tenía en la mano, y lo soltó a toda prisa, justo a tiempo para no
llegar a desaparecer del todo.
—¡De buena me he librado! —dijo Alicia, bastante asustada por aquel
cambio inesperado, pero muy contenta de verse sana y salva—. ¡Y ahora al
jardín!
Y echó a correr hacia la puertecilla. Pero, ¡ay!, la puertecita volvía a estar
cerrada y la llave de oro seguía como antes sobre la mesa de cristal. «¡Las cosas
están peor que nunca!», pensó la pobre Alicia. «¡Porque nunca había sido tan
pequeña como ahora, nunca! ¡Y declaro que la situación se está poniendo
imposible!»

Mientras decía estas palabras, le resbaló un pie, y un segundo más tarde,
¡chap!, estaba hundida hasta el cuello en agua salada. Lo primero que se le
ocurrió fue que se había caído de alguna manera en el mar. «Y en este caso
podré volver a casa en tren», se dijo para sí. (Alicia había ido a la playa una sola
vez en su vida, y había llegado a la conclusión general de que, fuera uno a donde
fuera, la costa inglesa estaba siempre llena de casetas de baño, niños jugando
con palas en la arena, después una hilera de casas y detrás una estación de
ferrocarril.) Sin embargo, pronto comprendió que estaba en el charco de
lágrimas que había derramado cuando medía casi tres metros de estatura.
—¡Ojalá no hubiera llorado tanto! —dijo Alicia, mientras nadaba a su
alrededor, intentando encontrar la salida—. ¡Supongo que ahora recibiré el
castigo y moriré ahogada en mis propias lágrimas! ¡Será de veras una cosa
extraña! Pero todo es extraño hoy.

En este momento oyó que alguien chapoteaba en el charco, no muy lejos
de ella, y nadó hacia allí para ver quién era. Al principio creyó que se trataba de
una morsa o un hipopótamo, pero después se acordó de lo pequeña que era
ahora, y comprendió que sólo era un ratón que había caído en el charco como
ella.

—¿Servirá de algo ahora —se preguntó Alicia— dirigir la palabra a este
ratón? Todo es tan extraordinario aquí abajo, que no me sorprendería nada que
pudiera hablar. De todos modos, nada se pierde por intentarlo. —Así pues,
Alicia empezó a decirle—: Oh, Ratón, ¿sabe usted cómo salir de este charco?
¡Estoy muy cansada de andar nadando de un lado a otro, oh, Ratón!
Alicia pensó que éste sería el modo correcto de dirigirse a un ratón; nunca
se había visto antes en una situación parecida, pero recordó haber leído en la
Gramática Latina de su hermano «el ratón - del ratón - al ratón - para el ratón -
¡oh, ratón!» El Ratón la miró atentamente, y a Alicia le pareció que le guiñaba
uno de sus ojillos, pero no dijo nada. «Quizá no sepa hablar inglés», pensó 
Alicia. «Puede ser un ratón francés, que llegó hasta aquí con Guillermo el
Conquistador.» (Porque a pesar de todos sus conocimientos de historia, Alicia
no tenía una idea muy clara de cuánto tiempo atrás habían tenido lugar algunas
cosas.) Siguió pues:
—Où est ma chatte?
Era la primera frase de su libro de francés. El Ratón dio un salto
inesperado fuera del agua y empezó a temblar de pies a cabeza.
—¡Oh, le ruego que me perdone! —gritó Alicia apresuradamente, temiendo
haber herido los sentimientos del pobre animal—. Olvidé que a usted no le
gustan los gatos.
—¡No me gustan los gatos! —exclamó el Ratón en voz aguda y
apasionada—. ¿Te gustarían a ti los gatos si tú fueses yo?
—Bueno, puede que no —dijo Alicia en tono conciliador—. No se enfade
por esto. Y, sin embargo, me gustaría poder enseñarle a nuestra gata Dina.
Bastaría que usted la viera para que empezaran a gustarle los gatos. Es tan
bonita y tan suave —siguió Alicia, hablando casi para sí misma, mientras nadaba
perezosa por el charco—, y ronronea tan dulcemente junto al fuego, lamiéndose
las patitas y lavándose la cara... y es tan agradable tenerla en brazos... y es tan
hábil cazando ratones... ¡Oh, perdóneme, por favor! —gritó de nuevo Alicia,
porque esta vez al Ratón se le habían puesto todos los pelos de punta y tenía que
estar enfadado de veras—. No hablaremos más de Dina, si usted no quiere.
—¡Hablaremos dices! —chilló el Ratón, que estaba temblando hasta la
mismísima punta de la cola—. ¡Como si yo fuera a hablar de semejante tema!
Nuestra familia ha odiado siempre a los gatos: ¡Bichos asquerosos,
despreciables, vulgares! ¡Que no vuelva a oír yo esta palabra!
—¡No la volveré a pronunciar! —dijo Alicia, apresurándose a cambiar el
tema de la conversación—. ¿Es usted... es usted amigo... de... de los perros? El
Ratón no dijo nada y Alicia siguió diciendo atropelladamente—: Hay cerca de
casa un perrito tan mono que me gustaría que lo conociera! Un pequeño terrier
de ojillos brillantes, sabe, con el pelo largo, rizado, castaño. Y si le tiras un palo,
va y lo trae, y se sienta sobre dos patas para pedir la comida, y muchas cosas
más... no me acuerdo ni de la mitad... Y es de un granjero, sabe, y el granjero
dice que es un perro tan útil que no lo vendería ni por cien libras. Dice que mata
todas las ratas y... ¡Dios mío! —exclamó Alicia trastornada—. ¡Temo que lo he
ofendido otra vez!

Porque el Ratón se alejaba de ella nadando con todas sus fuerzas, y
organizaba una auténtica tempestad en la charca con su violento chapoteo.
Alicia lo llamó dulcemente mientras nadaba tras él:
—¡Ratoncito querido! ¡vuelve atrás, y no hablaremos más de gatos ni de
perros, puesto que no te gustan!
Cuando el Ratón oyó estas palabras, dio media vuelta y nadó lentamente
hacia ella: tenía la cara pálida (de emoción, pensó Alicia) y dijo con vocecita
temblorosa:
—Vamos a la orilla, y allí te contaré mi historia, y entonces comprenderás
por qué odio a los gatos y a los perros.
Ya era hora de salir de allí, pues la charca se iba llenando más y más de los
pájaros y animales que habían caído en ella: había un pato y un dodo, un loro y 
un aguilucho y otras curiosas criaturas. Alicia abrió la marcha y todo el grupo
nadó hacia la orilla.

Capítulo 3: Una carrera loca y una larga historia

El grupo que se reunió en la orilla tenía un aspecto realmente extraño: los
pájaros con las plumas sucias, los otros animales con el pelo pegado al cuerpo, y
todos calados hasta los huesos, malhumorados e incómodos.
Lo primero era, naturalmente, discurrir el modo de secarse: lo discutieron
entre ellos, y a los pocos minutos a Alicia le parecía de lo más natural
encontrarse en aquella reunión y hablar familiarmente con los animales, como
si los conociera de toda la vida. Sostuvo incluso una larga discusión con el Loro,
que terminó poniéndose muy tozudo y sin querer decir otra cosa que «soy más
viejo que tú, y tengo que saberlo mejor». Y como Alicia se negó a darse por
vencida sin saber antes la edad del Loro, y el Loro se negó rotundamente a
confesar su edad, ahí acabó la conversación.

Por fin el Ratón, que parecía gozar de cierta autoridad dentro del grupo,
les gritó:
—¡Sentaos todos y escuchadme! ¡Os aseguro que voy a dejaros secos en un
santiamén!
Todos se sentaron pues, formando un amplio círculo, con el Ratón en
medio. Alicia mantenía los ojos ansiosamente fijos en él, porque estaba segura
de que iba a pescar un resfriado de aúpa si no se secaba en seguida.
—¡Ejem! —carraspeó el Ratón con aires de importancia—, ¿Estáis
preparados? Esta es la historia más árida y por tanto más seca que conozco.
¡Silencio todos, por favor! «Guillermo el Conquistador, cuya causa era apoyada
por el Papa, fue aceptado muy pronto por los ingleses, que necesitaban un jefe y
estaban hace tiempo acostumbrados a usurpaciones y conquistas. Edwindo Y
Morcaro, duques de Mercia y Northumbría...»
—¡Uf! —graznó el Loro, con un escalofrío.
—Con perdón —dijo el Ratón, frunciendo el ceño, pero con mucha
cortesía—. ¿Decía usted algo?
—¡Yo no! —se apresuró a responder el Loro.
—Pues me lo había parecido —dijo el Ratón—. Continúo. «Edwindo y
Morcaro, duques de Mercia y Northumbría, se pusieron a su favor, e incluso
Stigandio, el patriótico arzobispo de Canterbury, lo encontró conveniente...»
—¿Encontró qué? —preguntó el Pato.
—Encontrólo —repuso el Ratón un poco enfadado—. Desde luego, usted
sabe lo que quiere decir.
—¡Claro que sé lo que quiere decir! —refunfuñó el Pato—. Cuando yo
encuentro algo es casi siempre una rana o un gusano. Lo que quiero saber es qué
fue lo que encontró el arzobispo.
El Ratón hizo como si no hubiera oído esta pregunta y se apresuró a
continuar con su historia:
—«Lo encontró conveniente y decidió ir con Edgardo Athelingo al
encuentro de Guillermo y ofrecerle la corona. Guillermo actuó al principio con
moderación.
Pero la insolencia de sus normandos...» ¿Cómo te sientes ahora, querida?
continuó, dirigiéndose a Alicia.
—Tan mojada como al principio —dijo Alicia en tono melancólico—. Esta
historia es muy seca, pero parece que a mí no me seca nada.
—En este caso —dijo solemnemente el Dodo, mientras se ponía en pie—,
propongo que se abra un receso en la sesión y que pasemos a la adopción
inmediata de remedios más radicales...
—¡Habla en cristiano! —protestó el Aguilucho—. No sé lo que quieren decir
ni la mitad de estas palabras altisonantes, y es más, ¡creo que tampoco tú sabes
lo que significan!

Y el Aguilucho bajó la cabeza para ocultar una sonrisa; algunos de los otros
pájaros rieron sin disimulo.
—Lo que yo iba a decir —siguió el Dodo en tono ofendido— es que el mejor
modo para secarnos sería una Carrera Loca.
—¿Qué es una Carrera Loca? —preguntó Alicia, y no porque tuviera
muchas ganas de averiguarlo, sino porque el Dodo había hecho una pausa, como
esperando que alguien dijera algo, y nadie parecía dispuesto a decir nada.
—Bueno, la mejor manera de explicarlo es hacerlo.
(Y por si alguno de vosotros quiere hacer también una Carrera Loca
cualquier día de invierno, voy a contaros cómo la organizó el Dodo.)

Primero trazó una pista para la Carrera, más o menos en círculo («la forma
exacta no tiene importancia», dijo) y después todo el grupo se fue colocando
aquí y allá a lo largo de la pista. No hubo el «A la una, a las dos, a las tres, ya»,
sino que todos empezaron a correr cuando quisieron, y cada uno paró cuando
quiso, de modo que no era fácil saber cuándo terminaba la carrera. Sin embargo,
cuando llevaban corriendo más o menos media hora, y volvían a estar ya secos,
el Dodo gritó súbitamente:
—¡La carrera ha terminado!
Y todos se agruparon jadeantes a su alrededor, preguntando:
—¿Pero quién ha ganado?
El Dodo no podía contestar a esta pregunta sin entregarse antes a largas
cavilaciones, y estuvo largo rato reflexionando con un dedo apoyado en la frente
(la postura en que aparecen casi siempre retratados los pensadores), mientras
los demás esperaban en silencio. Por fin el Dodo dijo:
—Todos hemos ganado, y todos tenemos que recibir un premio.
—¿Pero quién dará los premios? —preguntó un coro de voces.
—Pues ella, naturalmente —dijo el Dodo, señalando a Alicia con el dedo.
Y todo el grupo se agolpó alrededor de Alicia, gritando como locos:
—¡Premios! ¡Premios!

Alicia no sabía qué hacer, y se metió desesperada una mano en el bolsillo, y
encontró una caja de confites (por suerte el agua salada no había entrado
dentro), y los repartió como premios. Había exactamente un confite para cada
uno de ellos.

—Pero ella también debe tener un premio —dijo el Ratón.
—Claro que sí —aprobó el Dodo con gravedad, y, dirigiéndose a Alicia,
preguntó—: ¿Qué más tienes en el bolsillo?
—Sólo un dedal —dijo Alicia.
—Venga el dedal —dijo el Dodo.

Y entonces todos la rodearon una vez más, mientras el Dodo le ofrecía
solemnemente el dedal con las palabras:
—Os rogamos que aceptéis este elegante dedal.
Y después de este cortísimo discurso, todos aplaudieron con entusiasmo.

Alicia pensó que todo esto era muy absurdo, pero los demás parecían
tomarlo tan en serio que no se atrevió a reír, y, como tampoco se le ocurría nada
que decir, se limitó a hacer una reverencia, y a coger el dedal, con el aire más
solemne que pudo.

Había llegado el momento de comerse los confites, lo que provocó bastante
ruido y confusión, pues los pájaros grandes se quejaban de que sabían a poco, y
los pájaros pequeños se atragantaban y había que darles palmaditas en la
espalda. Sin embargo, por fin terminaron con los confites, y de nuevo se
sentaron en círculo, y pidieron al Ratón que les contara otra historia.
—Me prometiste contarme tu vida, ¿te acuerdas? —dijo Alicia—. Y por qué
odias a los... G. y a los P. —añadió en un susurro, sin atreverse a nombrar a los
gatos y a los perros por su nombre completo para no ofender al Ratón de nuevo.
—¡Arrastro tras de mí una realidad muy larga y muy triste! —exclamó el
Ratón, dirigiéndose a Alicia y dejando escapar un suspiro.
—Desde luego, arrastras una cola larguísima —dijo Alicia, mientras echaba
una mirada admirativa a la cola del Ratón—, pero ¿por qué dices que es triste?

Y tan convencida estaba Alicia de que el Ratón se refería a su cola, que,
cuando él empezó a hablar, la historia que contó tomó en la imaginación de
