Alice buna o kadar çok güldü ki, sesini duymasınlar diye yine koruya kaçtı. Gözetlemek için yeniden çıktığı zaman, Balık-Uşak gitmişti, öteki de kapının önünde yere oturmuş sersem sersem havaya bakıyordu.

