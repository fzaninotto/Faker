Tavşan 'Ne yazık mı dedin?' diye sordu.

Alice 'Hayır, demedim' dedi. 'Bunda yazık denecek bir şey yok ki! Niçin diye sordumdu.'

Tavşan 'Kraliçe'yi tokatladı...' diye söze başlamıştı ki Alice bir kahkaha attı. Tavşan korkak bir sesle 'Aman sus' dedi, 'Sonra Kraliçe işitecek! Ha, ne diyordum, biraz geç kalmıştı, Kraliçe de dedi ki...'

O sırada Kraliçe gök gürültüsü gibi bir sesle 'Yerlerinize geçin!' diye bağırdı, herkes de birbirine çarpa çarpa dört yana koşuşmaya başladı. Ama neyse, bir iki dakika içinde yerli yerlerine geçtiler, oyun da başladı.

Alice, ömründe hiç böyle acayip kriket alanı görmemişti! Her yan tümsek, çukur doluydu! Toplar canlı kirpi, oyun sopaları da canlı flamingo kuşlarıydı; Köprüleri de, ayaklarıyla ellerini yere dayayıp yay gibi bükülen askerler oluşturuyordu.

Alice için ilk güçlük flamingosunu kullanmak oldu, hayvanın gövdesini güzelce kolunun altına sıkıştırıp bacaklarını sallandırdı, ama tam boynunu doğrultup kafası ile kirpiye vuracağı sırada, kuş başını kaldırıp suratına bakıyordu; hem bakışında öyle bir şaşkınlık vardı ki Alice kendini tutamıyor, kahkahayı koparıyordu; kuşun boynunu düzeltip tam topa vuracağı zaman bu kez de kirpinin yerinde tortop durmaktan vazgeçip, sürüne sürüne uzaklaştığını görüyordu. Üstelik, kirpisini yuvarlamak istediği yolun üstünde kesinlikle bir çukur yahut bir tümsek oluyordu; köprülük eden askerler de durmadan yer değiştiriyorlardı. Çok geçmeden Alice oyunun güçlüğünü anladı.

Oyuncular da, sıralarını beklemeden, ikide bir kavga ederek ve kirpiler senindi, benimdi diye dövüşerek, hep birden oynuyorlardı. Kısa süre içinde Kraliçe öyle öfkelendi, öyle öfkelendi ki dakikada bir tepiniyor 'Kesin kafasını şunun! Kesin kafasını bunun!' diye haykırıp duruyordu.

Alice yavaş yavaş telaşlanmaya başlamıştı; gerçi henüz Kraliçeyle aralarında bir şey olmamıştı, ama neredeyse olacağa benziyordu. 'Ya sonra?' diye düşündü, 'Sonra ne olur benim durumum? Buranın insanları kafa kesmeye pek meraklı! Nasıl olmuş da biri sağ kalabilmiş, insan ona şaşıyor!'

Artık kaçmak için çevrede bir yol arıyor. 'Acaba kimseye görünmeden sıvışabilir miyim?' diye düşünüyordu; bir de baktı, havada acayip bir şey belirmiş. Önce epeyce şaştı ama bir iki dakika sonra bunun bir gülümseme olduğunu anladı, kendi kendine 'Cheshire Kedisidir' diye söylendi. 'Çok şükür konuşacak birini buldum.' Konuşabilecek kadar ağzı ortaya çıkınca Kedi 'Nasılsın?' diye sordu.

Alice gözler de ortaya çıkıncaya kadar bekledi, sonra başını salladı. 'Kulaklarından hiç olmazsa biri görünmedikçe konuşmanın bir yararı olmaz ki' diye düşündü. Bir iki dakika sonra bütün kafa belirdi. Bunun üzerine Alice, flamingosunu yere bırakarak oyunu anlatmaya başladı; kendisini dinleyecek birini bulduğuna pek keyiflenmişti; Kedi gözüken kısımlarını, yeter bularak öylece kalmış, daha fazla belirmemişti.

Alice yakınan bir sesle 'Mızıkçılık etmeden oyun oynamıyorlar ki' dedi, 'Hem de kopardıkları kavga gürültüden insan kimin ne dediğini işitemiyor. Oyunlarında kural mural da yok, varsa bile kimsenin aldırdığı yok... Oyun araçlarının hepsinin canlı olması da insanı çileden çıkarıyor hani. Söz gelişi, topumu şimdi altından geçireceğim köprü, bakın, kalkıp alanın ta neresine gitmiş de dolaşıyor. Demin de Kraliçe'nin kirpisine vuracaktım, baktı ki benim kirpi yaklaşıyor, aldı başını kaçtı!'

Kedi yavaşça 'Kraliçe'den hoşlandın mı?' diye sordu.

Alice 'Hiç mi hiç' dedi, 'Öyle bir (tam o sırada Kraliçe'nin, hemen arkasında kendisini dinlediğini farketti, sözünü değiştiriverdi) iyi oyuncu ki, nasıl olsa o kazanacak, onun için oynamanın bir anlamı yok.'

Kraliçe gülümsedi ve yürüyüp geçti.

Kral Alice'in yanına gelerek 'Kiminle konuşuyorsun?" diye sordu; bir yandan da şaşkınlıkla Kedi'nin kafasına bakıyordu.

Alice 'Bir arkadaşımdır' dedi, 'Cheshire Kedisi; izin verirseniz takdim edeyim.'

Kral 'Suratı hiç hoşuma gitmedi ama neyse, isterse elimi öpsün' dedi.

Kedi yanıtladı 'Öpmesem daha iyi.'

Kral 'Küstahlaşma' dedi. Sonra konuşurken Alice'in arkasını siper alarak 'Hem yüzüme de öyle bakma' diye ekledi.

Alice 'Bir kedi bir krala pekâlâ bakabilir' dedi. 'Bir kitapta okumuştum, ama hangisinde, şimdi anımsamıyorum.'

Kral kararını vermiş gibi 'Herhalde ortadan kalkmalı' diyerek, o sırada yakınlardan geçen Kraliçe'ye seslendi 'Güzelim! Ne olur, şu kediyi ortadan kaldırsana!' dedi.

Kraliçe'nin büyük küçük bütün güçlükleri gidermek için bir tek yöntemi vardı. Başını çevirip bakmaya gerek bile görmeden 'Kafasını kessinler!' diye haykırdı.

Kral, dünden hazırmış gibi, 'Celladı gidip kendim getiririm' diyerek ivedi ivedi yürüyüp gitti.

Alice, uzaktan Kraliçe'nin öfkeyle çınlayan sesini işitince 'Bari gidip oyun ne durumda bir bakayım' diye düşündü. Sıralarını kaçıran üç oyuncuyu idama mahkûm ettiğini daha demin işitmişti, işlerin gidişini de hiç beğenmiyordu, oyun öyle çorbaya dönmüştü ki kendi sırasının gelip gelmediğini bile bilmiyordu. Onun için kirpisini aramaya başladı.

Kirpisi başka bir kirpiyle kavgaya tutuşmuştu; Alice'in, birini öbürüne çarptırıp kriket yapması için bu, bulunmaz bir fırsattı; ama sorun şuradaydı ki flamingosu bahçenin ta öbür ucuna gitmişti, orada uçup ağaçlardan birine konacağım diye beceriksizce kanat çırpıp duruyordu.

O, flamingosunu yakalayıp dönünceye kadar dövüş bitmiş, iki kirpi de ortadan yok olmuşlardı. Alice 'Neyse, zararı yok' diye düşündü 'Zaten bütün köprüler de bahçenin öte yanına gitmişler.' Kaçmasın diye flamingosunu koltuğunun altına sıkıştırdı ve arkadaşı Kedi ile biraz daha konuşmaya gitti.

Kedi'nin yanına geldiği zaman çevresinde epeyce bir kalabalık biriktiğini görerek şaşaladı. Kral, Kraliçe ve Cellat arasında bir tartışma oluyordu; üçü bir ağızdan konuşuyor, öbürleri de ne yapacaklarını şaşırmış, rahatsız rahatsız duruyorlardı.

Alice görünür görünmez hepsi de sorunu çözümlesin diye ona döndüler, her biri kendi düşüncesini yineliyordu, fakat gene hep bir ağızdan konuştukları için, Alice ne dediklerini güçlükle anladı.

Celladın dediğine göre; gövdesi olmayan baş kesilemezdi, ömründe hiç böyle bir şey yapmamıştı, bu yaştan sonra yapmaya da niyeti yoktu.

Kralın dediğine göre; başı olan herşey idam edilebilirdi, saçma saçma konuşmanın yeri yoktu.

Kraliçe'nin dediğine göre de; hemen o dakika bir şey yapılmayacak olursa, kim var kim yok herkesin kafası kesilecekti. (Çevredekilerin o kadar durgun ve endişeli görünmesine neden de bu son söylenen sözlerdi.)

Alice 'Kedi Düşes'indir, ona sorun daha iyi' demekten başka çare bulamadı.

Kraliçe Cellat'a dönerek, 'Hapishaneye atılmıştı' dedi 'Git de buraya getir'. Cellat ok gibi fırlayıp gidiverdi.

Cellat gider gitmez, Kedi'nin kafası yavaş yavaş kaybolmaya başladı; adam Düşes'le döndüğü zaman gözden büsbütün silinmişti. Kral ile Cellat bir aşağı bir yukarı koşarak nereye gitti diye aramaya başladılar, öbürleri de yine oyunlarına döndüler.

BÖLÜM IX. SU KAPLUMBAĞASI'NIN ÖYKÜSÜ

Düşes, 'Ah şekerim benim! Seni gördüğüme ne kadar sevindim bilmezsin' diyerek sevgiyle Alice'in koluna girdi, birlikte yürümeye başladılar.

Alice de onu böyle keyfi yerinde bulunca pek hoşnut olmuştu; 'Mutfakta rasladığım zamanki hırçınlığı biberdenmiş demek' diye düşündü. Kendi kendine 'Eğer ben Düşes olursam' dedi (bunu pek umarak söylememişti ama) 'mutfağımda biber miber bulundurmam. Çorba onsuz da pekâlâ oluyor. Belki insanları kavgacı yapan da hep biberdir.' Kendi kendine yeni kurallar bulduğu için pek hoşnut, düşünmeyi sürdürdü 'Suratsız eden sirke, huysuz eden de arpa şekeriyle buna benzer tatlılardır. Ah, insanlar bunu bilseler böyle hasislik etmezlerdi.'

Bu sırada Düşesi büsbütün unutmuştu, ta kulağının dibinde sesini işitince korkudan sıçradı. Düşes 'Şekerim, bir şey düşünüyorsun ki konuşmayı unuttun' dedi. 'Bundan çıkan ders nedir söylerdim ama unutmuşum, biraz sonra aklıma gelir.'

Alice 'Belki de bundan çıkacak ders yoktur' diyecek oldu. Düşes 'Hadi hadi, kızım' dedi 'Her şeyden alınacak bir ders vardır, yeter ki insan bilebilsin'. Böyle söylerken Alice'e daha sokuldu.

Alice Düşes'in kendisine bu kadar yaklaşmasını istemiyordu. Bir kez, çok mu çok çirkindi; ikincisi de, çenesini tam kızcağızın omuzuna dayayacak boydaydı, çenesiyse sivriydi, insana adeta batıyordu. Ama Alice kabalık etmek istemedi, onun için gücü yettiği kadar taşımayı göze aldı. 'Oyun şimdi yoluna girmiş galiba' dedi.

Düşes 'Evet girmiş' diye yanıtladı 'Bundan alınacak ders de şudur, dünyayı yürüten güç sevgidir, sevgi!'

Alice 'Birisi de herkesin kendi işine bakmasıdır, demişti' diye mırıldandı.

Düşes sivri çenesini Alice'in omuzuna büsbütün batırarak 'Eh, bu da hemen hemen aynı şey demektir' dedikten sonra ekledi, 'Bundan alınacak ders de şudur: Sen anlamaya bak, sesler kendi başlarının çaresine bakarlar.'

Alice kendi kendine 'Her şeyden bir ders çıkarmaya ne de meraklı!' diye söylendi.

Düşes, biraz durduktan sonra, 'Neden kolumu beline dolamadığıma şaşıyorsun değil mi?' dedi 'Nedeni, flamingonun huyunu pek bilmiyorum da ondan. Bir denesem mi?'

Alice bu denemeye hiç de hevesli olmadığı için çekine çekine yanıtladı 'Belki ısırır!'

Düşes 'Çok doğru' dedi. 'Flamingolar da, hardal da insanı ısırır. Bundan alınacak ders şudur: Bir türden kuşlar birlikte yaşar.'

Alice 'Ama hardal kuş değil ki' dedi.

Düşes 'Her zamanki gibi gene haklısın' dedi. 'Her şeyi ne de yerli yerince söylüyorsun!'

Alice 'Galiba bir maden' dedi.

Alice'in her sözüne evet demeye hazır olan Düşes, 'Maden ya!' dedi, 'Buralarda büyük bir hardal madeni vardır. Bundan alınacak ders de: 'Maden varsa manen yoktur'

Düşesin bu söylediklerini pek dinlememiş olan Alice 'Bildim, bildim' dedi 'hardal sebzedir. Görünüşü pek benzemez ama sebzedir'.

Düşes 'Ben de seninle aynı düşüncedeyim" dedi. 'Bundan alınacak ders de: 'Dışın nasıl görünüyorsa için de öyle olsun, yahut, daha da yalın söylenmesini istersen: .'Kendinizi herkese olduğunuz yahut olabileceğinizden farklı olmamış olduğunuz gibi gösterecek olan durumunuzdan farklı değilmiş gibi göstermeyeceğiniz sanısına kapılmayın.'

Alice terbiyeli terbiyeli 'Bunu bir yere yazsam daha iyi anlardım' dedi 'Siz söylerken pek anlayamadım.'

Düşes, hoşnut, 'Keyfim olunca söylediklerimin yanında bu hiç kalır' dedi.

Alice 'Aman kuzum' dedi, 'Bundan daha uzununu söylemek için kendinizi sıkıntıya sokmayın.'

Düşes 'A! O nasıl söz, hiç sıkıntı olurmu?' dedi 'Hem, şimdiye kadar söylediğim sözlerin hepsini sana armağan ediyorum.'

Alice 'Ucuz tarafından bir armağan!' diye düşündü. 'İyi ki yıldönümlerinde bu türden armağanler vermiyorlar!' Ama doğrusu bunları yüksek sesle söylemeye pek cesaret edemedi.

Düşes sivri küçük çenesiyle yine Alice'i dürtükleyerek 'Gene mi düşünceye daldın?' diye sordu.

Artık yavaş yavaş canı sıkılmaya başlayan Alice biraz sert bir sesle: 'Düşünmek yasak değil ya!' diye yanıtladı.

Düşes 'Domuzlara uçmak ne kadar yasak değilse, sana da düşünmek o kadar yasak değil' dedi. 'Bundan alınacak der...'

Tam, o sevgili ders sözcüğünü söylerken, Alice, Düşes'in sesinin sönüp gittiğini, kolunda duran kolunun da titrediğini fark ederek şaşırdı. Başını kaldırıp baktı ki Kraliçe kollarını kavuşturmuş, kopmaya hazırlanan bir fırtına gibi önlerine dikilmiş duruyor.

Düşes titrek, bitkin bir sesle 'Hava pek güzel Haşmetli Kraliçem!' diye söze başladı.

Kraliçe konuşurken ayağını yere vurarak 'Sana söylüyorum' diye haykırdı 'Şu dakkada buradan ya kendin defolur gidersin, yahut kafan gider! Seç ikisinden birini!'

Düşes de seçeceğini seçip bir anda ortadan kayboluverdi.

Kraliçe Alice'e 'Haydi, biz oyunumuza bakalım' dedi; Alice korkusundan tek sözcük söyleyecek durumda değildi, Kraliçe'nin peşinden ağır ağır kriket alanına doğru yürüdü.

Öteki konuklar Kraliçe'nin yokluğunu fırsat bilmişler, gölgelikte dinleniyorlardı. Onu görür görmez hemen yine oyuna başladılar. Kraliçe yalnızca, bir an gecikmenin hepsinin canlarına mal olacağını söyleyip geçti.

Oyun oynadıkları kadar Kraliçe oyuncularla kavga ederek 'Şunun kafasını uçurun!', 'Bunun kafasını uçurun!' diye haykırıp durdu. İdam yargısı çıkanlara askerler nöbetçi veriliyor, onlar da bu görevi yerine getirmek için köprülüğü bırakıyorlardı; öyle ki, yarım saat kadar sonra ortada hiç köprü kalmadı. Alice, Kral, Kraliçe'den başka öteki oyuncular da idam hükmü giymişlerdi.

Kraliçe, soluğu kesilmiş, oyunu bıraktı ve Alice'e 'Yalancı Kaplumbağa'yı gördün mü?' diye sordu.

Alice 'Hayır' diye yanıtladı. 'Zaten Yalancı Kaplumbağa nedir onu bile bilmiyorum.'

Kraliçe 'Yalancı Kaplumbağa, çorbası yapılan şeydir'(4) dedi.

Alice 'Böyle bir şeyi ömrümde ne gördüm, ne de işittim' dedi.

Kraliçe 'Hadi öyleyse' dedi 'Gel de sana öyküsünü anlatsın!'

Kraliçe'yle birlikte giderlerken Alice, Kralın mahkûm olanlara yavaşça 'Hepiniz bağışlandınız!' dediğini duydu. Kendi kendine 'İşte bu iyi doğrusu!' dedi; çünkü idama mahkûm edilenlerin çokluğu onu epeyce üzmüştü.

Az sonra güneşte derin derin uyuyan bir Ejder'in yanına geldiler. (Eğer Ejder nedir bilmiyorsanız resme bakın.) Kraliçe 'Hadi tembel, kalk!' dedi, 'Bu küçüğü al, Yalancı Kaplumbağa'ya götür de öyküsünü dinlesin! Ben dönüp o idama mahkûm ettiklerim ne olmuş bir bakayım.' Böylece, Alice'i Ejder'le yalnız bırakarak gitti. Ejder, kalkıp oturdu, gözlerini uğuşturdu. Kraliçe gözden kayboluncaya kadar arkasından baktı, sonra için için gülerek yarı Alice'e, yarı kendi kendine 'Ne tuhaf!' dedi.

Alice 'Tuhaf olan nedir?' diye sordu.

Ejder 'Ne olacak, Kraliçe' dedi. 'Hep kendi düşlemleri; yoksa kimsenin boynunun vurulduğu filan yok. Hadi, gel bakalım!'

Alice Ejder'in arkasından giderken 'Burada da herkes 'Hadi, gel' diyor. Bana bu kadar buyurulmamıştı ömrümde!' diye düşünüyordu.

Pek çok gitmemişlerdi ki, uzakta bir kayanın üstünde tek başına üzgün üzgün oturan Yalancı Kaplumbağa'yı gördüler. Daha yaklaştıkları zaman Alice onun, yüreği paralanıyormuşçasına, iç çektiğini işitti. Öyle acıdı ki zavallıya. Ejdere 'Derdi nedir?' diye sordu. Ejder'se, aşağı yukarı, demin söylediği sözcükleri yineledi: 'Hep kendi düşlemleri; bir derdi yok! Hadi gel!'

Kendilerine yaşlı gözlerle bakan, fakat hiç sesini çıkarmayan Yalancı Kaplumbağa'nın yanına gittiler. Ejder 'Bu küçük bayan senin öykünü dinlemek istiyor!' dedi.

Yalancı Kaplumbağa derinden gelen bir sesle 'anlatayım' dedi. 'İkiniz de oturun, ben öykümü bitirinceye kadar da ağzınızı açmayın!'

Oturdular; bir iki dakika kadar kimse ağız açmadı. Alice kendi kendine 'Eğer anlatmaya başlamazsa, nasıl bitirebilecek?' diye düşündü, ama gene de sabırla bekledi.

Sonunda Yalancı Kaplumbağa derin derin içini çekip söze başladı. 'Ben, bir zamanlar sahici bir Kaplumbağa'ydım.'

Bu sözleri, yalnızca Ejder'in arada bir duyulan 'Hjckrrh!' ünlemi ve Yalancı Kaplumbağa'nın sürekli hıçkırıklarıyla bozulan uzun bir sessizlik izledi. Alice az daha ayağa kalkıp 'Öykünüz pek meraklıydı, teşekkür ederim' diyecekti ama, herhalde bunun bir arkası olacak diye düşünerek yerinde oturdu, bir şey söylemedi.

Sonunda Yalancı Kaplumbağa sözünün arkasını getirebildi, şimdi biraz daha sakinleşmişti ama gene ikide bir hıçkırıyordu. 'Biz küçükken denizin dibinde, okula giderdik, öğretmenimiz de yaşlı bir Kaplumbağaydı, öyle yaşlıydı ki ona 'Tosbağa' derdik...'

Alice 'Madem karada yaşamıyordu niye Tosbağa derdiniz?' diye sordu.

Yalancı Kaplumbağa kızgın kızgın 'Tosbağa derdik, çünkü bizi okuturdu. Siz de amma sersemsiniz ha!' dedi.

Ejder de 'Bu kadar da basit bir şeyi anlamadığın için yazıklar olsun sana' diye ekledi. Sonra ikisi de sözü kesip Aliceciğe dik dik baktılar, zavallı kız utancından yerin dibine geçti.

Sonunda Ejder Yalancı Kaplumbağa'ya 'Hadi bakalım, aslanım! Akşamlara kadar uzatma!' dedi, o da öyküsünü şöyle sürdürdü:

'Evet, inanmıyorsunuz belki ama, biz denizde okula giderdik...' dedi.

Alice 'Ben inanmıyorum demedim ki!' diye sözünü kesti.

Yalancı Kaplumbağa 'Dediniz pekâlâ işte' diye yanıtladı.

Ejder, Alice'in ağzını açmasına vakit bırakmadan 'Sen çeneni kapat hele!' diye atıldı. Yalancı Kaplumbağa da öyküsünü anlatmaya koyuldu.

'Çok iyi okutulduk... Hatta, her gün okula giderdik...' dedi.

Alice 'Ben de her gün okula gidiyorum, bunda o kadar öğünecek bir şey yok ki!' dedi.

Yalancı Kaplumbağa epeyce merakla 'Ek dersleriniz de var mı?' diye sordu.

Alice 'Evet' dedi, 'Fransızca ile müzik öğrendik.'

Yalancı Kaplumbağa 'Yıkanma da öğrendiniz mi?' dedi.

Bu soru Alice'in ağırına gitmişti: 'Elbette öğrenmedik!' dedi.

Yalancı Kaplumbağa, içi rahatlayarak 'A! Öyleyse sizinki pek iyi bir okul değilmiş. Oysa bizimkinde, taksit pusulasının en altına Fransızca, müzik ve yıkanma ektir diye yazılır.'

Alice 'Ama denizin dibinde yıkanma pek gerekmez sanırım" dedi.

Yalancı Kaplumbağa içini çekerek 'Zaten durumumuz o kadar iyi değildi; ben o ek dersleri alamadım, yalnızca ana dersleri okudum" dedi.

Alice 'Hangileriydi o dersler?' diye sordu.

Yalancı Kaplumbağa 'Doğal olarak, en başta akmak yaşmak geliyor' diye yanıtladı; 'Sonra aritmetiğin çeşitli kolları, hoplamak, çığırtmak, çapıtmak ve sövmek.'

