BÖLÜM IV. TAVŞAN BİR PUSULA GÖNDERİYOR

Bu, ağır ağır geri gelen Beyaz Tavşan'dı, yürürken sanki bir şey arıyormuş gibi şaşkın şaşkın çevresine bakınıyordu. Kendi kendine şöyle mırıldandığını işitti 'Ah! Düşes! Ah! patilerim, bıyıklarım! Ah, kürküm! Beni idam ettirecek, sansarın sansar olduğu kadar kesin bu! Onları da nerede düşürdüm acaba?' Alice, hemen onun beyaz eldivenleriyle yelpazesini aradığını anladı, iyi kalpli bir kız olduğu için o da aranmaya koyuldu, ama hiçbir yerde onlardan eser yoktu, o birikintide yüzdüğünden beri her şey değişmişti galiba: O küçücük kapının ve cam masanın bulunduğu koca salon büsbütün gözden yitmişti.

Biraz sonra Beyaz Tavşan aranıp duran Alice'i fark etti ve öfkeyle bağırdı: 'Hey, Marie Ann, burada ne işin var! Hemen eve koş da bana bir çift eldivenle bir yelpaze getir! Hadi, çabuk ol!' Alice o kadar korktu ki Tavşan'a yanıldığını söylemeyi unutarak onun parmağıyla gösterdiği yana koştu.

Koşarken kendi kendine, 'Beni hizmetçi sandı' diyordu. 'Kim olduğumu öğrenince kim bilir ne şaşıracak! Ama şimdi ona eldivenlerle yelpazeyi götüreyim daha iyi... Bulabilirsem, doğallıkla.' Böyle söylenirken kapısının üstünde 'B. Tavşan' kazılmış parlak, pirinçten bir levha bulunan küçük bir evin önüne geldi. Kapıyı çalmaksızın içeri girdi ve gerçek Marie Ann'e rasgelip de eldivenlerle yelpazeyi alamadan dışarı atılmaktan korktuğu için merdivenleri ikişer ikişer çıktı.

Alice kendi kendine 'İnsanı bir tavşanın işe koşması ne tuhaf' diyordu. 'Galiba çok geçmeden Dinah da bana iş buyurmaya kalkacak.' Bunun üzerine neler olacağını gözünün önüne getirdi: 'Miss Alice! Haydi gelin de yürüyüşe hazırlanın bakayım!' 'Bir dakika dadı, geliyorum! Ama Dinah dönünceye kadar bu fare deliğini gözetlemem gerekiyor ki Fare dışarı çıkmasın' sonra sözünü şöyle sürdürdü: 'Ama acaba Dinah böyle önüne gelene buyuracak olursa onu evde tutarlar mı bilmem!'

Bu arada derli toplu küçük bir odaya gelmişti: Bu odanın penceresinin içinde bir masa, masanın üstünde de (Alice'in umduğu gibi) iki üç çift eldivenle bir yelpaze duruyordu. Bir çift eldivenle yelpazeyi aldı, tam odadan çıkıyordu ki gözüne aynanın yanında duran küçücük bir şişe ilişti. Bu şişenin üstünde 'Beni iç' yazılı bir yafta yoktu, ama Alice tıpasını açarak şişeyi ağzına dayadı. Kendi kendine, 'Ne zaman bir şey içsem yahut yesem kesinlikle acayip bir şeyler oluyor' dedi. 'Bakalım bu şişenin marifeti ne. Beni yine büyütür umarım. Çünkü bu kadar küçük kalmaktan bıktım artık.'

Gerçekten de öyle oldu, hem de umduğundan çok daha çabuk. Daha şişenin yarısını bile içmemişti ki başının tavana dayandığını duyumsadı: Boynu kırılmasın diye eğilmek zorunda kaldı. Şişeyi hemen bırakarak kendi kendine, 'Bu kadarı yeter... Daha fazla büyümesem bari... Bu durumda bile kapıdan çıkamıyorum... Keşke o kadar çok içmeseydim' dedi.

Ne yazık ki dileyeceğini dilemekte geç kalmıştı. Hâlâ büyüdükçe büyüyordu, az sonra yere diz çökmek zorunda kaldı. Bir dakika sonra artık diz çökecek yer bile kalmamıştı. Bu sefer, bir dirseğini kapıya dayayıp öteki kolunu da başının üstüne kıvırarak yerde yatmayı denedi. Hâlâ büyüyordu. Sonunda artık son çare olarak bir kolunu pencereden dışarı çıkardı, bir ayağını da ocağın bacasından yukarı doğru uzattı. 'Artık ne olursa olsun, bundan fazla bir şey yapamam; benim durumum ne olacak böyle' diye söylendi.

Alice'in talihi varmış ki, artık büyülü şişe etkisinigöstereceği kadar göstermiş, onun da büyümesi durmuştu. Ama ne de olsa duruşu pek rahatsızdı, bu odadan da hiç çıkacak gibi gözükmediği için üzgün oluşuna pek şaşmamalı.

