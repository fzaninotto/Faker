Beyaz Tavşan çabuk çabuk geçip giderken ayağının dibindeki uzun otlar hışırdadı, Korkak Fare biraz ötedeki su birikintisinden şıpır şıpır yürüdü geçti. Mart Tavşanı ile arkadaşları sonu hiç gelmeyen kahvaltılarını paylaştılar. Kraliçe'nin kulak tırmalayan sesiyle zavallı konuklarını idama mahkûm ettiğini duyar gibi oldu; bir kez daha çevresinde tabaklar çanaklar uçar dururken yavru domuz Düşes'in kucağında aksırdı; bir kez daha Ejder haykırdı, Kertenkele taş tahtasının kalemini gıcırdattı; susturulan kobaylar derinden derine ses verdiler; bunlara zavallı Yalancı Kaplumbağa'nın uzaktan uzağa duyulan hıçkırıkları karıştı.

Öylece gözleri kapalı otururken kendini gerçekten Harikalar Ülkesi'nde sandı; ama biliyordu ki gözlerini bir açsa, her şey aslındaki donuk rengini alacak: otlar rüzgârda hışırdıyor, havuz sallanan sazlara uyarak şıpırdıyor olacak; çay fincanlarının şıkırtısının yerine koyun çıngıraklarının sesi, Kraliçe'nin çığlıklarının yerine çobanın seslenişi duyulacak; yavru domuzun aksırığı, Ejder'in haykırışı, özetle bütün o acayip seslerin yerine (biliyordu ki) durmadan işleyen çiftliğin gürültüsü patırtısı gelecek! Yalancı Kaplumbağa'nın hıçkırıklarının yerini de uzaktan uzağa ineklerin böğürmesi alacak...

Sonunda, bu küçük kardeşin nasıl büyüyüp ileride kendisi gibi bir kadın olacağını düşündü. Nasıl daha olgun yaşlarda da, çocukluk çağının o saf ve sevgi dolu kalbini taşıyacağını, nasıl çevresine başka küçükleri toplayıp, onların uyanık gözlerini garip masallarla (belki de yıllar önceki Harikalar Ülkesi düşüyle) parlatacağını; kendi küçüklüğünü ve o mutlu yaz günlerini anımsayarak nasıl onların çocuk dertleriyle dertlenip çocuk sevinçleriyle sevineceğini düşündü.

