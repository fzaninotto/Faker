Uşak 'Ne istersen onu' diyerek ıslık çalmaya başladı.

Alice 'Bununla konuşmak da boşuna' diye üzgün üzgün söylendi. "Aptalın biri!" Sonra kapıyı açıp içeri girdi.

Kapı bir baştan öbür başa dumanla dolu koca bir mutfağa açılıyordu. Düşes, ortada üç ayaklı bir iskemleye oturmuş, bir bebek emziriyor, Aşçı da ocağa eğilmiş, içinde herhalde çorba bulunan bir kazanı karıştırıyordu.

Aksıracağı gelen Alice kendi kendine 'Belli ki çorbanın biberi fazla kaçmış' dedi.

Gerçekten de hava çok biberliydi, Düşes bile arada bir aksırıyordu; hele bebek, ardı arası kesilmeksizin bir aksırıyor, bir viyaklıyordu. Mutfakta aksırmayanların biri Aşçı, öbürü de ocağın yanında oturan ve sırıtmaktan ağzı kulaklarına varan Kedi'ydi.

Alice, önce söze başlamakla belki kabalık etmiş olacağından korkarak, çekine çekine Düşes'e sordu:

'Bağışlayın efendim, acaba kediniz neden böyle sırıtıyor, lütfen söyler misiniz?'

Düşes 'Cheshire kedisidir de ondan, ahmak!' diye yanıtladı.

Son sözcüğü öyle bir hırsla söylemişti ki Alice yerinden sıçradı. Ama sonra kendine değil, bebeğe söylendiğini anladı, biraz cesaretlenerek konuşmayı sürdürdü:

'Cheshire kedilerinin durmadan sırıttığını bilmiyordum, daha doğrusu, kedilerin sırıttığını işitmemiştim.'

Düşes "Hepsi de canları isterse sırıtabilirler, çoğu sırıtır da" dedi.

Sonunda bir konuşmaya girişebildiğinden pek hoşnut olan Alice, terbiyeli terbiyeli: "Ben sırıtanını hiç görmedim" dedi.

Düşes de "Öyleyse pek bir şey bilmiyormuşsun, doğrusu bu" dedi.

Alice bu yanıtın edasını hiç beğenmemişti, konuşacak başka bir konu bulmanın daha iyi olacağını düşündü. O, konuşacak bir şey bulmak için uğraşadursun, beri yanda aşçı çorba kazanını ateşten indirmiş ve eline geçirdiğini Düşes ile bebeğe fırlatmaya koyulmuştu; önce maşa ve kürek geldi, ardından bir tabak çanak yağmurudur başladı. Düşes, bunlar bir yanına çarpsa bile, aldırmıyordu. Bebek zaten öylesine viyaklıyordu ki, başına yediklerinin canını acıtıp acıtmadığını kestirmek olanaksızdı.

Alice korkusundan yerinde sıçrayıp duruyor "Kuzum ne olur, yapmayın!" diye bağırıyordu. Kocaman bir tava bebeğin burnunu sıyırıp geçerken "Ah! O güzelim burun gitti!" diye bir çığlık kopardı.

Düşes kısık bir sesle "Herkes yalnızca kendi işine baksa dünya şimdikinden çok daha hızlı dönerdi" diye homurdandı.

Alice, bilgisini biraz olsun göstermek için bir fırsat ele geçirmekten pek hoşnut "Ama bunun bir yararı olmazdı ki" dedi. "Düşünün bir kez, gece gündüz ne duruma gelirdi! Dünya kendi çevresinde yirmi saatte bir döner..."

