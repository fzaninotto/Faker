— посчастливилось ему мимоходом отрезать, вынимая что-то из брички. —
— Эй, Порфирий, — кричал Ноздрев, — принеси-ка щенка! Каков щенок! —
— продолжал он, обращаясь к Чичикову. — Краденый, ни за самого себя не
