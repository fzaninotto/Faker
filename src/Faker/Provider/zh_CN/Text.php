白光如一柄白团扇，摇摇摆摆的闪起在他房里了。 
“也终于在这里！” 
他说着，狮子似的赶快走进那房里去，但跨进里面的时候，便不见了白光的影踪，只有莽苍苍的一间旧房，和几个破书桌都没在昏暗里。他爽然的站着，慢慢的再定睛，然而白光却分明的又起来了，这回更广大，比硫黄火更白净，比朝雾更霏微，而且便在靠东墙的一张书桌下。 
