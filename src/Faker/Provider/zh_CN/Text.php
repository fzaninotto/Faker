            return parent::strlen($text);
        }
    }

    protected static function validStart($word)
    {
        return !in_array($word, static::$notBeginPunctuations);
    }

    protected static function appendEnd($text)
    {
        $text = static::fixPairPunctuation($text);

        $words = static::explode($text);

        // remove all not end punctuations
        $preventLoopTooMuchTimes = 0;
        while (in_array($words[count($words) - 1], static::$notEndPunctuations) && $preventLoopTooMuchTimes < 10) {
            array_pop($words);
            $preventLoopTooMuchTimes++;
        }

        // if the last char is not a valid punctuation, append a default one
        if (!in_array($words[count($words) - 1], static::$punctuations)) {
            $words[] = static::$defaultEndPunctuations;
        }

        return static::implode($words);
    }

    protected static function fixPairPunctuation($text)
    {
        $words = static::explode($text);

        $expectedPunctuation = array();
        $expectedPunctuationInsertPos = array();
        $insertPunctuation = array();
        $lastInsertPos = 0;
        $lastCharIsPunctuation = '';

        foreach ($words as $pos => $word) {
            if (array_key_exists($word, static::$punctuationPairs)) {
                $expectedPunctuation[] = static::$punctuationPairs[$word];
                $expectedPunctuationInsertPos[] = -1;
            } elseif (in_array($word, static::$punctuationPairs)) {
                if (!empty($expectedPunctuation) && $word === $expectedPunctuation[count($expectedPunctuation) - 1]) {
                    array_pop($expectedPunctuation);
                    array_pop($expectedPunctuationInsertPos);
                } else {
                    $insertPunctuation[$lastInsertPos] = array_search($word, static::$punctuationPairs);
                }
            } elseif (in_array($word, static::$punctuations)) {
                if (!$lastCharIsPunctuation) {
                    if (!empty($expectedPunctuationInsertPos) && $expectedPunctuationInsertPos[count($expectedPunctuationInsertPos) - 1] < 0) {
                        $expectedPunctuationInsertPos[count($expectedPunctuationInsertPos) - 1] = $pos;
                    }
                }
                $lastCharIsPunctuation = true;
            } else {
                if ($lastCharIsPunctuation) {
                    $lastInsertPos = $pos;
                }
                $lastCharIsPunctuation = false;
            }
        }

        foreach ($expectedPunctuationInsertPos as $index => $pos) {
            $insertPunctuation[$pos] = $expectedPunctuation[$index];
        }

        foreach ($insertPunctuation as $pos => $punctuation) {
            $words[$pos] = $punctuation . $words[$pos];
        }

        return static::implode($words);
    }

    /**
     * UTF-8 strlen
     *
     * @see https://en.wikipedia.org/wiki/UTF-8#Description
     *
     * @param string $text
     *
     * @return int
     */
    protected static function utf8Strlen($text)
    {
        $count = 0;
        for ($i = 0, $len = strlen($text); $i < $len; $i++) {
            $char = ord($text[$i]);
            if ($char <= 0x7F) {
                // 00000000 - 01111111
                // ASCII
                $count++;
            } elseif ($char <= 0xBF) {
                // 10000000 - 10111111
                // Not the first byte. Do nothing.
            } elseif ($char <= 0xFD) {
                // 11000000 - 11011111
                // 11100000 - 11101111
                // 11110000 - 11110111
                // 11111000 - 11111011
                // 11111100 - 11111101
                // The first byte of UTF-8 character
                $count++;
            } else {
                // Unexpected first byte of UTF-8 character
            }
        }
        return $count;
    }

    /**
     * Get UTF-8 string characters array
     *
     * TODO: this function is not used
     *
     * @see https://en.wikipedia.org/wiki/UTF-8#Description
     *
     * @param string $text
     *
     * @return array
     */
    protected static function utf8Chars($text)
    {
        $chars = array();
        for ($i = 0, $len = strlen($text); $i < $len; $i++) {
            $char = ord($text[$i]);
            if ($char <= 0x7F) {
                // 00000000 - 01111111
                // ASCII
                $chars[] = $text[$i];
            } elseif ($char <= 0xBF) {
                // 10000000 - 10111111
                // Not the first byte. Do nothing.
                $chars[count($chars) - 1] = $text[$i];
            } elseif ($char <= 0xFD) {
                // 11000000 - 11111101

                // 11000000 - 11011111
                $temp = $text[$i];
                $temp .= $text[++$i];
                // 11100000 - 11101111
                if ($char > 0xDF) {
                    $temp .= $text[++$i];
                }
                // 11110000 - 11110111
                if ($char > 0xEF) {
                    $temp .= $text[++$i];
                }
                // 11111000 - 11111011
                if ($char > 0xF7) {
                    $temp .= $text[++$i];
                }
                // 11111100 - 11111101
                if ($char > 0xFB) {
                    $temp .= $text[++$i];
                }
                $chars[] = $temp;
            } else {
                // Unexpected first byte of UTF-8 character
            }
        }
        return $chars;
    }
