    /**
     * 根据 $maxNbChars 返回鲁迅的名言警句
     *
     * @example '以人为鉴，明白非常，是使人能够反省的妙法。哀其不幸，怒其不争。'
     * @param integer $maxNbChars Maximum number of characters the text should contain (minimum: 10)
     * @param integer $indexSize  invalid
     * @return string
     */
    public function realText($maxNbChars = 200, $indexSize = 2)
    {
        if ($maxNbChars < 10) {
            throw new \InvalidArgumentException('maxNbChars must be at least 10');
        }

        // filter out less maxNbChars sentences.
        $words = array_filter(static::$baseText, function ($word) use ($maxNbChars) {
            return static::strlen($word) < $maxNbChars;
        });

        $result = array();
        $resultLength = 0;

        while ($resultLength < $maxNbChars) {
            // fetch a random word to append
            $word = static::randomElement($words);

            $result[] = $word;
            $resultLength += static::strlen($word);
        }

        // remove the element that caused the text to overflow
        array_pop($result);

        // build result
        $result = static::implode($result);

        return $result;
    }
