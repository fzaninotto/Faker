“这回又完了！” 
他大吃一惊，直跳起来，分明就在耳边的话，回过头去却并没有什么人，仿佛又听得嗡的敲了一声磬，自己的嘴也说道： 
“这回又完了！” 
他忽而举起一只手来，屈指计数着想，十一，十三回，连今年是十六回，竟没有一个考官懂得文章，有眼无珠，也是可怜的事，便不由嘻嘻的失了笑。然而他愤然了，蓦地从书包布底下抽出誊真的制艺和试帖⑶来，拿着往外走，刚近房门，却看见满眼都明亮，连一群鸡也正在笑他，便禁不住心头突突的狂跳，只好缩回里面了。 
