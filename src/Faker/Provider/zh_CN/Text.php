赵家遭抢之后，未庄人大抵很快意而且恐慌，阿Q也很快意而且恐慌。但四天之后，阿Q在半夜里忽被抓进县城里去了。那时恰是暗夜，一队兵，一队团丁，一队员警，五个侦探，悄悄地到了未庄，乘昏暗围住土穀祠，正对门架好机关枪；然而阿Q不衝出。许多时没有动静，把总焦急起来了，悬了二十千的赏，纔有两个团丁冒了险，逾垣进去，里应外合，一拥而入，将阿Q抓出来；直待擒出祠外面的机关枪左近，他纔有些清醒了。
到进城，已经是正午，阿Q见自己被搀进一所破衙门，转了五六个弯，便推在一间小屋里。他刚刚一跄踉，那用整株的木料做成的栅栏门便跟著他的脚跟阖上了，其馀的三面都是牆壁，仔细看时，屋角上还有两个人。
阿Q虽然有些忐忑，却并不很苦闷，因为他那土穀祠里的卧室，也并没有比这间屋子更高明。那两个也仿佛是乡下人，渐渐和他兜搭起来了，一个说是举人老爷要追他祖父欠下来的陈租，一个不知道为了什么事。他们问阿Q，阿Q爽利的答道，“因为我想造反。”
他下半天便又被抓出栅栏门去了，到得大堂，上面坐著一个满头剃得精光的老头子。阿Q疑心他是和尚，但看见下麵站著一排兵，两旁又站著十几个长衫人物，也有满头剃得精光像这老头子的，也有将一尺来长的头髮披在背后像那假洋鬼子的，都是一脸横肉，怒目而视的看他；他便知道这人一定有些来历，膝关节立刻自然而然的宽鬆，便跪了下去了。
“站著说！不要跪！”长衫人物都吆喝说。
阿Q虽然似乎懂得，但总觉得站不住，身不由己的蹲了下去，而且终于趁势改为跪下了。
“奴隶性！……”长衫人物又鄙夷似的说，但也没有叫他起来。
“你从实招来罢，免得吃苦。我早都知道了。招了可以放你。”那光头的老头子看定了阿Q的脸，沉静的清楚的说。
“招罢！”长衫人物也大声说。
“我本来要……来投……”阿Q胡里胡涂的想了一通，这纔断断续续的说。
“那么，为什么不来的呢？”老头子和气的问。
“假洋鬼子不准我！”
“胡说此刻说，也迟了。现在你的同党在那里？”
“什么？……
“那一晚打劫赵家的一伙人。”
“他们没有来叫我。他们自己搬走了。”阿Q提起来便愤愤。
“走到那里去了呢？说出来便放你了。”老头子更和气了。
“我不知道，……他们没有来叫我……”
然而老头子使了一个眼色，阿Q便又被抓进栅栏门里了。他第二次抓出栅栏门，是第二天的上午。
大堂的情形都照旧。上面仍然坐著光头的老头子，阿Q也仍然下了跪。
老头子和气的问道，“你还有什么话说么？”
阿Q一想，没有话，便回答说，“没有。”
于是一个长衫人物拿了一张纸，并一支笔送到阿Q的面前，要将笔塞在他手里。阿Q这时很吃惊，几乎“魂飞魄散”了：因为他的手和笔相关，这回是初次。他正不知怎样拿；那人却又指著一处地方教他画花押。
“我……我……不认得字。”阿Q一把抓住了笔，惶恐而且惭愧的说。
“那么，便宜你，画一个圆圈！”
阿Q要画圆圈了，那手捏著笔却只是抖。于是那人替他将纸铺在地上，阿Q伏下去，使尽了平生的力气画圆圈。他生怕被人笑话，立志要画得圆，但这可恶的笔不但很沉重，并且不听话，刚刚一抖一抖的几乎要合缝，却又向外一耸，画成瓜子模样了。
阿Q正羞愧自己画得不圆，那人却不计较，早已掣了纸笔去，许多人又将他第二次抓进栅栏门。
他第二次进了栅栏，倒也并不十分懊恼。他以为人生天地之间，大约本来有时要抓进抓出，有时要在纸上画圆圈的，惟有圈而不圆，却是他“行状”上的一个污点。但不多时也就释然了，他想：孙子纔画得很圆的圆圈呢。于是他睡著了。
然而这一夜，举人老爷反而不能睡：他和把总呕了气了。举人老爷主张第一要追赃，把总主张第一要示众。把总近来很不将举人老爷放在眼里了，拍案打凳的说道，“惩一儆百！你看，我做革命党还不上二十天，抢案就是十几件，全不破案，我的面子在那里？破了案，你又来迂。不成！这是我管的！”举人老爷窘急了，然而还坚持，说是倘若不追赃，他便立刻辞了帮办民政的职务。而把总却道，“请便罢！”于是举人老爷在这一夜竟没有睡，但幸第二天倒也没有辞。
阿Q第三次抓出栅栏门的时候，便是举人老爷睡不著的那一夜的明天的上午了。他到了大堂，上面还坐著照例的光头老头子；阿Q也照例的下了跪。
老头子很和气的问道，“你还有什么话么？”
阿Q一想，没有话，便回答说，“没有。”
许多长衫和短衫人物，忽然给他穿上一件洋布的白背心，上面有些黑字。阿Q很气苦：因为这很像是带孝，而带孝是晦气的。然而同时他的两手反缚了，同时又被一直抓出衙门外去了。
阿Q被抬上了一辆没有蓬的车，几个短衣人物也和他同坐在一处。这车立刻走动了，前面是一班背著洋炮的兵们和团丁，两旁是许多张著嘴的看客，后面怎样，阿Q没有见。但他突然觉到了：这岂不是去杀头么？他一急，两眼发黑，耳朵里喤的一声，似乎发昏了。然而他又没有全发昏，有时虽然著急，有时却也泰然；他意思之间，似乎觉得人生天地间，大约本来有时也未免要杀头的。
他还认得路，于是有些诧异了：怎么不向著法场走呢？他不知道这是在游街，在示众。但即使知道也一样，他不过便以为人生天地间，大约本来有时也未免要游街要示众罢了
他省悟了，这是绕到法场去的路，这一定是“嚓”的去杀头。他惘惘的向左右看，全跟著马蚁似的人，而在无意中，却在路旁的人丛中发见了一个吴妈。很久违，伊原来在城里做工了。阿Q忽然很羞愧自己没志气：竟没有唱几句戏。他的思想仿佛旋风似的在脑里一迴旋：《小孤孀上坟》欠堂皇，《龙虎斗》里的“悔不该……”也太乏，还是“手执钢鞭将你打”罢。他同时想手一扬，纔记得这两手原来都捆著，于是“手执钢鞭”也不唱了。
“过了二十年又是一个……”阿Q在百忙中，“无师自通”的说出半句从来不说的话。
“好！！！”从人丛里，便发出豺狼的嗥叫一般的声音来。
车子不住的前行，阿Q在喝采声中，轮转眼睛去看吴妈，似乎伊一向并没有见他，却只是出神的看著兵们背上的洋炮。
阿Q于是再看那些喝采的人们
这刹那中，他的思想又仿佛旋风似的在脑里一迴旋了。四年之前，他曾在山脚下遇见一隻饿狼，永是不近不远的跟定他，要吃他的肉。他那时吓得几乎要死，幸而手里有一柄斫柴刀，纔得仗这壮了胆，支持到未庄；可是永远记得那狼眼睛，又凶又怯，闪闪的像两颗鬼火，似乎远远的来穿透了他的皮肉。而这回他又看见从来没有见过的更可怕的眼睛了，又钝又锋利，不但已经咀嚼了他的话，并且还要咀嚼他皮肉以外的东西，永是不近不远的跟他走。
这些睛们似乎连成一气，已经在那里咬他的灵魂。
“救命，……”
然而阿Q没有说。他早就两眼发黑，耳朵里嗡的一声，觉得全身仿佛微尘似的迸散了。
至于当时的影响，最大的倒反在举人老爷，因为终于没有追赃，他全家都号啕了。其次是赵府，非特秀才因为上城去报官，被不好的革命党剪了辫子，而且又破费了二十千的赏钱，所以全家也号啕了。从这一天以来，他们便渐渐的都发生了遗老的气味。
至于舆论，在未庄是无异议，自然都说阿Q坏，被枪毙便是他的坏的证据：不坏又何至于被枪毙呢？而城里的舆论却不佳，他们多半不满足，以为枪毙并无杀头这般好看；而且那是怎样的一个可笑的死囚呵，游了那么久的街，竟没有唱一句戏：他们白跟一趟了。
