“然而几个人旣然起来，你不能说决没有毁坏这铁屋的希望。”
是的，我虽然自有我的确信，然而说到希望，却是不能抹杀的，因爲希望是在于将来，决不能以我之必无的证明，来折服了他之所谓可有，于是我终于答应他也做文章了，这便是最初的一篇《狂人日记》。从此以后，便一发而不可收，每写些小说模样的文章，以敷衍朋友们的嘱托，积久就有了十馀篇。
在我自己，本以爲现在是已经并非一个切迫而不能已于言的人了，但或者也还未能忘怀于当日自己的寂寞的悲哀罢，所以有时候仍不免呐喊几声，聊以慰藉那在寂寞里奔驰的猛士，使他不惮于前驱。至于我的喊声是勇猛或是悲哀，是可憎或是可笑，那倒是不暇顾及的；但旣然是呐喊，则当然须听将令的了，所以我往往不恤用了曲笔，在《药》的瑜儿的坟上平空添上一个花环，在《明天》里也不敍单四嫂子竟没有做到看见儿子的梦，因爲那时的主将是不主张消极的。至于自己，却也并不愿将自以爲苦的寂寞，再来传染给也如我那年青时候似的正做著好梦的青年。
