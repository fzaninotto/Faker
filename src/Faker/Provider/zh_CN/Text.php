别家的炊烟早消歇了，碗筷也洗过了，而陈士成还不去做饭。寓在这里的杂姓是知道老例的，凡遇到县考的年头，看见发榜后的这样的眼光，不如及早关了门，不要多管事。最先就绝了人声，接着是陆续的熄了灯火，独有月亮，却缓缓的出现在寒夜的空中。 
