“你钞了这些有什么用？”有一夜，他翻著我那古碑的钞本，发了研究的质问了。“没有什么用。”“那么，你钞他是什么意思呢？”“没有什么意思。”“我想，你可以做点文章……”
我懂得他的意思了，他们正办《新青年》，然而那时仿佛不特没有人来赞同，并且也还没有人来反对，我想，他们许是感到寂寞了，但是说：
