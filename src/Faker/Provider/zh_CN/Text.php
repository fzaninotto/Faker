待三个萝卜吃完时，他已经打定了进城的主意了。

第六章　从中兴到末路

在未庄再看见阿Q出现的时候，是刚过了这年的中秋。人们都惊异，说是阿Q回来了，于是又回上去想道，他先前那里去了呢？阿Q前几回的上城，大抵早就兴高采烈的对人说，但这一次却并不，所以也没有一个人留心到。他或者也曾告诉过管土谷祠的老头子，然而未庄老例，只有赵太爷、钱太爷和秀才大爷上城才算一件事。假洋鬼子尚且不足数，何况是阿Q：因此老头子也就不替他宣传，而未庄的社会上也就无从知道了。

但阿Q这回的回来，却与先前大不同，确乎很值得惊异。天色将黑，他睡眼蒙胧的在酒店门前出现了，他走近柜台，从腰间伸出手来，满把是银的和铜的，在柜上一扔说，“现钱！打酒来！”穿的是新夹袄，看去腰间还挂著一个大搭连，沉钿钿的将裤带坠成了很弯很弯的弧线。未庄老例，看见略有些醒目的人物，是与其慢也宁敬的，现在虽然明知道是阿Q，但因为和破夹袄的阿Q有些两样了，古人云，“士别三日便当刮目相待”，所以堂倌，掌柜，酒客，路人，便自然显出一种凝而且敬的形态来。掌柜既先之以点头，又继之以谈话：

“豁，阿Q，你回来了！”

“回来了。”

“发财发财，你是——在……”

“上城去了！”

这一件新闻，第二天便传遍了全未庄。人人都愿意知道现钱和新夹袄的阿Q的中兴史，所以在酒店里，茶馆里，庙檐下，便渐渐的探听出来了。这结果，是阿Q得了新敬畏。

据阿Q说，他是在举人老爷家里帮忙。这一节，听的人都肃然了。这老爷本姓白，但因为合城里只有他一个举人，所以不必再冠姓，说起举人来就是他。这也不独在未庄是如此，便是一百里方圆之内也都如此，人们几乎多以为他的姓名就叫举人老爷的了。在这人的府上帮忙，那当然是可敬的。但据阿Q又说，他却不高兴再帮忙了，因为这举人老爷实在太“妈妈的”了。这一节，听的人都叹息而且快意，因为阿Q本不配在举人老爷家里帮忙，而不帮忙是可惜的。

据阿Q说，他的回来，似乎也由于不满意城里人，这就在他们将长凳称为条凳，而且煎鱼用葱丝，加以最近观察所得的缺点，是女人的走路也扭得不很好。然而也偶有大可佩服的地方，即如未庄的乡下人不过打三十二张的竹牌，只有假洋鬼子能够叉“麻酱”，城里却连小乌龟子都叉得精熟的。什么假洋鬼子，只要放在城里的十几岁的小乌龟子的手里，也就立刻是“小鬼见阎王”。这一节，听的人都赧然了。

“你们可看见过杀头么？”阿Q说，“咳，好看。杀革命党。唉，好看好看，……”他摇摇头，将唾沫飞在正对面的赵司晨的脸上。这一节，听的人都凛然了。但阿Q又四面一看，忽然扬起右手，照着伸长脖子听得出神的王胡的后项窝上直劈下去道：

“嚓！”

王胡惊得一跳，同时电光石火似的赶快缩了头，而听的人又都悚然而且欣然了。从此王胡瘟头瘟脑的许多日，并且再不敢走近阿Q的身边；别的人也一样。

阿Q这时在未庄人眼睛里的地位，虽不敢说超过赵太爷，但谓之差不多，大约也就没有什么语病的了。

然而不多久，这阿Q的大名忽又传遍了未庄的闺中。虽然未庄只有钱赵两姓是大屋，此外十之九都是浅闺，但闺中究竟是闺中，所以也算得一件神异。女人们见面时一定说，邹七嫂在阿Q那里买了一条蓝绸裙，旧固然是旧的，但只化了九角钱。还有赵白眼的母亲，——一说是赵司晨的母亲，待考，——也买了一件孩子穿的大红洋纱衫，七成新，只用三百大钱九二串。于是伊们都眼巴巴的想见阿Q，缺绸裙的想问他买绸裙，要洋纱衫的想问他买洋纱衫，不但见了不逃避，有时阿Q已经走过了，也还要追上去叫住他，问道：

“阿Q，你还有绸裙么？没有？纱衫也要的，有罢？”

后来这终于从浅闺传进深闺里去了。因为邹七嫂得意之馀，将伊的绸裙请赵太太去鉴赏，赵太太又告诉了赵太爷而且着实恭维了一番。赵太爷便在晚饭桌上，和秀才大爷讨论，以为阿Q实在有些古怪，我们门窗应该小心些；但他的东西，不知道可还有什么可买，也许有点好东西罢。加以赵太太也正想买一件价廉物美的皮背心。于是家族决议，便托邹七嫂即刻去寻阿Q，而且为此新辟了第三种的例外：这晚上也姑且特准点油灯。

油灯干了不少了，阿Q还不到。赵府的全眷都很焦急，打着呵欠，或恨阿Q太飘忽，或怨邹七嫂不上紧。赵太太还怕他因为春天的条件不敢来，而赵太爷以为不足虑：因为这是“我”去叫他的。果然，到底赵太爷有见识，阿Q终于跟着邹七嫂进来了。

“他只说没有没有，我说你自己当面说去，他还要说，我说……”邹七嫂气喘吁吁的走着说。

“太爷！”阿Q似笑非笑的叫了一声，在檐下站住了。

“阿Q，听说你在外面发财，”赵太爷踱开去，眼睛打量着他的全身，一面说。“那很好，那很好的。这个，……听说你有些旧东西，……可以都拿来看一看，……这也并不是别的，因为我倒要……”

“我对邹七嫂说过了。都完了。”

“完了？”赵太爷不觉失声的说，“那里会完得这样快呢？”

“那是朋友的，本来不多。他们买了些，……”

“总该还有一点罢。”

“现在，只剩了一张门幕了。”

“就拿门幕来看看罢。”赵太太慌忙说。

“那么，明天拿来就是，”赵太爷却不甚热心了。“阿Q，你以后有什么东西的时候，你尽先送来给我们看，……”

“价钱决不会比别家出得少！”秀才说。秀才娘子忙一瞥阿Q的脸，看他感动了没有。

“我要一件皮背心。”赵太太说。

阿Q虽然答应着，却懒洋洋的出去了，也不知道他是否放在心上。这使赵太爷很失望，气愤而且担心，至于停止了打呵欠。秀才对于阿Q的态度也很不平，于是说，这忘八蛋要提防，或者不如吩咐地保，不许他住在未庄。但赵太爷以为不然，说这也怕要结怨，况且做这路生意的大概是“老鹰不吃窝下食”，本村倒不必担心的；只要自己夜里警醒点就是了。秀才听了这“庭训”，非常之以为然，便即刻撤销了驱逐阿Q的提议，而且叮嘱邹七嫂，请伊千万不要向人提起这一段话。

但第二日，邹七嫂便将那蓝裙去染了皂，又将阿Q可疑之点传扬出去了，可是确没有提起秀才要驱逐他这一节。然而这已经于阿Q很不利。最先，地保寻上门了，取了他的门幕去，阿Q说是赵太太要看的，而地保也不还并且要议定每月的孝敬钱。其次，是村人对于他的敬畏忽而变相了，虽然还不敢来放肆，却很有远避的神情，而这神情和先前的防他来“嚓”的时候又不同，颇混著“敬而远之”的分子了。

只有一班闲人们却还要寻根究底的去探阿Q的底细。阿Q也并不讳饰，傲然的说出他的经验来。从此他们才知道，他不过是一个小脚色，不但不能上墙，并且不能进洞，只站在洞外接东西。有一夜，他刚才接到一个包，正手再进去，不一会，只听得里面大嚷起来，他便赶紧跑，连夜爬出城，逃回未庄来了，从此不敢再去做。然而这故事却于阿Q更不利，村人对于阿Q的“敬而远之”者，本因为怕结怨，谁料他不过是一个不敢再偷的偷儿呢？这实在是“斯亦不足畏也矣”。

第七章　革命

宣统三年九月十四日——即阿Q将搭连卖给赵白眼的这一天——三更四点，有一只大乌篷船到了赵府上的河埠头。这船从黑魆魆中荡来，乡下人睡得熟，都没有知道；出去时将近黎明，却很有几个看见的了。据探头探脑的调查来的结果，知道那竟是举人老爷的船！

那船便将大不安载给了未庄，不到正午，全村的人心就很动摇。船的使命，赵家本来是很秘密的，但茶坊酒肆里却都说，革命党要进城，举人老爷到我们乡下来逃难了。惟有邹七嫂不以为然，说那不过是几口破衣箱，举人老爷想来寄存的，却已被赵太爷回复转去。其实举人老爷和赵秀才素不相能，在理本不能有“共患难”的情谊，况且邹七嫂又和赵家是邻居，见闻较为切近，所以大概该是伊对的。

然而谣言很旺盛，说举人老爷虽然似乎没有亲到，却有一封长信，和赵家排了“转折亲”。赵太爷肚里一轮，觉得于他总不会有坏处，便将箱子留下了，现就塞在太太的床底下。至于革命党，有的说是便在这一夜进了城，个个白盔白甲：穿着崇正皇帝的素。

阿Q的耳朵里，本来早听到过革命党这一句话，今年又亲眼见过杀掉革命党。但他有一种不知从那里来的意见，以为革命党便是造反，造反便是与他为难，所以一向是“深恶而痛绝之”的。殊不料这却使百里闻名的举人老爷有这样怕，于是他未免也有些“神往”了，况且未庄的一群鸟男女的慌张的神情，也使阿Q更快意。

“革命也好罢，”阿Q想，“革这伙妈妈的的命，太可恶！太可恨！……便是我，也要投降革命党了。”

阿Q近来用度窘，大约略略有些不平；加以午间喝了两碗空肚酒，愈加醉得快，一面想一面走，便又飘飘然起来。不知怎么一来，忽而似乎革命党便是自己，未庄人却都是他的俘虏了。他得意之馀，禁不住大声的嚷道：

“造反了！造反了！”

未庄人都用了惊惧的眼光对他看。这一种可怜的眼光，是阿Q从来没有见过的，一见之下，又使他舒服得如六月里喝了雪水。他更加高兴的走而且喊道：

“好，……我要什么就是什么，我欢喜谁就是谁。得得，锵锵！悔不该，酒醉错斩了郑贤弟，悔不该，呀呀呀……得得，锵锵，得，锵令锵！我手执钢鞭将你打……”

赵府上的两位男人和两个真本家，也正站在大门口论革命。阿Q没有见，昂了头直唱过去。

“得得，……”

“老Q，”赵太爷怯怯的迎著低声的叫。

“锵锵，”阿Q料不到他的名字会和“老”字联结起来，以为是一句别的话，与己无干，只是唱。“得，锵，锵令锵，锵！”

“老Q。”

“悔不该……”

“阿Q！”秀才只得直呼其名了。

阿Q这才站住，歪著头问道，“什么？”

“老Q，……现在……”赵太爷却又没有话，“现在……发财么？”

“发财？自然。要什么就是什么……”

“阿……Q哥，像我们这样穷朋友是不要紧的……”赵白眼惴惴的说，似乎想探革命党的口风。

“穷朋友？你总比我有钱。”阿Q说著自去了。

大家都怃然，没有话。赵太爷父子回家，晚上商量到点灯。赵白眼回家，便从腰间扯下搭连来，交给他女人藏在箱底里。

阿Q飘飘然的飞了一通，回到土谷祠，酒已经醒透了。这晚上，管祠的老头子也意外的和气，请他喝茶；阿Q便向他要了两个饼，吃完之后，又要了一支点过的四两烛和一个树烛台，点起来，独自躺在自己的小屋里。他说不出的新鲜而且高兴，烛火像元夜似的闪闪的跳，他的思想也迸跳起来了：

“造反？有趣，……来了一阵白盔白甲的革命党，都拿着板刀，钢鞭，炸弹，洋炮，三尖两刃刀，钩镰枪，走过土谷祠，叫道，‘阿Q！同去同去！’于是一同去。……

“这时未庄的一伙鸟男女才好笑哩，跪下叫道，‘阿Q，饶命！’谁听他！第一个该死的是小D和赵太爷，还有秀才，还有假洋鬼子，……留几条么？王胡本来还可留，但也不要了。……

“东西，……直走进去打开箱子来：元宝，洋钱，洋纱衫，……秀才娘子的一张宁式床先搬到土谷祠，此外便摆了钱家的桌椅，——或者也就用赵家的罢。自己是不动手的了，叫小D来搬，要搬得快，搬得不快打嘴巴。……

“赵司晨的妹子真丑。邹七嫂的女儿过几年再说。假洋鬼子的老婆会和没有辫子的男人睡觉，吓，不是好东西！秀才的老婆是眼胞上有疤的。……吴妈长久不见了，不知道在那里，——可惜脚太大。”

阿Q没有想得十分停当，已经发了鼾声，四两烛还只点去了小半寸，红焰焰的光照着他张开的嘴。

“荷荷！”阿Q忽而大叫起来，抬了头仓皇的四顾，待到看见四两烛，却又倒头睡去了。

第二天他起得很迟，走出街上看时，样样都照旧。他也仍然肚饿，他想着，想不起什么来；但他忽而似乎有了主意了，慢慢的跨开步，有意无意的走到静修庵。

庵和春天时节一样静，白的墙壁和漆黑的门。他想了一想，前去打门，一只狗在里面叫。他急急拾了几块断砖，再上去较为用力的打，打到黑门上生出许多麻点的时候，才听得有人来开门。

阿Q连忙捏好砖头，摆开马步，准备和黑狗来开战。但庵门只开了一条缝，并无黑狗从中冲出，望进去只有一个老尼姑。

“你又来什么事？”伊大吃一惊的说。

“革命了……你知道？……”阿Q说得很含糊。

“革命革命，革过一革的，……你们要革得我们怎么样呢？”老尼姑两眼通红的说。

“什么？……”阿Q诧异了。

“你不知道，他们已经来革过了！”

“谁？……”阿Q更其诧异了。

“那秀才和洋鬼子！”

阿Q很出意外，不由的一错愕；老尼姑见他失了锐气，便飞速的关了门，阿Q再推时，牢不可开，再打时，没有回答了。

那还是上午的事。赵秀才消息灵，一知道革命党已在夜间进城，便将辫子盘在顶上，一早去拜访那历来也不相能的钱洋鬼子。这是“咸与维新”的时候了，所以他们便谈得很投机，立刻成了情投意合的同志，也相约去革命。他们想而又想，才想出静修庵里有一块“皇帝万岁万万岁”的龙牌，是应该赶紧革掉的，于是又立刻同到庵里去革命。因为老尼姑来阻挡，说了三句话，他们便将伊当作满政府，在头上很给了不少的棍子和栗凿。尼姑待他们走后，定了神来检点，龙牌固然已经碎在地上了，而且又不见了观音娘娘座前的一个宣德炉。

这事阿Q后来才知道。他颇悔自己睡着，但也深怪他们不来招呼他。他又退一步想道：

“难道他们还没有知道我已经投降了革命党么？”

第八章　不准革命

未庄的人心日见其安静了。据传来的消息，知道革命党虽然进了城，倒还没有什么大异样。知县大老爷还是原官，不过改称了什么，而且举人老爷也做了什么——这些名目，未庄人都说不明白——官，带兵的也还是先前的老把总。只有一件可怕的事是另有几个不好的革命党夹在里面捣乱，第二天便动手剪辫子，听说那邻村的航船七斤便著了道儿，弄得不像人样子了。但这却还不算大恐怖，因为未庄人本来少上城，即使偶有想进城的，也就立刻变了计，碰不著这危险。阿Q本也想进城去寻他的老朋友，一得这消息，也只得作罢了。

但未庄也不能说是无改革。几天之后，将辫子盘在顶上的逐渐增加起来了，早经说过，最先自然是茂才公，其次便是赵司晨和赵白眼，后来是阿Q。倘在夏天，大家将辫子盘在头顶上或者打一个结，本不算什么稀奇事，但现在是暮秋，所以这“秋行夏令”的情形，在盘辫家不能不说是万分的英断，而在未庄也不能说无关于改革了。

赵司晨脑后空荡荡的走来，看见的人大嚷说，

“豁，革命党来了！”

阿Q听到了很羡慕。他虽然早知道秀才盘辫的大新闻，但总没有想到自己可以照样做，现在看见赵司晨也如此，才有了学样的意思，定下实行的决心。他用一支竹筷将辫子盘在头顶上，迟疑多时，这才放胆的走去。

他在街上走，人也看他，然而不说什么话，阿Q当初很不快，后来便很不平。他近来很容易闹脾气了；其实他的生活，倒也并不比造反之前反艰难，人见他也客气，店铺也不说要现钱。而阿Q总觉得自己太失意：既然革了命，不应该只是这样的。况且有一回看见小D，愈使他气破肚皮了。

小D也将辫子盘在头顶上了，而且也居然用一支竹筷。阿Q万料不到他也敢这样做，自己也决不准他这样做！小D是什么东西呢？他很想即刻揪住他，拗断他的竹筷，放下他的辫子，并且批他几个嘴巴，聊且惩罚他忘了生辰八字，也敢来做革命党的罪。但他终于饶放了，单是怒目而视的吐一口唾沫道“呸！”

这几日里，进城去的只有一个假洋鬼子。赵秀才本也想靠著寄存箱子的渊源，亲身去拜访举人老爷的，但因为有剪辫的危险，所以也中止了。他写了一封“黄伞格”的信，托假洋鬼子带上城，而且托他给自己绍介绍介，去进自由党。假洋鬼子回来时，向秀才讨还了四块洋钱，秀才便有一块银桃子挂在大襟上了；未庄人都惊服，说这是柿油党的顶子，抵得一个翰林；赵太爷因此也骤然大阔，远过于他儿子初隽秀才的时候，所以目空一切，见了阿Q，也就很有些不放在眼里了。

阿Q正在不平，又时时刻刻感著冷落，一听得这银桃子的传说，他立即悟出自己之所以冷落的原因了：要革命，单说投降，是不行的；盘上辫子，也不行的；第一著仍然要和革命党去结识。他生平所知道的革命党只有两个，城里的一个早已“嚓”的杀掉了，现在只剩了一个假洋鬼子。他除却赶紧去和假洋鬼子商量之外，再没有别的道路了。

钱府的大门正开着，阿Q便怯怯的躄进去。他一到里面，很吃了惊，只见假洋鬼子正站在院子的中央，一身乌黑的大约是洋衣，身上也挂著一块银桃子，手里是阿Q曾经领教过的棍子，已经留到一尺多长的辫子都拆开了披在肩背上，蓬头散发的像一个刘海仙。对面挺直的站着赵白眼和三个闲人，正在必恭必敬的听说话。

阿Q轻轻的走近了，站在赵白眼的背后，心里想招呼，却不知道怎么说才好：叫他假洋鬼子固然是不行的了，洋人也不妥，革命党也不妥，或者就应该叫洋先生了罢。

洋先生却没有见他，因为白着眼睛讲得正起劲：

“我是性急的，所以我们见面，我总是说：洪哥！我们动手罢！他却总说道No！——这是洋话，你们不懂的。否则早已成功了。然而这正是他做事小心的地方。他再三再四的请我上湖北，我还没有肯。谁愿意在这小县城里做事情。……”

“唔，……这个……”阿Q候他略停，终于用十二分的勇气开口了，但不知道因为什么，又并不叫他洋先生。

听着说话的四个人都吃惊的回顾他。洋先生也才看见：

“什么？”

“我……”

“出去！”

“我要投……”

“滚出去！”洋先生扬起哭丧棒来了。

赵白眼和闲人们便都吆喝道：“先生叫你滚出去，你还不听么！”

阿Q将手向头上一遮，不自觉的逃出门外；洋先生倒也没有追。他快跑了六十多步，这才慢慢的走，于是心里便涌起了忧愁：洋先生不准他革命，他再没有别的路；从此决不能望有白盔白甲的人来叫他，他所有的抱负，志向，希望，前程，全被一笔勾销了。至于闲人们传扬开去，给小D王胡等辈笑话，倒是还在其次的事。

他似乎从来没有经验过这样的无聊。他对于自己的盘辫子，仿佛也觉得无意味，要侮蔑；为报仇起见，很想立刻放下辫子来，但也没有竟放。他游到夜间，赊了两碗酒，喝下肚去，渐渐的高兴起来了，思想里才又出现白盔白甲的碎片。

有一天，他照例的混到夜深，待酒店要关门，才踱回土谷祠去。

拍，吧～～！

他忽而听得一种异样的声音，又不是爆竹。阿Q本来是爱看热闹，爱管闲事的，便在暗中直寻过去。似乎前面有些脚步声；他正听，猛然间一个人从对面逃来了。阿Q一看见，便赶紧翻身跟着逃。那人转弯，阿Q也转弯，那人站住了，阿Q也站住。他看后面并无什么，看那人便是小D。

“什么？”阿Q不平起来了。

“赵……赵家遭抢了！”小D气喘吁吁的说。

阿Q的心怦怦的跳了。小D说了便走；阿Q却逃而又停的两三回。但他究竟是做过“这路生意”，格外胆大，于是躄出路角，仔细的听，似乎有些嚷嚷，又仔细的看，似乎许多白盔白甲的人，络绎的将箱子抬出了，器具抬出了，秀才娘子的宁式床也抬出了，但是不分明，他还想上前，两只脚却没有动。

这一夜没有月，未庄在黑暗里很寂静，寂静到像羲皇时候一般太平。阿Q站着看到自己发烦，也似乎还是先前一样，在那里来来往往的搬，箱子抬出了，器具抬出了，秀才娘子的宁式床也抬出了，……抬得他自己有些不信他的眼睛了。但他决计不再上前，却回到自己的祠里去了。

土谷祠里更漆黑；他关好大门，摸进自己的屋子里。他躺了好一会，这才定了神，而且发出关于自己的思想来：白盔白甲的人明明到了，并不来打招呼，搬了许多好东西，又没有自己的份，——这全是假洋鬼子可恶，不准我造反，否则，这次何至于没有我的份呢？阿Q越想越气，终于禁不住满心痛恨起来，毒毒的点一点头：“不准我造反，只准你造反？妈妈的假洋鬼子，——好，你造反！造反是杀头的罪名呵，我总要告一状，看你抓进县里去杀头，——满门抄斩，——嚓！嚓！”

第九章　大团圆

赵家遭抢之后，未庄人大抵很快意而且恐慌，阿Q也很快意而且恐慌。但四天之后，阿Q在半夜里忽被抓进县城里去了。那时恰是暗夜，一队兵，一队团丁，一队员警，五个侦探，悄悄地到了未庄，乘昏暗围住土谷祠，正对门架好机关枪；然而阿Q不冲出。许多时没有动静，把总焦急起来了，悬了二十千的赏，才有两个团丁冒了险，逾垣进去，里应外合，一拥而入，将阿Q抓出来；直待擒出祠外面的机关枪左近，他才有些清醒了。

到进城，已经是正午，阿Q见自己被搀进一所破衙门，转了五六个弯，便推在一间小屋里。他刚刚一跄踉，那用整株的木料做成的栅栏门便跟着他的脚跟阖上了，其馀的三面都是墙壁，仔细看时，屋角上还有两个人。

阿Q虽然有些忐忑，却并不很苦闷，因为他那土谷祠里的卧室，也并没有比这间屋子更高明。那两个也仿佛是乡下人，渐渐和他兜搭起来了，一个说是举人老爷要追他祖父欠下来的陈租，一个不知道为了什么事。他们问阿Q，阿Q爽利的答道，“因为我想造反。”

他下半天便又被抓出栅栏门去了，到得大堂，上面坐着一个满头剃得精光的老头子。阿Q疑心他是和尚，但看见下面站着一排兵，两旁又站着十几个长衫人物，也有满头剃得精光像这老头子的，也有将一尺来长的头发披在背后像那假洋鬼子的，都是一脸横肉，怒目而视的看他；他便知道这人一定有些来历，膝关节立刻自然而然的宽松，便跪了下去了。

“站着说！不要跪！”长衫人物都吆喝说。

阿Q虽然似乎懂得，但总觉得站不住，身不由己的蹲了下去，而且终于趁势改为跪下了。

“奴隶性！……”长衫人物又鄙夷似的说，但也没有叫他起来。

“你从实招来罢，免得吃苦。我早都知道了。招了可以放你。”那光头的老头子看定了阿Q的脸，沉静的清楚的说。

“招罢！”长衫人物也大声说。

“我本来要……来投……”阿Q胡里胡涂的想了一通，这才断断续续的说。

“那么，为什么不来的呢？”老头子和气的问。

“假洋鬼子不准我！”

“胡说！此刻说，也迟了。现在你的同党在那里？”

“什么？……”

“那一晚打劫赵家的一伙人。”

“他们没有来叫我。他们自己搬走了。”阿Q提起来便愤愤。

“走到那里去了呢？说出来便放你了。”老头子更和气了。

“我不知道，……他们没有来叫我……”

然而老头子使了一个眼色，阿Q便又被抓进栅栏门里了。他第二次抓出栅栏门，是第二天的上午。

大堂的情形都照旧。上面仍然坐着光头的老头子，阿Q也仍然下了跪。

老头子和气的问道，“你还有什么话说么？”

阿Q一想，没有话，便回答说，“没有。”

于是一个长衫人物拿了一张纸，并一支笔送到阿Q的面前，要将笔塞在他手里。阿Q这时很吃惊，几乎“魂飞魄散”了：因为他的手和笔相关，这回是初次。他正不知怎样拿；那人却又指著一处地方教他画花押。

“我……我……不认得字。”阿Q一把抓住了笔，惶恐而且惭愧的说。

“那么，便宜你，画一个圆圈！”

阿Q要画圆圈了，那手捏着笔却只是抖。于是那人替他将纸铺在地上，阿Q伏下去，使尽了平生的力气画圆圈。他生怕被人笑话，立志要画得圆，但这可恶的笔不但很沉重，并且不听话，刚刚一抖一抖的几乎要合缝，却又向外一耸，画成瓜子模样了。

阿Q正羞愧自己画得不圆，那人却不计较，早已掣了纸笔去，许多人又将他第二次抓进栅栏门。

他第二次进了栅栏，倒也并不十分懊恼。他以为人生天地之间，大约本来有时要抓进抓出，有时要在纸上画圆圈的，惟有圈而不圆，却是他“行状”上的一个污点。但不多时也就释然了，他想：孙子才画得很圆的圆圈呢。于是他睡着了。

然而这一夜，举人老爷反而不能睡：他和把总呕了气了。举人老爷主张第一要追赃，把总主张第一要示众。把总近来很不将举人老爷放在眼里了，拍案打凳的说道，“惩一儆百！你看，我做革命党还不上二十天，抢案就是十几件，全不破案，我的面子在那里？破了案，你又来迂。不成！这是我管的！”举人老爷窘急了，然而还坚持，说是倘若不追赃，他便立刻辞了帮办民政的职务。而把总却道，“请便罢！”于是举人老爷在这一夜竟没有睡，但幸第二天倒也没有辞。

阿Q第三次抓出栅栏门的时候，便是举人老爷睡不着的那一夜的明天的上午了。他到了大堂，上面还坐着照例的光头老头子；阿Q也照例的下了跪。

老头子很和气的问道，“你还有什么话么？”

阿Q一想，没有话，便回答说，“没有。”

许多长衫和短衫人物，忽然给他穿上一件洋布的白背心，上面有些黑字。阿Q很气苦：因为这很像是带孝，而带孝是晦气的。然而同时他的两手反缚了，同时又被一直抓出衙门外去了。

阿Q被抬上了一辆没有蓬的车，几个短衣人物也和他同坐在一处。这车立刻走动了，前面是一班背着洋炮的兵们和团丁，两旁是许多张著嘴的看客，后面怎样，阿Q没有见。但他突然觉到了：这岂不是去杀头么？他一急，两眼发黑，耳朵里喤的一声，似乎发昏了。然而他又没有全发昏，有时虽然着急，有时却也泰然；他意思之间，似乎觉得人生天地间，大约本来有时也未免要杀头的。

他还认得路，于是有些诧异了：怎么不向着法场走呢？他不知道这是在游街，在示众。但即使知道也一样，他不过便以为人生天地间，大约本来有时也未免要游街要示众罢了。

他省悟了，这是绕到法场去的路，这一定是“嚓”的去杀头。他惘惘的向左右看，全跟着马蚁似的人，而在无意中，却在路旁的人丛中发见了一个吴妈。很久违，伊原来在城里做工了。阿Q忽然很羞愧自己没志气：竟没有唱几句戏。他的思想仿佛旋风似的在脑里一回旋：《小孤孀上坟》欠堂皇，《龙虎斗》里的“悔不该……”也太乏，还是“手执钢鞭将你打”罢。他同时想手一扬，才记得这两手原来都捆着，于是“手执钢鞭”也不唱了。

“过了二十年又是一个……”阿Q在百忙中，“无师自通”的说出半句从来不说的话。

“好！！！”从人丛里，便发出豺狼的嗥叫一般的声音来。

车子不住的前行，阿Q在喝采声中，轮转眼睛去看吴妈，似乎伊一向并没有见他，却只是出神的看着兵们背上的洋炮。

阿Q于是再看那些喝采的人们。

这刹那中，他的思想又仿佛旋风似的在脑里一回旋了。四年之前，他曾在山脚下遇见一只饿狼，永是不近不远的跟定他，要吃他的肉。他那时吓得几乎要死，幸而手里有一柄斫柴刀，才得仗这壮了胆，支持到未庄；可是永远记得那狼眼睛，又凶又怯，闪闪的像两颗鬼火，似乎远远的来穿透了他的皮肉。而这回他又看见从来没有见过的更可怕的眼睛了，又钝又锋利，不但已经咀嚼了他的话，并且还要咀嚼他皮肉以外的东西，永是不近不远的跟他走。

这些眼睛们似乎连成一气，已经在那里咬他的灵魂。

“救命，……”

然而阿Q没有说。他早就两眼发黑，耳朵里嗡的一声，觉得全身仿佛微尘似的迸散了。

至于当时的影响，最大的倒反在举人老爷，因为终于没有追赃，他全家都号啕了。其次是赵府，非特秀才因为上城去报官，被不好的革命党剪了辫子，而且又破费了二十千的赏钱，所以全家也号啕了。从这一天以来，他们便渐渐的都发生了遗老的气味。

至于舆论，在未庄是无异议，自然都说阿Q坏，被枪毙便是他的坏的证据：不坏又何至于被枪毙呢？而城里的舆论却不佳，他们多半不满足，以为枪毙并无杀头这般好看；而且那是怎样的一个可笑的死囚呵，游了那么久的街，竟没有唱一句戏：他们白跟一趟了。

一九二一年十二月。



社戏

我在倒数上去的二十年中，只看过两回中国戏，前十年是绝不看，因为没有看戏的意思和机会，那两回全在后十年，然而都没有看出什么来就走了。

第一回是民国元年我初到北京的时候，当时一个朋友对我说，北京戏最好，你不去见见世面么？我想，看戏是有味的，而况在北京呢。于是都兴致勃勃的跑到什么园，戏文已经开场了，在外面也早听到咚咚地响。我们挨进门，几个红的绿的在我的眼前一闪烁，便又看见戏台下满是许多头，再定神四面看，却见中间也还有几个空座，挤过去要坐时，又有人对我发议论，我因为耳朵已经喤的响着了，用了心，才听到他是说“有人，不行！”。

我们退到后面，一个辫子很光的却来领我们到了侧面，指出一个地位来。这所谓地位者，原来是一条长凳，然而他那坐板比我的上腿要狭到四分之三，他的脚比我的下腿要长过三分之二。我先是没有爬上去的勇气，接着便联想到私刑拷打的刑具，不由的毛骨悚然的走出了。

走了许多路，忽听得我的朋友的声音道，“究竟怎的？”我回过脸去，原来他也被我带出来了。他很诧异的说，“怎么总是走，不答应？”我说，“朋友，对不起，我耳朵只在咚咚喤喤的响，并没有听到你的话。”

后来我每一想到，便很以为奇怪，似乎这戏太不好，——否则便是我近来在戏台下不适于生存了。

第二回忘记了那一年，总之是募集湖北水灾捐而谭叫天还没有死。捐法是两元钱买一张戏票，可以到第一舞台去看戏，扮演的多是名角，其一就是小叫天。我买了一张票，本是对于劝募人聊以塞责的，然而似乎又有好事家乘机对我说了些叫天不可不看的大法要了。我于是忘了前几年的咚咚喤喤之灾，竟到第一舞台去了，但大约一半也因为重价购来的宝票，总得使用了才舒服。我打听得叫天出台是迟的，而第一舞台却是新式构造，用不着争座位，便放了心，延宕到九点钟才去，谁料照例，人都满了，连立足也难，我只得挤在远处的人丛中看一个老旦在台上唱。那老旦嘴边插著两个点火的纸撚子，旁边有一个鬼卒，我费尽思量，才疑心他或者是目连的母亲，因为后来又出来了一个和尚。然而我又不知道那名角是谁，就去问挤小在我的左边的一位胖绅士。他很看不起似的斜瞥了我一眼，说道， “龚云甫！”我深愧浅陋而且粗疏，脸上一热，同时脑里也制出了决不再问的定章，于是看小旦唱，看花旦唱，看老生唱，看不知什么角色唱，看一大班人乱打，看两三个人互打，从九点多到十点，从十点到十一点，从十一点到十一点半，从十一点半到十二点，——然而叫天竟还没有来。

我向来没有这样忍耐的等待过什么事物，而况这身边的胖绅士的吁吁的喘气，这台上的咚咚喤喤的敲打，红红绿绿的晃荡，加之以十二点，忽而使我省误到在这里不适于生存了。我同时便机械的拧转身子，用力往外只一挤，觉得背后便已满满的，大约那弹性的胖绅士早在我的空处胖开了他的右半身了。我后无回路，自然挤而又挤，终于出了大门。街上除了专等看客的车辆之外，几乎没有什么行人了，大门口却还有十几个人昂着头看戏目，别有一堆人站着并不看什么，我想：他们大概是看散戏之后出来的女人们的，而叫天却还没有来……

然而夜气很清爽，真所谓“沁人心脾”，我在北京遇著这样的好空气，仿佛这是第一遭了。

这一夜，就是我对于中国戏告了别的一夜，此后再没有想到他，即使偶而经过戏园，我们也漠不相关，精神上早已一在天之南一在地之北了。

但是前几天，我忽在无意之中看到一本日本文的书，可惜忘记了书名和著者，总之是关于中国戏的。其中有一篇，大意仿佛说，中国戏是大敲，大叫，大跳，使看客头昏脑眩，很不适于剧场，但若在野外散漫的所在，远远的看起来，也自有他的风致。我当时觉著这正是说了在我意中而未曾想到的话，因为我确记得在野外看过很好的戏，到北京以后的连进两回戏园去，也许还是受了那时的影响哩。可惜我不知道怎么一来，竟将书名忘却了。

至于我看好戏的时候，却实在已经是“远哉遥遥”的了，其时恐怕我还不过十一二岁。我们鲁镇的习惯，本来是凡有出嫁的女儿，倘自己还未当家，夏间便大抵回到母家去消夏。那时我的祖母虽然还康建，但母亲也已分担了些家务，所以夏期便不能多日的归省了，只得在扫墓完毕之后，抽空去住几天，这时我便每年跟了我的母亲住在外祖母的家里。那地方叫平桥村，是一个离海边不远，极偏僻的，临河的小村庄；住户不满三十家，都种田，打鱼，只有一家很小的杂货店。但在我是乐土：因为我在这里不但得到优待，又可以免念“秩秩斯干幽幽南山”了。

和我一同玩的是许多小朋友，因为有了远客，他们也都从父母那里得了减少工作的许可，伴我来游戏。在小村里，一家的客，几乎也就是公共的。我们年纪都相仿，但论起行辈来，却至少是叔子，有几个还是太公，因为他们合村都同姓，是本家。然而我们是朋友，即使偶而吵闹起来，打了太公，一村的老老少少，也决没有一个会想出“犯上”这两个字来，而他们也百分之九十九不识字。

我们每天的事情大概是掘蚯蚓，掘来穿在铜丝做的小钩上，伏在河沿上去钓虾。虾是水世界里的呆子，决不惮用了自己的两个钳捧著钩尖送到嘴里去的，所以不半天便可以钓到一大碗。这虾照例是归我吃的。其次便是一同去放牛，但或者因为高等动物了的缘故罢，黄牛水牛都欺生，敢于欺侮我，因此我也总不敢走近身，只好远远地跟着，站着。这时候，小朋友们便不再原谅我会读“秩秩斯干”，却全都嘲笑起来了。

至于我在那里所第一盼望的，却在到赵庄去看戏。赵庄是离平桥村五里的较大的村庄；平桥村太小，自己演不起戏，每年总付给赵庄多少钱，算作合做的。当时我并不想到他们为什么年年要演戏。现在想，那或者是春赛，是社戏了。

就在我十一二岁时候的这一年，这日期也看看等到了。不料这一年真可惜，在早上就叫不到船。平桥村只有一只早出晚归的航船是大船，决没有留用的道理。其馀的都是小船，不合用；央人到邻村去问，也没有，早都给别人定下了。外祖母很气恼，怪家里的人不早定，絮叨起来。母亲便宽慰伊，说我们鲁镇的戏比小村里的好得多，一年看几回，今天就算了。只有我急得要哭，母亲却竭力的嘱咐我，说万不能装模装样，怕又招外祖母生气，又不准和别人一同去，说是怕外祖母要担心。

总之，是完了。到下午，我的朋友都去了，戏已经开场了，我似乎听到锣鼓的声音，而且知道他们在戏台下买豆浆喝。

这一天我不钓虾，东西也少吃。母亲很为难，没有法子想。到晚饭时候，外祖母也终于觉察了，并且说我应当不高兴，他们太怠慢，是待客的礼数里从来没有的。吃饭之后，看过戏的少年们也都聚拢来了，高高兴兴的来讲戏。只有我不开口；他们都叹息而且表同情。忽然间，一个最聪明的双喜大悟似的提议了，他说，“大船？八叔的航船不是回来了么？”十几个别的少年也大悟，立刻撺掇起来，说可以坐了这航船和我一同去。我高兴了。然而外祖母又怕都是孩子，不可靠；母亲又说是若叫大人一同去，他们白天全有工作，要他熬夜，是不合情理的。在这迟疑之中，双喜可又看出底细来了，便又大声的说道，“我写包票！船又大；迅哥儿向来不乱跑；我们又都是识水性的！”

诚然！这十多个少年，委实没有一个不会凫水的，而且两三个还是弄潮的好手。

外祖母和母亲也相信，便不再驳回，都微笑了。我们立刻一哄的出了门。

我的很重的心忽而轻松了，身体也似乎舒展到说不出的大。一出门，便望见月下的平桥内泊著一只白篷的航船，大家跳下船，双喜拔前篙，阿发拔后篙，年幼的都陪我坐在舱中，较大的聚在船尾。母亲送出来吩咐“要小心”的时候，我们已经点开船，在桥石上一磕，退后几尺，即又上前出了桥。于是架起两支橹，一支两人，一里一换，有说笑的，有嚷的，夹着潺潺的船头激水的声音，在左右都是碧绿的豆麦田地的河流中，飞一般径向赵庄前进了。

两岸的豆麦和河底的水草所发散出来的清香，夹杂在水汽中扑面的吹来；月色便朦胧在这水汽里。淡黑的起伏的连山，仿佛是踊跃的铁的兽脊似的，都远远的向船尾跑去了，但我却还以为船慢。他们换了四回手，渐望见依稀的赵庄，而且似乎听到歌吹了，还有几点火，料想便是戏台，但或者也许是渔火。

那声音大概是横笛，宛转，悠扬，使我的心也沉静，然而又自失起来，觉得要和他弥散在含着豆麦蕴藻之香的夜气里。

那火接近了，果然是渔火；我才记得先前望见的也不是赵庄。那是正对船头的一丛松柏林，我去年也曾经去游玩过，还看见破的石马倒在地下，一个石羊蹲在草里呢。过了那林，船便弯进了叉港，于是赵庄便真在眼前了。

最惹眼的是屹立在庄外临河的空地上的一座戏台，模胡在远处的月夜中，和空间几乎分不出界限，我疑心画上见过的仙境，就在这里出现了。这时船走得更快，不多时，在台上显出人物来，红红绿绿的动，近台的河里一望乌黑的是看戏的人家的船篷。

“近台没有什么空了，我们远远的看罢。”阿发说。

这时船慢了，不久就到，果然近不得台旁，大家只能下了篙，比那正对戏台的神棚还要远。其实我们这白篷的航船，本也不愿意和乌篷的船在一处，而况没有空地呢……

在停船的匆忙中，看见台上有一个黑的长胡子的背上插著四张旗，捏著长枪，和一群赤膊的人正打仗。双喜说，那就是有名的铁头老生，能连翻八十四个筋斗，他日里亲自数过的。

我们便都挤在船头上看打仗，但那铁头老生却又并不翻筋斗，只有几个赤膊的人翻，翻了一阵，都进去了，接着走出一个小旦来，咿咿呀呀的唱。双喜说，“晚上看客少，铁头老生也懈了，谁肯显本领给白地看呢？”我相信这话对，因为其时台下已经不很有人，乡下人为了明天的工作，熬不得夜，早都睡觉去了，疏疏朗朗的站着的不过是几十个本村和邻村的闲汉。乌篷船里的那些土财主的家眷固然在，然而他们也不在乎看戏，多半是专到戏台下来吃糕饼水果和瓜子的。所以简直可以算白地。

然而我的意思却也并不在乎看翻筋斗。我最愿意看的是一个人蒙了白布，两手在头上捧著一支棒似的蛇头的蛇精，其次是套了黄布衣跳老虎。但是等了许多时都不见，小旦虽然进去了，立刻又出来了一个很老的小生。我有些疲倦了，托桂生买豆浆去。他去了一刻，回来说，“没有。卖豆浆的聋子也回去了。日里倒有，我还喝了两碗呢。现在去舀一瓢水来给你喝罢。”

我不喝水，支撑著仍然看，也说不出见了些什么，只觉得戏子的脸都渐渐的有些稀奇了，那五官渐不明显，似乎融成一片的再没有什么高低。年纪小的几个多打呵欠了，大的也各管自己谈话。忽而一个红衫的小丑被绑在台柱子上，给一个花白胡子的用马鞭打起来了，大家才又振作精神的笑着看。在这一夜里，我以为这实在要算是最好的一折。

然而老旦终于出台了。老旦本来是我所最怕的东西，尤其是怕他坐下了唱。这时候，看见大家也都很扫兴，才知道他们的意见是和我一致的。那老旦当初还只是踱来踱去的唱，后来竟在中间的一把交椅上坐下了。我很担心；双喜他们却就破口喃喃的骂。我忍耐的等著，许多工夫，只见那老旦将手一抬，我以为就要站起来了，不料他却又慢慢的放下在原地方，仍旧唱。全船里几个人不住的吁气，其馀的也打起哈欠来。双喜终于熬不住了，说道，怕他会唱到天明还不完，还是我们走的好罢。大家立刻都赞成，和开船时候一样踊跃，三四人径奔船尾，拔了篙，点退几丈，回转船头，驾起橹，骂着老旦，又向那松柏林前进了。

月还没有落，仿佛看戏也并不很久似的，而一离赵庄，月光又显得格外的皎洁。回望戏台在灯火光中，却又如初来未到时候一般，又漂渺得像一座仙山楼阁，满被红霞罩着了。吹到耳边来的又是横笛，很悠扬；我疑心老旦已经进去了，但也不好意思说再回去看。

不多久，松柏林早在船后了，船行也并不慢，但周围的黑暗只是浓，可知已经到了深夜。他们一面议论著戏子，或骂，或笑，一面加紧的摇船。这一次船头的激水声更其响亮了，那航船，就像一条大白鱼背着一群孩子在浪花里蹿，连夜渔的几个老渔父，也停了艇子看着喝采起来。

离平桥村还有一里模样，船行却慢了，摇船的都说很疲乏，因为太用力，而且许久没有东西吃。这回想出来的是桂生，说是罗汉豆正旺相，柴火又现成，我们可以偷一点来煮吃。大家都赞成，立刻近岸停了船；岸上的田里，乌油油的都是结实的罗汉豆。

“阿阿，阿发，这边是你家的，这边是老六一家的，我们偷那一边的呢？”双喜先跳下去了，在岸上说。

我们也都跳上岸。阿发一面跳，一面说道，“且慢，让我来看一看罢，”他于是往来的摸了一回，直起身来说道，“偷我们的罢，我们的大得多呢。”一声答应，大家便散开在阿发家的豆田里，各摘了一大捧，抛入船舱中。双喜以为再多偷，倘给阿发的娘知道是要哭骂的，于是各人便到六一公公的田里又各偷了一大捧。

我们中间几个年长的仍然慢慢的摇著船，几个到后舱去生火，年幼的和我都剥豆。不久豆熟了，便任凭航船浮在水面上，都围起来用手撮著吃。吃完豆，又开船，一面洗器具，豆荚豆壳全抛在河水里，什么痕迹也没有了。双喜所虑的是用了八公公船上的盐和柴，这老头子很细心，一定要知道，会骂的。然而大家议论之后，归结是不怕。他如果骂，我们便要他归还去年在岸边拾去的一枝枯桕树，而且当面叫他“八癞子”。

“都回来了！那里会错。我原说过写包票的！”双喜在船头上忽而大声的说。

我向船头一望，前面已经是平桥。桥脚上站着一个人，却是我的母亲，双喜便是对伊说著话。我走出前舱去，船也就进了平桥了，停了船，我们纷纷都上岸。母亲颇有些生气，说是过了三更了，怎么回来得这样迟，但也就高兴了，笑着邀大家去吃炒米。

大家都说已经吃了点心，又渴睡，不如及早睡的好，各自回去了。

第二天，我向午才起来，并没有听到什么关系八公公盐柴事件的纠葛，下午仍然去钓虾。

“双喜，你们这班小鬼，昨天偷了我的豆了罢？又不肯好好的摘，蹋坏了不少。” 我抬头看时，是六一公公棹著小船，卖了豆回来了，船肚里还有剩下的一堆豆。

“是的。我们请客。我们当初还不要你的呢。你看，你把我的虾吓跑了！”双喜说。

六一公公看见我，便停了楫，笑道，“请客？——这是应该的。”于是对我说， “迅哥儿，昨天的戏可好么？”

我点一点头，说道，“好。”

“豆可中吃呢？”

我又点一点头，说道，“很好。”

不料六一公公竟非常感激起来，将大拇指一翘，得意的说道，“这真是大市镇里出来的读过书的人才识货！我的豆种是粒粒挑选过的，乡下人不识好歹，还说我的豆比不上别人的呢。我今天也要送些给我们的姑奶奶尝尝去……”他于是打着楫子过去了。

待到母亲叫我回去吃晚饭的时候，桌上便有一大碗煮熟了的罗汉豆，就是六一公公送给母亲和我吃的。听说他还对母亲极口夸奖我，说“小小年纪便有见识，将来一定要中状元。姑奶奶，你的福气是可以写包票的了。”但我吃了豆，却并没有昨夜的豆那么好。

真的，一直到现在，我实在再没有吃到那夜似的好豆，——也不再看到那夜似的好戏了。

（一九二二年十月）
