Alice lette op het Witte Konijn, dat aan de lijst stond te frommelen, erg benieuwd
wat de volgende getuige voor iemand zou zijn, “want ze hebben tot nu toe nog niet
veel bewijzen in handen -” zei ze bij zich zelf. Stel je haar verrassing voor toen het
Witte Konijn zo hard als hij met zijn schrille stem kon, oplas: “Alice!”

XII. De Getuigenis van Alice
HIER ben ik,’ riep Alice, die in de verwarring van het ogenblik helemaal vergat hoe
groot ze de laatste minuten geworden was. En zij sprong zo haastig op dat ze met
een slip van haar rok de bank van de jury-leden omvergooide, zodat deze allemaal
op de hoofden van de toeschouwers beneden hen terecht kwamen. Daar lagen zij te
spartelen. Het herinnerde Alice heel erg aan een kom goudvissen, die zij de vorige
week per ongeluk omver had gestoten.
‘O neem me niet kwalijk!’ riep ze verschrikt en zij begon zo gauw als ze kon hen
weer op te rapen. Het ongeluk met de goudvissen spookte door haar hoofd en zij had
een vaag idee dat die dieren, wanneer zij ze niet ineens bij elkaar zocht en in de
jury-bank terugzette, dood zouden gaan.
‘De rechtszitting kan geen voortgang hebben,’ zei de Koning heel ernstig, ‘voor
alle jury-leden op hun eigen plaats terug zijn - allemaal,’ herhaalde hij met grote
nadruk en hij keek Alice strak aan toen hij dat zei.
Alice keek naar de bank van de jury-leden en zag dat zij de Hagedis op zijn hoofd
had gezet. Het arme dier wuifde neerslachtig met zijn staart, niet in staat om zich om
te keren. Ze pakte hem gauw op en zette hem recht. ‘Niet dat het er veel op aankomt,’
zei ze bij

zichzelf, ‘ik denk dat hij op zijn kop evenveel invloed heeft op het verhoor als wanneer
hij rechtop staat.’
Zodra de jury-leden zich een beetje hersteld
hadden van de schok en hun pennen en schriften weer gevonden waren en aan hen
teruggegeven, begonnen zij heel ijverig een verslag van het ongeluk op te schrijven.
Behalve de Hagedis, die blijkbaar te

veel in de war was om iets anders te doen dan met zijn mond open naar het plafond
van de zaal te staren.
‘Wat weet je af van deze zaak?’ zei de Koning tot Alice.
‘Niets,’ zei Alice.
‘Niets wat?’ vervolgde de Koning.
‘Niets, maar dan ook helemaal niets,’ zei. Alice.
‘Dat is heel belangrijk,’ zei de Koning en wendde zich tot de jury. De jury-leden
begonnen het juist op te schrijven toen het Witte Konijn hem in de rede viel.
‘Onbelangrijk bedoelt Uwe Majesteit natuurlijk,’ zei hij heel eerbiedig, maar hij
fronste zijn voorhoofd en trok allerlei gezichten naar de Koning onderwijl.
‘Natuurlijk, onbelangrijk bedoelde ik,’ zei de Koning haastig en hij ging zachtjes
bij zich zelf verder ‘belangrijk-onbelangrijk-onbelangrijk-belangrijk,’ alsof hij
probeerde welk woord het beste klonk.
Sommige jury-leden schreven op ‘belangrijk’ en sommige ‘onbelangrijk’. Alice
kon dit zien, omdat zij dicht genoeg bij hen zat om in hun schriften te kijken, ‘maar
het komt er toch niets op aan,’ dacht ze bij zich zelf.
Op dit ogenblik riep de Koning, die enige tijd ijverig in zijn notitie-boekje had
zitten schrijven: ‘Stilte!’ en dan las hij uit het boekje voor: ‘Wet twee-en-veertig.
Alle personen van meer dan een kilometer lang moeten de rechtszaal verlaten.’
Iedereen keek naar Alice.
‘Ik ben geen kilometer lang,’ zei Alice.
‘Dat ben je wel,’ zei de Koning.

‘Bijna twee kilometer,’ voegde de Koningin er aan toe.
‘Toch ga ik niet weg,’ zei Alice, ‘het is trouwens geen echte wet, U hebt hem net
bedacht.’
‘Het is de oudste wet in het boek,’ zei de Koning.
‘Dan zou het Wet no. één zijn,’ zei Alice.
De Koning werd bleek en deed haastig zijn boekje dicht. ‘Overweeg uw oordeel,’
zei hij met zachte, bevende stem tot de jury.
‘Er zijn nog meer getuigen, Majesteit,’ zei het Witte Konijn en sprong haastig op,
‘dit papier is juist binnen gekomen.’
‘Wat staat er op?’ zei de Koningin.
‘Ik heb het nog niet opengedaan,’ zei het Witte Konijn, ‘maar het is blijkbaar een
brief, geschreven door de gevangene en gericht aan - aan iemand.’
‘Dat moet wel,’ zei de Koning, ‘anders was hij aan niemand gericht en dat komt
niet dikwijls voor.’
‘Aan wie is hij geadresseerd?’ vroeg een van de jury-leden.
‘Het is niet geadresseerd,’ zei het Witte Konijn, ‘op de buitenkant staat eigenlijk
helemaal niets.’ Hij vouwde het papier open, terwijl hij dit zei en voegde er aan toe:
‘Het is trouwens geen brief, het is een gedicht.’
‘Is het geschreven met de hand van de gevangene?’ vroeg een van de jury-leden.
‘Nee,’ zei het Witte Konijn, ‘en dat is wel het gekste van alles.’ (De jury-leden
keken heel beteuterd)
‘Dan moet hij iemand anders handschrift na hebben

gedaan,’ zei de Koning. (De gezichten van de juryleden klaarden op).
‘Majesteit,’ zei de Hartenboer, ‘ik heb het niet geschreven en niemand kan bewijzen
dat ik het wel heb gedaan. Er staat geen naam aan het slot.’
‘Als je het niet ondertekend hebt,’ zei de Koning, ‘maakt dat de zaak enkel erger.
Je moet een of andere misdaad van plan zijn geweest. Anders had je als eerlijk man
je naam er wel onder gezet.’
Iedereen klapte luid in zijn handen. Dit was het eerste werkelijk verstandige woord
dat de Koning die dag had gezegd.
‘Dat bewijst zijn schuld,’ zei de Koningin.
‘Het bewijst niets!’ zei Alice, ‘U weet niet eens wat er in staat.’
‘Lees voor,’ zei de Koning.
Het Witte Konijn zette zijn bril op. ‘Waar zal ik beginnen,Majesteit?’ vroeg hij.
‘Begin bij het begin,’ zei de Koning ernstig, ‘en ga net zo lang door tot je aan het
slot bent. Houd dan op!’
Dit was het gedicht, dat het Witte Konijn voorlas:
Ze zeiden dat u bij haar waart
En noemden mij aan hem.
Zij roemde wel mijn goede aard,
Maar zei dat ik niet zwem.
Hij schreef hun dat ik er niet was
(Zo is het, weet ik nu).
Komt haar de zaak geheel van pas,
Wat wordt er dan van u?

Ik gaf haar twee, zij twee aan hem,
En drie of meer gaaft gij.
Zij keerden dan van u naar hem,
Toch waren zij van mij.
Wordt gij en ik bij dit bestel
Betrokken in die zaak,
Voor uwe vrijspraak zorgt hij wel,
Nauwkeurig als ik waak.
Mijn mening was, dat uwe stem
(Voor ziekte 't heeft belet)
Een hinderpaal was tussen hem,
Onszelf en u en het.
Laat hem niet weten dat haar wens
Zo was ten naasten bij.
Dit is geheim voor dier en mens
Tussen uzelf en mij.
‘Dit is het belangrijkste bewijsstuk, dat ik tot nu toe gehoord heb,’ zei de Koning en
wreef in zijn handen, ‘laat nu de jury -.’
‘Als iemand er iets van begrijpt,’ zei Alice (ze was de laatste minuten zo groot
geworden, dat ze helemaal niet bang meer was om hen in de rede te vallen), ‘krijgt
hij van mij een stuiver. Ik geloof dat het absoluut niets te betekenen heeft.’
De jury-leden schreven allemaal in hun schriften: ‘Zij gelooft dat het absoluut
niets te betekenen heeft,’ maar

niet een van hen probeerde het gedicht uit te leggen.
‘Als het niets betekent,’ zei de Koning, ‘bespaart
ons dat een heleboel moeite, want dan hoeven we ook niet naar een verklaring te
zoeken. En toch weet ik het niet,’ ging hij voort en spreidde het papier uit op zijn
knie en keek ernaar met één oog: ‘Het heeft toch blijkbaar wel iets te betekenen, -
Maar zei dat ik niet zwem - jij kunt niet zwemmen?’ ging hij verder en wendde zich
daarbij tot de Hartenboer.
De Hartenboer schudde bedroefd zijn hoofd. ‘Zie ik daarnaar uit?’ zei hij. (Wat
hij beslist niet deed, want hij was van karton gemaakt).
‘Precies,’ zei de Koning en hij mompelde verder bij zichzelf: ‘- Zo is het, weet ik
nu -, dat is iemand

van de jury natuurlijk. - Ik gaf haar een, zij twee aan hem -, wel dat slaat natuurlijk
op de gebakjes.’
‘Maar het gaat verder: Zij keerden dan van u naar hem,’ zei Alice.
‘Nu, dat zijn ze!’ riep de Koning triomfantelijk en wees naar de gebakjes op de
tafel, ‘dat is overduidelijk. Dan verder - Voor ziekte 't heeft belet -Jij bent niet ziek,
liefste?’ zei hij tot de Koningin.
‘Nooit!’ zei de Koningin woedend en gooide een inktpot naar de Hagedis. (De
arme kleine Jan was opgehouden met zijn ene vinger in zijn schrift te schrijven, toen
hij merkte dat dat toch geen zin had. Maar nu begon hij haastig weer en gebruikte
zolang als het ging de inkt, die van zijn gezicht droop).
‘Dan beletten die woorden je ook niets,’ zei de Koning en keek glimlachend de
zaal rond. Er heerste een diepe stilte.
‘Het was een woordspeling,’ voegde de Koning er beledigd aan toe en iedereen
begon te lachen.
‘Laat de jury uitspraak doen,’ zei de Koning ongeveer voor de twintigste maal die
dag.
‘Nee, neen,’ zei de Koningin, ‘eerst de veroordeling en dan de uitspraak.’
‘Grote onzin,’ riep Alice luid. ‘Stel je voor, eerst de veroordeling!’
‘Houd je mond,’ zei de Koningin, die purper werd van woede.
‘Ik denk er niet over,’ zei Alice.
‘Sla haar hoofd af,’ riep de Koningin zo hard als ze kon. Niemand bewoog zich.

‘Wat geef ik om jullie?’ zei Alice (zij had nu haar gewone lengte teruggekregen),
‘jullie zijn toch maar een pak kaarten.’
Tegelijk stoof het hele pak in de lucht en vloog op haar neer. Ze uitte een lichte
kreet, half uit angst, half uit boosheid, en probeerde ze weg te slaan. En toen

merkte ze dat ze aan de waterkant lag met haar hoofd in de schoot van haar zuster,
die zachtjes de dorre bladeren wegstreek, die over haar gezicht waren gedwarreld.
‘Wordt wakker Alice,’ zei haar zusje, ‘wat heb jij lang geslapen!’
‘O, ik heb zo leuk gedroomd,’ zei Alice en zij vertelde haar zuster zo goed als ze
kon al de avonturen, die jullie juist hebt gelezen. En toen zij klaar was, gaf haar zusje
haar een zoen en zei: ‘Het was een heel leuke droom, maar ga nu gauw thee drinken,
het is laat geworden.’ Toen sprong Alice op en holde weg en onderwijl dacht zij wat
een wonderlijke droom het toch was geweest.
