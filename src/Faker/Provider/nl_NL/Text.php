Alice merkte tot haar verrassing dat de steentjes op de grond allemaal in koekjes
veranderden en plotseling kreeg ze een prachtig idee. ‘Als ik een van deze koekjes
opeet,’ dacht ze, ‘zal ik in elk geval wel weer veranderen; ik kan onmogelijk groter
worden, dan zullen ze me dus wel kleiner maken, denk ik.’
Dus at ze haastig een van de koekjes op en ze bemerkte tot haar grote vreugde dat
ze onmiddellijk begon te krimpen. Zodra ze klein genoeg was om de deur door te
gaan, liep ze het huis uit en zag dat een hele groep kleine beesten buiten op haar
wachtte. De arme kleine hagedis Jan stond in het midden, overeind gehouden door
twee guinese biggetjes, die hem iets uit een fles te drinken gaven. Zodra ze Alice
zagen, holden ze op haar af, maar Alice liep zo hard als ze kon weg en kwam al gauw
veilig en wel in een dicht bos.
‘Ik wil eerst proberen,’ zei Alice bij zich zelf ‘of ik mijn gewone lengte terug kan
krijgen en dan ga ik de

weg naar die mooie tuin zoeken. Dat lijkt mij het beste plan.’
Het was zonder twijfel een prachtig plan, heel helder en eenvoudig; de enige
moeilijkheid was, dat ze niet het flauwste idee had hoe ze het ooit uit kon voeren;
en terwijl zij tussen de bomen daar bezorgd over na liep te denken, hoorde ze vlak
boven haar hoofd een kort scherp geblaf en vreselijk geschrokken keek ze op.
Een reusachtige jonge hond keek met grote ronde ogen op haar neer en stak speels
zijn poot uit om haar aan te raken. ‘Braaf beestje,’ zei Alice vleiend en zij probeerde
vriendelijk tegen hem te fluiten; maar ze was tegelijk erg bang, dat hij misschien
honger zou hebben en haar ondanks al haar vriendelijkheid op zou eten.
Volkomen van streek pakte ze een stokje van de grond en hield dat de hond voor
zijn kop; de hond sprong, jankend van pret, op alle vier zijn poten in de lucht, rende
op de stok af en hapte er naar. Alice dook weg achter een grote distel om niet
ondersteboven te worden gelopen, maar toen ze aan de andere kant van de distel te
voorschijn kwam, schoot de hond opnieuw op de stok af en vloog in zijn vaart over
de kop. Alice, die vond dat dit erg veel weg had van een stoeipartij met een karrepaard,
en ieder ogenblik verwachtte onder de voet te worden gelopen, liep weer om de distel
heen. Toen deed de hond een hele reeks schijn-aanvallen op de stok, liep steeds een
stukje naar voren en een heel eind terug en bleef intussen alsmaar

schor aan 't blaffen, tot hij tenslotte een eind verder ging zitten, hijgend, met zijn
tong uit zijn bek en zijn grote ogen half gesloten.
Alice vond dit een goede gelegenheid om te ontsnappen; dus liep ze hard weg tot
ze erg moe werd en buiten adem en het geblaf van de hond ver genoeg weg was.
‘En toch was het een aardig hondje,’ zei Alice en

ging tegen een boterbloem leunen om een beetje uit te rusten en wuifde zich koelte
toe met een van de bladeren. ‘Ik zou hem best kunstjes willen leren - als ik maar
groot genoeg was om dat te doen - o lieve help! Daar was ik bijna vergeten dat ik
weer groter wil worden! Laat eens kijken, hoe zal ik dat klaarspelen? Ik denk dat ik
wel weer iets zal moeten eten of drinken; maar de grote vraag is: wat?’
En dat was zeker de grote vraag. Alice keek rond naar de bloemen en de
grassprietjes, maar ze kon niets vinden dat haar geschikt leek om in deze
omstandigheden te eten of te drinken. Naast haar groeide een grote paddestoel,
ongeveer even groot als zij zelf; en toen zij er onder had gekeken en aan beide kanten
ervan en er achter, vond ze dat ze er ook wel eens bovenop kon kijken.
Ze ging op haar tenen staan en gluurde over de rand van de paddestoel en haar
ogen troffen meteen die van een grote blauwe rups, die boven op de paddestoel zat
met zijn armen over elkaar en rustig een lange Turkse pijp rookte en niet de minste
notitie nam van haar of van iets anders.

V. De raad van een rups
DE rups en Alice keken elkaar een hele tijd zwijgend aan; tenslotte nam de Rups de
pijp uit zijn mond en zei langzaam en slaperig: ‘Wie ben jij.’
Dit was een niet erg bemoedigend begin voor een gesprek. Alice antwoordde
tamelijk verlegen. ‘Ik weet het eigenlijk niet, mijnheer, tenminste - nu niet. Ik weet
wie ik vanmorgen was, maar ik moet sindsdien een paar keer veranderd zijn.’
‘Wat bedoel je daarmee,’ zei de Rups streng, ‘verklaar je nader.’
‘Dat kan ik niet, mijnheer,’ zei Alice, ‘want ik ben eigenlijk mezelf niet, ziet u.’
‘Ik zie niets,’ zei de Rups.
‘Ik kan het toch niet duidelijker uitleggen,’ antwoordde Alice beleefd, ‘want ik
begrijp het zelf niet goed; het is erg verwarrend om op één dag telkens kleiner en
groter te worden.’
‘Helemaal niet,’ zei de Rups.
‘Misschien hebt u dat nog niet zo ondervonden,’ zei Alice, ‘maar als u in een pop
moet veranderen en dan in en vlinder, zou u dat dan niet een beetje gek vinden?’
‘Niet in het minst!’ zei de Rups.
‘Misschien staat u er dan anders tegenover,’ zei Alice, ‘maar ik weet wel dat ik
het heel gek vind.’
‘Jij,’ zei de Rups verachtelijk, ‘wie ben jij?’
Zo waren ze weer aan 't begin van het gesprek

gekomen. Alice was er een beetje geërgerd over, dat de Rups zulke heel erg korte
opmerkingen maakte en zij richtte zich zo hoog als zij kon op en zei ernstig: ‘Ik vind
dat u me wel eerst kunt vertellen wie u bent.’
‘Waarom?’ zei de Rups.
Dit was weer een moeilijke vraag en daar Alice er geen goed antwoord op kon
vinden en de Rups blijkbaar in een erg slecht humeur was, liep ze weg.
‘Kom terug,’ riep de Rups haar na, ‘ik heb je iets belangrijks te vertellen.’

Dat klonk waarlijk veelbelovend. Alice keerde zich om en liep weer naar hem toe.
‘Houd je bedaard,’ zei de Rups.
‘Is dat alles?’ zei Alice, die zo goed mogelijk haar woede inhield. ‘Nee,’ zei de
Rups.
Alice bedacht, dat zij net zo goed nog een beetje kon wachten, want ze had toch
niets beters te doen en misschien zou hij haar tenslotte wel iets vertellen, dat de
moeite waard was. Een paar minuten rookte hij zwijgend door, maar toen deed hij
zijn armen van elkaar, haalde zijn pijp uit zijn mond en zei: ‘Dus je denkt, dat je
veranderd bent.’
‘Ik denk het haast wel, mijnheer,’ zei Alice, ‘ik kan me een hele boel niet meer
goed herinneren en ik blijf geen tien minuten achter elkaar even groot.’
‘Wat kan je je niet herinneren?’ zei de Rups.
‘Ik heb bijvoorbeeld geprobeerd om‘Hoe vrolijk plast de krokodil’ op te zeggen,
maar het werd heel wat anders,’ antwoordde Alice neerslachtig.
‘Zeg “Oud zijt gij Paai Witbol” op,’ zei de Rups.
Alice deed haar handen over elkaar en begon:

‘Oud zijt gij Paai Witbol’ sprak laatstmaal een knaap,
‘En het leven wordt grijsaards tot last.
Toch staat gij het liefst op uw hoofd als een aap,
Vindt gij dat uw leeftijd dit past?’
‘In mijn jeugd’, zei de paai,‘heb ik 't nimmer gedaan,
'k Was bang voor mijn kwetsbaar verstand,
Maar wijl mij dit blijkbaar sinds lang is ontgaan
Is het nu mijn geliefkoosde stand.’

‘Gijzijt oud,’ zei de knaap, ‘zoals ik reeds zei,
En uw buik wordt onnoemelijk zwaar,
Nu buitelt gij achterom uit de wei,
Ai, zeg mij, hoe speelt gij dit klaar’
‘In mijn jeugd,’ zei de oude en glimlachte staag,
‘Behield ik mijn veerkracht altoos
Door 't gebruik van dit zalfje - 'k verkoop het u graag,
Het kost u een stuiver de doos.’

‘Gij zijt oud,’ zei de knaap, ‘met uw kracht is het uit,
Uw gebit is reeds jaren vergaan,
Toch aat gij een gans op met haar en met huid,
Ai, zeg mij, hoe legt hij dat aan?’
‘In mijn jeugd,’ zei de grijsaard, ‘bezat ik een zaak
En 'k besprak elk probleem met mijn vrouw,
De spierkracht die 'k daardoor verleende aan mijn kaak
Verlicht mij het bestaan als ik kauw.’

‘Gij zijt oud,’ zei de knaap, ‘en niemand denkt heus,
Dat uw oog nog scherp ziet bij het werk,
Toch wiegt gij een aal op de punt van uw neus,
Wat maakt u zo handig en sterk?’
‘'k Heb drie vragen beantwoord, nu ben ik het moe,’
Zei zijn vader, ‘verwacht gij schavuit,
Dat 'k mijn ganse dag met die onzin verdoe,
Ga heen of ik trap u eruit.’

‘Zo is het niet’, zei de Rups.
‘Niet helemaal, geloof ik’, zei Alice bescheiden, ‘sommige woorden waren
veranderd.’
‘Het was fout van het begin tot het einde’, zei de Rups beslist en ze zwegen weer
een paar minuten.
De Rups nam het eerst weer het woord.
‘Hoe groot wil je zijn?’ vroeg hij.
O, het kan me niet veel schelen hoe groot ik ben’, antwoordde Alice haastig, ‘ik
wil alleen niet zo dikwijls van lengte veranderen, begrijpt u.’
‘Ik begrijp het niet’, zei de Rups.
Alice zei niets; ze was nog nooit in haar leven zo tegengesproken en zij voelde
dat zij haar goede humeur begon te verliezen.
‘Ben je nu tevreden?’ zei de Rups.
‘Ik zou als het kon graag een beetje groter worden’, zei Alice, ‘één decimeter is
zo erg klein.’
‘Het is een heel goede lengte’, zei de Rups boos en ging rechtop staan, terwijl hij
sprak (hij was precies één decimeter groot).
‘Maar ik ben er niet aan gewend’, klaagde de arme Alice, en zij dacht bij zich zelf
‘ik wou dat die beesten niet allemaal zo gauw beledigd waren.’
‘Je zult er op den duur wel aan wennen’, zei de Rups en hij stak de pijp weer in
zijn mond en begon te roken.
Dit keer wachtte Alice geduldig tot hij weer verkoos iets te zeggen. Na een paar
minuten haalde de Rups de pijp weer uit zijn mond, geeuwde een paar keer en rekte
zich uit. Toen daalde hij af van de paddestoel en kroop weg door het gras, terwijl hij

enkel opmerkte: ‘De ene kant zal je kleiner maken, en de andere groter.’
‘De ene kant van wat? En de andere kant van wat?’ dacht Alice.
‘Van de paddestoel’, zei de Rups, alsof zij het hardop gevraagd had en het volgende
ogenblik was hij verdwenen.
Alice keek in gedachten verzonken naar de paddestoel; zij probeerde uit te maken
wat de twee kanten daarvan waren, maar hij was volkomen rond en dus vond ze dit
een moeilijk probleem. Tenslotte legde ze echter haar armen zover als ze kon om de
paddestoel en brak met iedere hand een stuk van de rand af.
‘En welk stuk moet ik nu hebben?’ zei ze bij zich zelf en knabbelde een beetje
aan dat in haar rechterhand om de uitwerking eenste proberen; het volgende ogenblik
kreeg ze een geweldige klap: haar kin was tegen haar voet gestoten. Ze schrok heel
erg van deze plotselinge verandering, maar ze begreep dat ze geen tijd te verliezen
had, zo snel kromp ze in elkaar; daarom begon ze meteen iets te eten van het andere
stuk. Haar kin was zo dicht tegen haar voet gedrukt dat er nauwelijks plaats was om
haar mond open te doen; maar dit lukte tenslotte en ze kon gauw een hapje nemen
van het linker stuk.
‘Nu, mijn hoofd is tenminste vrij,’ zei Alice opgelucht, maar het volgende ogenblik
bemerkte ze tot haar grote verontrusting dat haar schouders nergens meer te vinden
waren; alles wat zij kon zien, wanneer

ze naar beneden keek, was een verbazend lange hals, die als een stengel scheen op
te rijzen uit een zee van bladeren ver beneden haar.
‘Wat zou al dat groen toch zijn?’ zei Alice. ‘En waar zijn mijn schouders gebleven?
En o, arme handen, waarom kan ik jullie niet eens meer zien?’ Zij bewoog ze terwijl
ze praatte, maar dat veroorzaakte enkel een kleine deining tussen de verre groene
bladeren.
Daar zij haar handen niet op kon heffen naar haar hoofd, probeerde zij haar hoofd
naar haar handen te brengen en zij was erg blij toen ze merkte dat haar hals als een
slang in iedere richting kon draaien.
Ze was er juist in geslaagd deze in een elegante zig-zag naar beneden te bewegen
en tussen de bladeren te steken, die de toppen van de bomen bleken te zijn, waaronder
zij had rondgedwaald, toen een scherp gesis haar haastig haar hoofd terug deed
trekken: een grote duif was pal in haar gezicht gevlogen en sloeg haar nu geweldig
met haar vleugels.
‘Slang,’ gilde de Duif.
‘Ik ben geen slang,’ zei Alice verontwaardigd, ‘laat mij met rust.’
‘Slang, zeg ik nog eens,’ herhaalde de Duif een beetje zachter en zij voegde er
met een soort snik aan toe: ‘Ik heb alles geprobeerd, maar niets schijnt hun te
bevallen.’
‘Ik begrijp helemaal niet waar u het over hebt,’ zei Alice.
‘Ik heb boomwortels geprobeerd, ik heb de waterkant geprobeerd en ik heb heggen
geprobeerd,’ ging de

Duif verder, ‘maar die slangen, ze blijven maar lastig.’
Alice begreep er steeds minder van, maar ze vond het zinloos om iets te zeggen
voor de Duif was uitgesproken.
‘Alsof het niet al moeilijk genoeg is om eieren uit te broeden,’ zei de Duif, ‘maar
ik moet ook nog dag en nacht op wacht staan voor slangen. En ik heb al drie weken
lang geen oog dicht gedaan.’
‘Het spijt me dat ik u last veroorzaakt heb,’ zei Alice, die haar bedoeling begon
te begrijpen.
‘En nu heb ik net de hoogste boom van het bos uitgezocht,’ vervolgde de Duif en
haar stem sloeg gillend over ‘en nu ik net dacht, dat ik hier geen last van ze zou
hebben, moeten ze beslist uit de hemel naar beneden komen kronkelen! Bah! Slang!’
‘Maar ik ben geen slang, zeg ik je,’ zei Alice, ‘ik ben, ik ben...’
‘Nou, wat ben je dan,’ zei de Duif,‘ik merk heus wel dat je iets probeert te
verzinnen!’
‘Ik, ik ben een klein meisje,’ zei Alice, bijna twijfelend, toen zij bedacht hoe vaak
zij die dag al veranderd was.
‘Dat geloof ik vast,’ zei de Duif met de diepste verachting, ‘ik heb in mijn leven
een hele boel meisjes gezien, maar nog nooit één met zo'n hals als jij. Nee, nee, je
bent een slang, ontken dat maar niet. Straks ga je me wijs maken dat je nog nooit
een ei hebt gegeten.’
‘Ik heb eieren gegeten, zeker!’ zei Alice, die een erg waarheidslievend kind was,
‘maar meisjes eten ook eieren, minstens evenveel als slangen.’

‘Dat geloof ik niet,’ zei de Duif, ‘maar als zij het doen dan zijn ze een soort slangen,
dat vind ik.’
Dit was een zo nieuw gezichtspunt voor Alice, dat ze er een paar minuten stil van
was, wat de Duif gelegenheid gaf om op te merken: ‘Je zoekt eieren, dat weet ik heel
goed, en wat kan het mij dan schelen of je nu een meisje bent of een slang.’
‘Dat kan mij wel wat schelen,’ zei Alice haastig, ‘maar ik zoek helemaal geen
eieren, en als ik het deed, zou ik die van u toch niet willen hebben, ik houd niet van
rauwe eieren.’
‘Nu, ga dan weg,’ zei de Duif knorrig, terwijl zij terugging naar haar nest. Alice
hurkte zo diep als ze kon tussen de bomen, maar haar hals raakte telkens verward in
de takken en zo nu en dan moest ze ophouden om zich los te maken. Na een poosje
bedacht ze dat ze nog altijd de stukjes van de paddestoel in haar handen had en zij
ging nu heel zorgvuldig te werk, knabbelde eerst aan het ene en dan aan het andere
stukje, werd soms groter en soms kleiner, tot ze haar gewone lengte weer bereikt
had.
Het was zo lang geleden dat zij zo geweest was, dat zij het eerst bijna vreemd
vond, maar ze was er al gauw aan gewend en begon zoals gewoonlijk in zichzelf te
praten.
‘Zo, de helft van mijn plan is klaar! Wat zijn al die veranderingen toch verwarrend!
Ik ben van het ene ogenblik op het andere niet zeker wat ik zal worden. Maar nu ben
ik tenminste weer gewoon en nu moet ik in die mooie tuin zien te komen. Ik vraag
me af hoe ik dat aan moet leggen!’

Terwijl ze dat zei kwam ze plotseling aan een open plaats in het bos met een klein
huisje er op dat ongeveer één meter twintig hoog was. ‘Daar kan ik zo nooit
heengaan,’ dacht Alice, ‘wie er ook woont; ik ben veel te groot, ze zouden gek worden
van angst.’
Dus begon ze weer aan het rechter stukje te knabbelen en ze dorst niet naar het
huisje te gaan voor zij twintig centimeter groot was.

VI. Big en peper
EEN poosje stond zij naar het huis te kijken en zich af te vragen wat ze nu zou
beginnen, toen plotseling een lakei in livrei uit het bos kwam rennen (ze dacht dat
hij wel een lakei zou zijn, omdat hij een livrei aan had; anders, wanneer ze hem alleen
naar zijn gezicht beoordeeld had, had ze hem een vis genoemd) en met zijn knokkels
hard op de deur ging kloppen. Deze werd open gedaan door een andere lakei in livrei
met een rond gezicht en grote ogen, als van een kikvors; en deze twee lakeien hadden,
zoals Alice opmerkte, gepoederd haar, dat hun hele hoofd met krullen bedekte. Ze
was erg benieuw wat dit nu weer te betekenen had en kroop voorzichtig een eindje
het bos uit om te luisteren.
De Vis-lakei had een brief onder zijn arm, die bijna even groot was als hij zelf en
deze overhandigde hij aan den ander, terwijl hij plechtig zei: ‘Voor de Hertogin. Een
uitnodiging van de Koningin om croquet te komen spelen.’
De Kikvors-lakei antwoordde even plechtig en met enkel een kleine verandering
in de volgorde van de woorden ‘Van de Koningin. Een uitnodiging voor de Hertogin
om croquet te komen spelen.’ Toen bogen ze allebei heel diep, zodat hun krullen in
elkaar verward raakten.
Alice moest hierom zo lachen, dat ze hard het bos

inliep uit angst dat ze haar zouden horen; en toen zij weer dorst te kijken, zag ze dat
de Vis-lakei weg was gegaan en de ander naast de deur op de grond zat en stompzinnig
naar de hemel staarde.
Alice liep verlegen naar de deur en klopte.
‘Het is volkomen onzin om te kloppen,’ zei de Lakei, ‘en wel om twee redenen.
Ten eerste omdat ik aan dezelfde kant van de deur ben als jij; ten tweede, omdat ze
daarbinnen zo'n herrie maken, dat ze je onmogelijk kunnen horen.’

En werkelijk, er werd daar een verschrikkelijk lawaai gemaakt - een onophoudelijk
gehuil en genies en zo nu en dan een geweldig gekletter alsof een schotel of een ketel
in stukken viel.
‘Pardon,’ zei Alice, ‘hoe moet ik dan naar binnen komen?’
‘Dat geklop van je,’ ging de Lakei voort zonder acht op haar woorden te slaan,
‘zou enige zin hebben, als de deur tussen ons in was. Bij voorbeeld, als jij binnen
was, zou je kunnen kloppen en dan kon ik je uitlaten, begrijp je?’ Hij keek terwijl
hij praatte, aldoor naar de hemel en Alice vond dat beslist onbehoorlijk. ‘Maar
misschien kan hij er niets aan doen,’ zei ze bij zich zelf, ‘zijn ogen staan wel erg
boven op zijn hoofd. Maar in ieder geval kon hij mij antwoorden.’ - ‘Hoe moet ik
binnen komen?’ herhaalde zij nog eens.
‘Ik blijf hier zitten,’ merkte de Lakei op, ‘tot morgen -’
Op dit ogenblik ging de deur van het huis open en een groot bord scheerde naar
buiten, precies op het hoofd van de Lakei af; het vloog rakelings langs zijn neus en
brak in stukken tegen een van de bomen achter hem.
‘- of overmorgen misschien,’ vervolgde de Lakei in dezelfde toon, precies alsof
er niets was gebeurd.
‘Hoe moet ik binnen komen?’ vroeg Alice weer, nu wat luider.
‘Moet jij eigenlijk wel naar binnen?’ zei de Lakei, ‘dat is de vraag, zie je.’
Dat was het zonder twijfel; alleen vond Alice het

niet prettig om het zo te horen. ‘Het is werkelijk verschrikkelijk,’ mopperde zij bij
zich zelf, ‘zo als al die beesten tegenspreken. Het is om iemand gek te maken!’
De Lakei vond dit blijkbaar een goede gelegenheid om zijn opmerking met een
kleine variatie te herhalen. ‘Ik zal hier blijven zitten,’ zei hij ‘dagen en dagen lang.’
‘Maar wat moet ik doen!’ zei Alice.
‘Waar je zin in hebt,’ zei de Lakei en begon te fluiten.
‘Het heeft geen zin om met hem te praten,’ zei Alice wanhopig, ‘hij is volkomen
gek.’ En zij deed de deur open en ging naar binnen.
Zij kwam onmiddellijk in een grote keuken, die helemaal vol was met rook; de
Hertogin zat in het midden op een kruk met drie poten een baby te verzorgen; de
keukenmeid stond over het vuur gebogen en roerde in een grote ketel, die blijkbaar
vol was met soep.
‘Er zit vast te veel peper in die soep,’ zei Alice bij zich zelf, voorzover zij dat al
niezende kon.
Er was in elk geval te veel in de lucht; zelfs de Hertogin niesde zo nu en dan; de
baby niesde en huilde afwisselend, zonder een ogenblik rust. De enigen in de keuken,
die niet niesden, waren de keukenmeid en een grote kat, die bij de kachel zat met
een grijns op zijn gezicht van het ene oor tot het andere.
‘Zoudt u mij misschien kunnen zeggen,’ zei Alice een beetje verlegen, want zij
wist niet goed, of het wel

behoorlijk was om het eerst iets te zeggen, ‘waarom uw kat zo grijnst?’
‘'t Is een Limburger,’ zei de Hertogin, ‘daarom. Big!’
Ze zei dit laatste woord zo heftig, dat Alice opsprong
van schrik, maar ze zag het volgende ogenblik, dat het tot de baby gericht was en
niet tot haar; dus vatte ze moed en ging verder: -
‘Ik wist niet dat Limburgse katten altijd grijnsden; ik wist eigenlijk helemaal niet
dat katten konden grijnzen.’
‘Dat kunnen ze allemaal,’ zei de Hertogin, ‘en de meeste doen het ook.’

‘Ik weet er geen een, die grijnst,’ zei Alice heel beleefd. Zij vond het erg prettig om
wat te kunnen babbelen.
‘Je weet niet veel,’ zei de Hertogin, ‘dat is duidelijk.’
De toon van deze opmerking beviel Alice helemaal niet en zij vond het beter op
een ander onderwerp over te gaan. Terwijl zij daar over nadacht, nam de keukenmeid
de ketel van het vuur en begon tegelijk alles wat in haar bereik was, naar de Hertogin
en de baby te gooien: eerst kwam het haardstel, toen volgde een hagelbui van
braadpannen, borden en schalen. De Hertogin lette er niet op, zelfs niet als ze haar
raakten; en de baby huilde al zo hard dat men onmogelijk uit kon maken, of het hem
pijn deed of niet.
‘O, let u toch alsjeblieft op wat u doet,’ schreeuwde Alice, die in doodsangst heen
en weer sprong, ‘o, daar gaat zijn lieve neusje’, toen een ongewoon grote braadpan
er vlak langs vloog en het bijna meenam.
‘Als iedereen zich met zijn eigen zaken bemoeide,’ snauwde de Hertogin, ‘zou de
