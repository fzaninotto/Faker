‘Dat is waar,’ zei de Hertogin, ‘en de moraal daarvan is: 't Is liefde enkel wat de
wereld rond doet draaien.’
‘Ik heb iemand horen zeggen,’ fluisterde Alice, ‘dat die goed draait, wanneer ieder
zich met zijn eigen zaken bemoeit.’
‘Zeker, dat is hetzelfde,’ zei de Hertogin en boorde haarscherpe kin in de schouder
van Alice, ‘en de

moraal daarvan is: Pas op de zin, dan zullen de woorden wel op zich zelf passen!’
‘Zij vindt het blijkbaar erg prettig om de moraal van alles te zoeken!’ dacht Alice.
‘Je verbaast je er waarschijnlijk over dat ik mijn arm niet om je middel sla,’ zei
de Hertogin na een poosje, ‘maar ik vertrouw het karakter van je flamingo niet
helemaal. Zal ik het eens wagen?’
‘Hij kan best bijten,’ zei Alice voorzichtig, die niet veel voelde voor het experiment.
‘Heel juist,’ zei de Hertogin, ‘flamingo's en mosterd bijten allebei. En de moraal
daarvan is: Vogels van dezelfde pluimage vliegen samen.’
‘Maar mosterd is geen vogel,’ merkte Alice op.
‘Precies,’ zei de Hertogin, ‘wat kan jij de dingen duidelijk uitleggen.’
‘'t Is een mineraal, geloof ik,’ zei Alice.
‘Natuurlijk,’ zei de Hertogin, die blijkbaar instemde met alles wat Alice zei, ‘er
is hier een grote mosterdmijn in de buurt. En de moraal daarvan is: Hoe meer het
mijn, hoe minder het dijn.’
‘O, nu weet ik het,’ riep Alice, die op deze laatste opmerking niet had gelet, ‘het
is een groente. Het ziet er wel niet zo uit, maar het is er toch een.’
‘Ik ben het volkomen met je eens,’ zei de Hertogin, ‘en de moraal daarvan is: Wees
zoals je wilt lijken of als je het wat eenvoudiger wilt: Verbeeld je nooit anders te zijn
dan wat anderen denken dat je was of zou zijn niet anders was wat ze anders denken
dat je was.’

‘Ik geloof dat ik dat beter zou begrijpen,’ zei Alice, ‘als ik het eens voor me zag,
maar ik kan het eigenlijk niet goed volgen zoals u het nu zegt.’
‘Dat is nog niets bij wat ik zou kunnen zeggen als ik wilde,’ antwoordde de
Hertogin gestreeld.
‘Maar doet u heus geen moeite om het nog wat langer te zeggen,’ zei Alice.
‘Maar helemaal geen moeite,’ zei de Hertogin, ‘ik geef je alles wat ik gezegd heb
cadeau.’
‘Een goedkoop soort cadeau!’ dacht Alice, ‘ik ben blij dat ze niet zulke
verjaarscadeautjes geven!’ Maar zij dorst dit niet hardop te zeggen.
‘Weer aan het denken?’ vroeg de Hertogin en boorde haar kin weer wat vaster in
de schouder van Alice.
‘Ik heb toch het recht om te denken,’ zei Alice, want zij begon zich nu wat te
ergeren.
‘Evenveel recht,’ zei de Hertogin, ‘als biggen hebben om te vliegen en de mo -’
Maar nu zweeg de stem van de Hertogin plotseling tot Alice's grote verrassing
precies in het midden van haar geliefde woord ‘moraal’ en de arm die in de hare was
gestoken begon te beven. Alice keek op en daar stond de Koningin, vlak tegen over
hen, met gevouwen armen en een blik als een donderwolk.
‘Mooi weer vandaag, Majesteit,’ begon de Hertogin bevend.
‘Ik waarschuw je nog één keer,’ riep de Koningin en zij stampte daarbij op de
grond, ‘of jij of je hoofd moet verdwijnen en wel in minder dan de helft van geen
tijd. Doe wat je wilt!’

De Hertogin deed wat zij wou en in een ogenblik was ze uit het gezicht.
‘Kom, laten we weer gaan spelen,’ zei de Koningin tot Alice en Alice was veel te
bang om een woord uit te brengen, maar liep langzaam achter haar aan naar het
croquetveld.
De andere gasten hadden gebruik gemaakt van de afwezigheid van de Koningin
en lagen in de schaduw wat te rusten. Maar toen zij haar aan zagen komen, holden
ze allen terug naar het spel. De Koningin zei enkel dat één ogenblik uitstel hun het
leven zou kosten.
Terwijl zij aan het spelen waren, hield de Koningin niet op om ruzie te maken met
andere spelers en te roepen ‘Zijn hoofd eraf!’ of ‘Haar hoofd eraf!’ De veroordeelden
werden bewaakt door de soldaten, die dan niet meer voor poortje konden dienen,
zodat er na een half uur niet één poortje meer over was en alle spelers behalve de
Koning, de Koningin en Alice gevangen waren genomen en ter dood veroordeeld.
Toen hield de Koningin volkomen buiten adem op en zei tegen Alice: ‘Heb jij de
Soepschildpad al gezien?’
‘Nee,’ zei Alice, ‘ik weet helemaal niet wat een Soepschildpad is.’
‘Dat is het beest waarvan schildpadsoep gemaakt wordt,’ zei de Koningin.
‘Ik heb er nog nooit van gehoord,’ zei Alice.
‘Kom dan mee,’ zei de Koningin, ‘hij zal je zijn geschiedenis vertellen.’
Toen ze samen wegwandelden hoorde zij de Koning

tegen het hele gezelschap zeggen: ‘Ik verleen jullie allemaal gratie.’ ‘Dat is nu eens
goed,’ zei ze bij zichzelf, want ze vond het verschrikkelijk dat de Koningin zoveel
doodvonnissen had uitgesproken.
Ze kwamen al heel gauw bij een Griffioen, die in de zon vast lag te slapen. (Als
je niet weet wat een griffioen
is, kijk je maar op het plaatje). ‘Sta op luilak,’ zei de Koningin, ‘en breng deze jonge
dame naar de Soepschildpad om zijn geschiedenis te horen. Ik moet terug naar mijn
doodvonnissen,’ en zij liep weg en liet Alice alleen met de Griffioen. Het uiterlijk
van dit beest beviel Alice niet erg, maar ze vond dat het tenslotte even veilig was om
hier te blijven als om die boze Koningin achterna te gaan en daarom bleef ze.
De Griffioen was gaan zitten en wreef in zijn ogen. Toen keek hij of de Koningin
niet meer te zien was

en toen grinnikte hij. ‘Wat een mop,’ zei hij half in zichzelf, half tegen Alice.
‘Wat is een mop?’ zei Alice.
‘Ach’, zei de Griffioen, ‘dat denk ze allemaal maar, er wordt nooit niemand
tereggesteld. Kom mee!’
‘Iedereen zegt hier Kom mee,’ dacht Alice toen zij langzaam achter hem aan liep,
‘ik ben nog nooit zoveel gecommandeerd in mijn leven, nog nooit!’
Het was geen lange wandeling voor zij de Soepschildpad zagen. Deze zat droevig
en eenzaam op een klein rotsblok en toen zij dichter bij hem kwamen, hoorde Alice
hem zuchten of zijn hart zou breken. Zij had diep medelijden met hem.
‘Wat scheelt hem?’ vroeg zij aan de Griffioen en de Griffioen antwoordde bijna
met dezelfde woorden als daarnet: ‘Dat denktie maar, hem scheelt helemaal niks,
weet je. Kom mee!’
Dus gingen ze naar de Soepschildpad, die hen met grote ogen vol tranen aankeek,
