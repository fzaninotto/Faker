‘Ik ben een arme man,’ zei de Hoedenmaker, ‘en de meeste dingen tintelden
daarna - enkel, de Maartse Haas zei -’
‘Niet waar!’ zei de Maartse Haas opgewonden.
Wel waar!’ zei de Hoedenmaker.
‘Ik ontken het,’ zei de Maartse Haas.
‘Hij ontkent het,’ zei de Koning, ‘laat dat stuk dan maar weg.’
‘Nu, in elk geval, de Zevenslaper zei -,’ ging de Hoedenmaker verder en keek
angstig rond om te zien of deze het ook zou ontkennen, maar de Zevenslaper was
