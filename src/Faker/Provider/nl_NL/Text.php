Want degenen die het dichtst bij de deur zaten begonnen plotseling allemaal te niezen.
‘Leg getuigenis af,’ zei de Koning.
‘Ik denk er niet aan,’ zei de Keukenmeid.
De Koning keek angstig naar het Witte Konijn, dat met een zachte stem zei: ‘Uwe
Majesteit moet deze getuige een kruisverhoor afnemen.’
‘Als het moet, dan moet het,’ zei de Koning droefgeestig. Hij legde zijn armen
over elkaar, fronste zijn voorhoofd zo sterk dat zijn ogen bijna niet meer te zien
waren en zei met een diepe stem tot de Keukenmeid: ‘Waar zijn gebakjes van
gemaakt?’

‘Van peper in het algemeen,’ zei de Keukenmeid.
‘Van stroop,’ zei een slaperige stem achter haar.
‘Arresteer die Zevenslaper,’schreeuwde de Koningin, ‘onthoofd die Zevenslaper!
Gooi die Zevenslaper er uit! Onderdruk hem! Knijp hem! Knip hem zijn snor af!’
Enige minuten lang was de hele rechtszaal in grote verwarring bezig om de
Zevenslaper te verwijderen. Toen ze allemaal weer waren gaan zitten was de
Keukenmeid verdwenen.
‘Doet er niet toe,’ zei de Koning alsof hij erg was opgelucht‘ “Roep de volgende
getuige,” en hij fluisterde zachtjes tot de Koningin: “Werkelijk liefste, jij moet de
