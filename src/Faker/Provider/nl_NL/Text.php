groeiden, waren wit, maar drie tuinmannen waren bezig om deze rood te schilderen.
Alice vond dit heel zonderling en zij ging op hen af om dat eens nader te bekijken
en juist toen zij vlak bij hen was, hoorde zij één van hen zeggen: ‘Kijk toch uit Vijf!
Spat toch niet zo met de verf!’
‘Ik kon er niets aan doen,’ zei Vijf knorrig, ‘Zeven stootte tegen mijn elleboog.’
Waarop Zeven opkeek en zei: ‘Goed zo Vijf! Altijd de schuld aan iemand anders
geven!’
‘Jij kunt beter je mond houden,’ zei Vijf, ‘Gisteren hoorde ik de Koningin nog
zeggen dat jij eigenlijk onthoofd moest worden!’
‘Waarom?’ vroeg degene, die het eerst iets gezegd had.
‘Dat gaat jou niets aan, Twee,’ zei Zeven.
‘Ja, dat gaat hem wel wat aan,’ zei Vijf, ‘en ik zal het hem wel vertellen; het was
omdat hij de keukenmeid tulpenbollen gebracht had inplaats van uien.’
Zeven gooide zijn verfkwast neer en begon: ‘Dat is nu toch wel het
onrechtvaardigste’ - toen zijn blik op Alice viel, die naar hen stond te kijken en hij
hield plotseling op; ook de anderen keken om en bogen dan allen diep.

‘Ach, zoudt u mij ook willen vertellen,’ zei Alice een beetje verlegen, ‘waarom u
die rozen schildert?’
Vijf en Zeven zeiden niets, maar keken naar Twee.
Twee begon zachtjes: ‘Ach, juffrouw, ziet u, er had hier eigenlijk een rode
rozenboom moeten staan en wij
hebben per ongeluk een witte geplant; en als de Koningin dat ontdekt, worden wij
allemaal onthoofd, ziet u. En daarom proberen we om voor zij komt -’ Op dit ogenblik
riep Vijf, die angstig de tuin rond had gekeken: ‘De Koningin! de Koningin!’ en de
drie tuinmannen wierpen zich plat op hun gezicht. Men hoorde het geluid van vele
voetstappen en Alice keek rond, verlangend om de Koningin te zien.
Eerst kwamen tien soldaten, die knuppels droegen; deze hadden alle hetzelfde
figuur als de drie tuinmannen, langwerpig en plat met aan de hoeken hun handen en
voeten. Toen de tien hovelingen; deze waren helemaal versierd met ruiten en liepen
twee aan twee, net als de soldaten. Na hen kwamen de koningskinderen; het waren
er tien en de lieve

kleintjes kwamen vrolijk hand in hand aanspringen, al weer twee aan twee; zij waren
opgeschikt met harten. Daarna kwamen de gasten, meest koningen en koninginnen
en onder hen herkende Alice het Witte Konijn; hij praatte zenuwachtig, en glimlachte
bij alles wat er werd gezegd en nam geen notitie van haar. Dan volgde de Hartenboer,
die de kroon van den Koning op een kussen droeg van rood fluweel; en aan het slot
van de grote optocht kwamen DE KONING EN DE KONINGIN VAN HARTEN.
Alice wist niet goed of zij ook op de grond moest gaan liggen, net als de drie
tuinmannen, maar zij herinnerde zich een dergelijk voorschrift bij optochten niet,
‘en trouwens, waar zou een optocht voor dienen,’ dacht ze, ‘alsieder met zijn gezicht
op de grond gaat liggen, zodat niemand iets kan zien?’ Dus bleef zij staan waar zij
stond en wachtte.
Toen de optocht vlak bij Alice gekomen was, hielden allen stil en keken naar haar
en de Koningin zei streng: ‘Wie is dit?’ Zij vroeg dit aan den Hartenboer, die enkel
diep boog en glimlachte als antwoord. ‘Idioot,’ zei de Koningin, en schudde
ongeduldig haar hoofd; toen wendde zij zich tot Alice en ging verder: ‘Hoe heet jij,
meisje?’
‘Ik heet Alice, Majesteit,’ zei Alice heel beleefd; maar zij voegde er bij zichzelf
aan toe. ‘Wat zal ik me druk maken! Het is toch maar een pak kaarten!’
‘En wie zijn dit?’ zei de Koningin, wijzend op de drie tuinmannen, die om de
rozenbomen lagen; want zie je, ze lagen op hun buik en de figuren op hun rug

waren precies eender als die van de andere kaarten, dus kon zij niet zien of het
tuinmannen waren of soldaten of hovelingen of drie van haar eigen kinderen.
‘Hoe kan ik dat weten?’ zei Alice, verbaasd over haar eigen moed, ‘daar heb ik
niets mee te maken.’
