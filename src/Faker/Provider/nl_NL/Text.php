‘Je kunt gaan,’ zei de Koning en de Hoedenmaker rende zo hard als hij kon de
rechtszaal uit, zonder zelfs zijn schoenen aan te trekken.
‘- en sla hem buiten zijn hoofd af’, zei de Koningin tot een van de zaalwachters.
Maar de Hoedenmaker
was allang uit het gezicht voor de deurwaarder bij de deur was.
‘Roep de volgende getuige!’ zei de Koning.
De volgende getuige was de Keukenmeid van de Hertogin. Zij had de peperbus
