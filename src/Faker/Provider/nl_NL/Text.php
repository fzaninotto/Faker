‘Zijn hun hoofden eraf?’ riep de Koningin.

‘Hun hoofden zijn weg met uw welnemen, Majesteit.’ riepen de soldaten als antwoord.
‘Goed’ riep de Koningin, ‘kan je croquet spelen?’
De soldaten zwegen en keken naar Alice, want deze vraag was blijkbaar tot haar
gericht.
‘Ja,’ riep Alice.
‘Kom dan mee,’ brulde de Koningin en Alice voegde zich bij de optocht, heel
benieuwd wat er verder zou gebeuren.
‘Het is, het is - mooi weer vandaag,’ zei een verlegen stem naast haar. Zij liep
naast het Witte Konijn, dat haar ernstig aankeek.
‘Heel mooi,’ zei Alice, ‘waar is de Hertogin?’
‘St, st,’ zei het Konijn zacht en snel. Hij keek angstig om zich heen terwijl hij
praatte, ging dan op zijn tenen staan, hield zijn mond dicht bij haar oor en fluisterde:
‘Zij is ter dood veroordeeld.’
‘Waarvoor?’ zei Alice.
‘Zei je wat vreselijk?’ vroeg het Konijn.
‘Nee,’ zei Alice, ‘ik vind het helemaal niet vreselijk. Ik zei waarvoor.’
‘Ze heeft de Koningin een draai om de oren gegeven,’ begon het Konijn. Alice
schoot in de lach.
‘St,’ fluisterde het Konijn angstig, ‘de Koningin zal je horen! Ze kwam te laat, zie
je, en de Koningin zei -’
‘Ga allemaal op je plaats!’ schreeuwde de Koningin met donderende stem en zij
begonnen alle richtingen uit te hollen, waarbij ze voortdurend tegen elkaar aan liepen;
maar na een paar minuten stonden zij allen op hun plaats en het spel begon. Alice
had nog nooit in

haar leven zo'n wonderlijk croquetveld gezien. Het bestond helemaal uit heuveltjes
en kuilen, de ballen waren levende egels, de hamers waren levende flamingo's en de
soldaten stonden voorover op handen
en voeten om de poorten te vormen.
De grootste moeilijkheid vond Alice eerst het hanteren van haar flamingo; zij kon
het lichaam heel gemakkelijk met hangende poten onder haar arm wegstoppen, maar
juist als zij zijn nek mooi recht had gestrekt en zij de egel een klap wou geven met
zijn kop, moest hij zich beslist omdraaien en haar aankijken met een zo verbaasde
uitdrukking, dat zij in lachen uitbarstte. En als zij zijn hoofd naar beneden had geduwd
en weer wou beginnen, had de egel zich intussen uitgerold en was bezig weg te
kruipen. Bovendien was er altijd, waar zij de egel ook heen sloeg, wel een heuveltje
of een kuil in de weg en de dubbelgebogen soldaten stonden voortdurend op en liepen
naar een ander deel van het veld, zodat Alice al gauw tot de ontdekking kwam, dat
dit wel een heel moeilijk spel was.
De spelers speelden allemaal tegelijk zonder op hun

beurt te wachten, maakten de hele tijd ruzie en vochten om de egels. En al heel gauw
raakte de Koningin in een vreselijke woede, liep stampvoetend rond en schreeuwde
ieder ogenblik: ‘Zijn hoofd eraf! Haar hoofd eraf!’
Alice voelde zich niets op haar gemak. Ze had nog wel geen ruzie gehad met de
Koningin, maar zij wist dat dat ieder ogenblik, gebeuren kon ‘en wat zal er dan van
mij worden?’ dacht zij. ‘Zij schijnen het hier bijzonder prettig te vinden om mensen
te onthoofden, ik begrijp eigenlijk niet dat er nog zo veel in leven zijn!’
Zij zocht juist een middel om te ontsnappen zonder dat iemand het zou zien, toen
zij een merkwaardige verschijning in de lucht opmerkte. Ze kon deze eerst onmogelijk
thuisbrengen, maar toen zij een poosje er naar gekeken had, zag ze dat het een grijns
was en ze zei bij zichzelf: ‘Het is de Kat van de Hertogin, nu heb ik tenminste iemand
om mee te praten.’
‘Hoe gaat het er mee?’ zei de Kat, zodra hij genoeg mond had om ermee te praten.
Alice wachtte tot zijn ogen verschenen waren en knikte toen.‘Het heeft geen zin
om iets te zeggen,’ dacht zij, ‘voor zijn oren er zijn of één oor tenminste.’ Het
volgende ogenblik verscheen de hele kop en nu zette Alice haar flamingo op de grond
en begon te praten over hetspel, want zij was erg blij dat er iemand naar haar luisterde.
De Kat vond blijkbaar dat er nu genoeg van hem zichtbaar was en liet het er verder
bij.
‘Ik geloof nooit dat ze eerlijk spelen,’ klaagde Alice,

‘ze maken allemaal zo verschrikkelijk veel ruzie, dat je jezelf niet eens kunt verstaan
- en ze hebben blijkbaar helemaal geen spelregels, tenminste, niemand houdt zich er
aan - en u weet niet hoe verwarrend het is dat alle dingen leven, bijvoorbeeld, het
poortje waar ik onder door wou slaan is weggelopen naar een ander stuk van het veld
en ik wou net de egel van de Koningin een klap geven, maar hij liep weg toen hij de
mijne aan zag komen!’
‘Hoe vind je de Koningin?’ zei de Kat zachtjes.
‘Niet erg aardig,’ zei Alice, ‘ze is zo geweldig -’ toen merkte ze net op tijd dat de
Koningin achter haar stond te luisteren en ging verder - ‘aan het winnen, dat het niet
eens de moeite waard is om het spel uit te spelen.’
De Koningin glimlachte en liep verder.
‘Met wie ben je aan het praten?’ zei de Koning, die op Alice toeliep en heel
nieuwsgierig naar de kop van de Kat keek.
‘Dat is een vriend van mij - een Kat,’ zei Alice, ‘mag ik u voorstellen?’
‘Zijn uiterlijk bevalt me helemaal niet,’ zei de Koning, ‘maar hij mag mij de hand
kussen als hij wil.’
‘Liever niet,’ merkte de Kat op.
‘Wees niet zo onbeschoft,’ zei de Koning, ‘en kijk me niet zo aan!’ Hij verschool
zich achter Alice, toen hij dit zei.
‘Een kat mag naar een Koning kijken,’ zei Alice, ‘dat heb ik in een of ander boek
gelezen, maar ik weet niet meer in welk.’

‘Hij moet daar weg,’ zei de Koning beslist en hij riep aan de Koningin, die juist
voorbijging: ‘Liefste, ik wil dat je die Kat daar weg laat halen.’
De Koningin had maar één manier om alle moeilijkheden, grote of kleine, op te
lossen en zij zei zonder op te kijken: ‘Sla zijn kop af!’
‘Ik zal zelf de beul halen,’ zei de Koning vlug en liep weg.
Alice vond dat zij wel eens terug moest gaan om te kijken hoe het met het spel
stond, want zij hoorde de stem van de Koningin op enige afstand krijsen van woede.
Zij had al drie spelers horen veroordelen omdat ze niet op hun beurt gelet hadden en
de hele situatie beviel haar allerminst, want het spel verliep zo verward, dat zij
waarlijk niet meer wist of het nu haar beurt was of niet. Dus ging ze haar egel maar
eens opzoeken.
De egel wasin een gevecht gewikkeld met een andere egel, wat Alice een bijzonder
goede gelegenheid vond om een van hen met de ander weg te slaan. De enige
moeilijkheid was, dat haar flamingo naar de andere kant van de tuin was gelopen,
waar hij - zoals Alice zag - vergeefs probeerde om in een van de bomen te vliegen.
Toen zij de flamingo gevangen had en hem terug had gebracht, was het gevecht
uit en de egels waren nergens meer te vinden, ‘maar het komt er niet veel op aan,’
dacht Alice, ‘want er zijn toch geen bogen meer op dit stuk van het veld.’ Daarom
stopte ze de flamingo onder haar arm, zodat hij niet kon ontsnappen en ging

terug om nog een beetje met haar vriend te babbelen.
Toen zij terugkwam bij de Kat zag zij tot haar grote verrassing een hele menigte
om haar heen staan. Er was een twist gaande tussen de beul, de Koning en de
Koningin,
die allemaal tegelijk praatten, terwijl de anderen doodstil waren en blijkbaar heel
slecht op hun gemak.
Zodra Alice verscheen, smeekten zij haar om het probleem op te lossen en zij
herhaalden voortdurend hun argumenten. Maar zij praatten allemaal tegelijk

en Alice vond het erg moeilijk om precies uit te maken wat zij zeiden.
Het argument van de beul was dat je iemand's hoofd niet kunt afslaan, als er geen
lichaam is, dat hij nog nooit zo iets gedaan had en dat hij niet van plan was om het
op zijn leeftijd nog te gaan doen.
De Koning echter beweerde dat iedereen die een hoofd heeft ook onthoofd kan
worden en dat je geen onzin moet praten.
De Koningin tenslotte zei, dat als het nu niet gauw gebeurde, zij iedereen hier zou
laten onthoofden. (Het was deze laatste opmerking, die het hele gezelschap zo ernstig
en angstig maakte!)
Alice kon niets anders bedenken dan: ‘Hij is van de Hertogin. U kunt het beter
aan haar vragen.’
‘Die is in de gevangenis,’ zei de Koningin tot de beul, ‘breng haar hier.’ En de
beul vloog weg als een pijl uit de boog.
De kop van de Kat begon op het ogenblik dat hij wegging, langzaam te verdwijnen
en toen deze terugkwam met de Hertogin was hij weg. Dus holden de Koning en de
beul wild heen en weer om hem te zoeken, terwijl de rest van het gezelschap terugging
naar het spel.

IX. Het verhaal van de soepschildpad
JE weet niet hoe blij ik ben je hier te zien, lieve kind,’ zei de Hertogin en zij stak
haar arm hartelijk in die van Alice. Zij wandelden samen verder.
Alice was heel blij dat de Hertogin in zo'n goed humeur was en zij dacht bij zichzelf
dat het misschien alleen de peper was, die haar zo boos gemaakt had, toen zij haar
in de keuken ontmoette.
‘Alsik Hertogin word,’ zei ze bij zichzelf (maar niet erg hoopvol) ‘zou ik helemaal
geen peper in mijn keuken willen hebben.
Het is nergens voor nodig dat er peper in de soep gaat en misschien maakt peper
mensen juist zo opvliegend’ dacht zij verder, erg blij dat ze dit had ontdekt, ‘en azijn
maakt ze zuur en kamille maakt ze bitter - en - en zuurtjes en zo maakt de kinderen
zoet. Ik wou dat de mensen dat eens gingen inzien, dan zouden zij er niet zo zuinig
mee zijn.’
Zij had de Hertogin helemaal vergeten en schrok een beetje toen zij haarstem vlak
bij haar oor hoorde: ‘Je denkt ergens aan liefje, en daardoor vergeet je te praten. Ik
weet niet precies wat de moraal daarvan is, maar ik zal er zo wel opkomen.’
‘Misschien heeft het geen moraal,’ waagde Alice op te merken.
