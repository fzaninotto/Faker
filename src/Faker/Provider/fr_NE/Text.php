Les Empires

Gravure rupestre représentant des girafes et autres
De nombreux fossiles de dinosaures (iguanodons) du Crétacé inférieur ont été trouvés à Tazolé,
au Sud-Est de l'Aïr. On a mis au jour dans le massif de l'Aïr des vestiges de céramiques
du VIIIe millénaire av. J.-C. avant notre ère, contemporains de l'invention de la poterie
dans d'autres régions du monde.

Entre 2000 et 3000 av. J.-C., la désertification du Sahara, commencée à cette époque, 
repousse ensuite vers le sud les populations d'agriculteurs et les céramistes, laissant
la place à des communautés d'éleveurs de bovins qui gravent sur les rochers de nombreuses 
représentations de leurs troupeaux. Des gravures de cette époque témoignent également de 
la présence à cette latitude d'éléphants, d'hippopotames et d'une faune de savane abondante et variée.

Le désert du Ténéré est riche en témoignages de la présence humaine au Néolithique par un abondant 
matériel de pierre (pointes de flèches, meules, etc.), jusqu'à la désertification de 
la région au ier siècle avant notre ère.

Ce fut d'abord l'empire songhaï, qui naquit et s'étendit dans la région du fleuve, vers 
l'actuelle frontière nigéro-malienne. La ville de Gao est décrite depuis le viiie siècle par 
les chroniques arabes comme un centre d'échanges entre l'Empire du Ghana et l'Égypte. 
Il connut son apogée entre le xve siècle et le xvie siècle, sous Sonni Ali Ber qui étendit ses frontières 
jusqu'à Oualata. Il succomba en 1591, sous les coups d'une expédition marocaine.

Une querelle avait opposé au xvie siècle les habitants de Gao et ceux de Kano pour savoir 
laquelle des deux villes était la plus peuplée. On apprend ainsi qu'elles atteignaient toutes deux 
les 100 000 habitants (Ki-Zerbo, 1965). Ce fut le royaume du Kanem-Bornou, un des plus vastes d'Afrique qui, 
sous le règne d'Iriss Alaoma à la fin du xvie siècle, englobait tout le Kanem, montait jusqu'au 
Kaouar et à l'Aïr et s'étendait à l'est jusqu'au Ouaddai (Tchad). Après avoir contenu 
les ambitions Songhaï au xive siècle, puis résisté au xixe siècle aux attaques des 
Peuls établis dans le Nord de l'actuel Nigeria, il tomba en 1893, après près de dix siècles 
d'existence, sous les coups de Rabah qui rêvait alors de se tailler un domaine entre le Soudan et le Tchad.

Les territoires constituant le Niger actuel établissent des relations transsahariennes vers le Maroc, 
par la vallée du Niger et Tombouctou (empires du Mali et du Songhaï), vers la Tunisie (Ifriqiya), à travers 
le Sahara central, et vers la Libye et l'Égypte par le Fezzan et le Tchad (États haoussas). Cette diversité 
explique la permanence des deux grands pôles de développement culturel et économique du pays : 
la vallée du Niger (Niamey) et le bassin du Tchad (Zinder). 
Chacun de ces royaumes constituait un centre commercial et intellectuel très prospère et était ouvert, 
dès le xiiie siècle, à l'islam et à l'écriture arabe. Les Songhaï exercent une forte influence sur 
la vallée du fleuve durant la dernière partie du Moyen Âge, tandis que l'empire de Kanem-Bornou domine 
la frontière orientale.

Le commerce régional porte sur l'échange sel-mil entre le Sahara central riche en gisements de sel et 
la savane productrice de mil, et sur la noix de cola produite dans les zones forestières. Ses voies de 
communication sont très tôt pénétrées par les missionnaires musulmans, puis contrôlées par les nomades 
islamisés (Touareg, Toubou).

À partir du xie siècle, les Touaregs arrivent par vagues dans l'Aïr, se répandent dans l'Azawak 
et commencent à lancer des raids sur les sédentaires du sud.

Au début du xixe siècle, les États haoussas islamisés, grands centres commerçants, dominent 
le Niger central méridional alors que les descendants des Songhaï réfugiés après l'effondrement 
de l'empire, avec leurs alliés les Zarma, installés entre le fleuve Niger et les Dallols Bosso 
