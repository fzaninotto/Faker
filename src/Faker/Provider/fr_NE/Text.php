Tous des nouveaux envahisseurs qui veulent le contrôle des territoires, 
des multiples guerres opposent Zarmas et Peuls ou Touaregs. 
Il en résulte l'inexistence d'une grande ville ou centre économique dans la région. 
Du côté Haoussa, les régions du Niger correspondantes sont les frontières méridionales 
des grandes cités-États que sont par exemple Kano, Zaria ou Katsina.

