    /**
     * 全角文字を1文字として文字列の一部分を取得する
     * @param string $text
     * @param int $start
     * @param int|null $length
     * @return string
     */
    protected static function substr($text, $start, $length = null)
    {
        if (function_exists('mb_substr')) return mb_substr($text, $start, $length, 'UTF-8');

        $chars = static::split($text);
        $chars = array_slice($chars, $start, $length);
        return \implode('', $chars);
    }

    /**
     * 終端文字をセットする
     * @param string $text
     * @return string
     */
    protected static function appendEnd($text)
    {
        $first = self::substr($text, 0, 1);
        $last  = self::substr($text, -1, 1);

        if (in_array($first, array_keys(static::$twins))) {
            $end = static::$twins[$first];
            if (strpos($text, $end) === false) {
                return preg_replace('/.$/u', $end, $text);
            }
        }

        if (in_array($last, static::$endPunct)) {
            return $text;
        }

        $end = static::$endPunct[0];
        if (in_array($last, static::$notEndPunct)) {
            return preg_replace('/.$/u', $end, $text);
        }
        return $text . $end;
    }

