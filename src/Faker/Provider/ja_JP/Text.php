「ケンタウルス、露をふらせ」と叫んで走ったり、青いマグネシヤの花火を燃したりして、たのしそうに遊んでいるのでした。けれどもジョバンニは、いつかまた深く首をたれて、そこらのにぎやかさとはまるでちがったことを考えながら、牛乳屋の方へ急ぐのでした。
　ジョバンニは、いつか町はずれのポプラの木が幾本も幾本も、高く星ぞらに浮かんでいるところに来ていました。その牛乳屋の黒い門をはいり、牛のにおいのするうすくらい台所の前に立って、ジョバンニは帽子をぬいで、
「今晩は」と言いましたら、家の中はしいんとして誰もいたようではありませんでした。
「今晩は、ごめんなさい」ジョバンニはまっすぐに立ってまた叫びました。するとしばらくたってから、年とった女の人が、どこかぐあいが悪いようにそろそろと出て来て、何か用かと口の中で言いました。
「あの、今日、牛乳が僕とこへ来なかったので、もらいにあがったんです」ジョバンニが一生けん命勢いよく言いました。
「いま誰もいないでわかりません。あしたにしてください」その人は赤い眼の下のとこをこすりながら、ジョバンニを見おろして言いました。
「おっかさんが病気なんですから今晩でないと困るんです」
「ではもう少したってから来てください」その人はもう行ってしまいそうでした。
「そうですか。ではありがとう」ジョバンニは、お辞儀をして台所から出ました。
　十字になった町のかどを、まがろうとしましたら、向こうの橋へ行く方の雑貨店の前で、黒い影やぼんやり白いシャツが入り乱れて、六、七人の生徒らが、口笛を吹いたり笑ったりして、めいめい烏瓜の燈火を持ってやって来るのを見ました。その笑い声も口笛も、みんな聞きおぼえのあるものでした。ジョバンニの同級の子供らだったのです。ジョバンニは思わずどきっとして戻ろうとしましたが、思い直して、いっそう勢いよくそっちへ歩いて行きました。
「川へ行くの」ジョバンニが言おうとして、少しのどがつまったように思ったとき、
「ジョバンニ、ラッコの上着が来るよ」さっきのザネリがまた叫びました。
「ジョバンニ、ラッコの上着が来るよ」すぐみんなが、続いて叫びました。ジョバンニはまっ赤になって、もう歩いているかもわからず、急いで行きすぎようとしましたら、そのなかにカムパネルラがいたのです。カムパネルラはきのどくそうに、だまって少しわらって、おこらないだろうかというようにジョバンニの方を見ていました。
　ジョバンニは、にげるようにその眼を避け、そしてカムパネルラのせいの高いかたちが過ぎて行ってまもなく、みんなはてんでに口笛を吹きました。町かどを曲がるとき、ふりかえって見ましたら、ザネリがやはりふりかえって見ていました。そしてカムパネルラもまた、高く口笛を吹いて向こうにぼんやり見える橋の方へ歩いて行ってしまったのでした。ジョバンニは、なんとも言えずさびしくなって、いきなり走りだしました。すると耳に手をあてて、わあわあと言いながら片足でぴょんぴょん跳んでいた小さな子供らは、ジョバンニがおもしろくてかけるのだと思って、わあいと叫びました。
　まもなくジョバンニは走りだして黒い丘の方へ急ぎました。

五　天気輪の柱

　牧場のうしろはゆるい丘になって、その黒い平らな頂上は、北の大熊星の下に、ぼんやりふだんよりも低く、連なって見えました。
　ジョバンニは、もう露の降りかかった小さな林のこみちを、どんどんのぼって行きました。まっくらな草や、いろいろな形に見えるやぶのしげみの間を、その小さなみちが、一すじ白く星あかりに照らしだされてあったのです。草の中には、ぴかぴか青びかりを出す小さな虫もいて、ある葉は青くすかし出され、ジョバンニは、さっきみんなの持って行った烏瓜のあかりのようだとも思いました。
　そのまっ黒な、松や楢の林を越えると、にわかにがらんと空がひらけて、天の川がしらしらと南から北へ亙っているのが見え、また頂の、天気輪の柱も見わけられたのでした。つりがねそうか野ぎくかの花が、そこらいちめんに、夢の中からでもかおりだしたというように咲き、鳥が一疋、丘の上を鳴き続けながら通って行きました。
　ジョバンニは、頂の天気輪の柱の下に来て、どかどかするからだを、つめたい草に投げました。
　町の灯は、暗の中をまるで海の底のお宮のけしきのようにともり、子供らの歌う声や口笛、きれぎれの叫び声もかすかに聞こえて来るのでした。風が遠くで鳴り、丘の草もしずかにそよぎ、ジョバンニの汗でぬれたシャツもつめたく冷やされました。
　野原から汽車の音が聞こえてきました。その小さな列車の窓は一列小さく赤く見え、その中にはたくさんの旅人が、苹果をむいたり、わらったり、いろいろなふうにしていると考えますと、ジョバンニは、もうなんとも言えずかなしくなって、また眼をそらに挙げました。
（この間原稿五枚分なし）
　ところがいくら見ていても、そのそらは、ひる先生の言ったような、がらんとした冷たいとこだとは思われませんでした。それどころでなく、見れば見るほど、そこは小さな林や牧場やらある野原のように考えられてしかたなかったのです。そしてジョバンニは青い琴の星が、三つにも四つにもなって、ちらちらまたたき、脚が何べんも出たり引っ込んだりして、とうとう蕈のように長く延びるのを見ました。またすぐ眼の下のまちまでが、やっぱりぼんやりしたたくさんの星の集まりか一つの大きなけむりかのように見えるように思いました。

六　銀河ステーション

　そしてジョバンニはすぐうしろの天気輪の柱がいつかぼんやりした三角標の形になって、しばらく蛍のように、ぺかぺか消えたりともったりしているのを見ました。それはだんだんはっきりして、とうとうりんとうごかないようになり、濃い鋼青のそらの野原にたちました。いま新しく灼いたばかりの青い鋼の板のような、そらの野原に、まっすぐにすきっと立ったのです。
　するとどこかで、ふしぎな声が、銀河ステーション、銀河ステーションと言う声がしたと思うと、いきなり眼の前が、ぱっと明るくなって、まるで億万の蛍烏賊の火を一ぺんに化石させて、そらじゅうに沈めたというぐあい、またダイアモンド会社で、ねだんがやすくならないために、わざと穫れないふりをして、かくしておいた金剛石を、誰かがいきなりひっくりかえして、ばらまいたというふうに、眼の前がさあっと明るくなって、ジョバンニは、思わず何べんも眼をこすってしまいました。
　気がついてみると、さっきから、ごとごとごとごと、ジョバンニの乗っている小さな列車が走りつづけていたのでした。ほんとうにジョバンニは、夜の軽便鉄道の、小さな黄いろの電燈のならんだ車室に、窓から外を見ながらすわっていたのです。車室の中は、青い天鵞絨を張った腰掛けが、まるでがらあきで、向こうの鼠いろのワニスを塗った壁には、真鍮の大きなぼたんが二つ光っているのでした。
　すぐ前の席に、ぬれたようにまっ黒な上着を着た、せいの高い子供が、窓から頭を出して外を見ているのに気がつきました。そしてそのこどもの肩のあたりが、どうも見たことのあるような気がして、そう思うと、もうどうしても誰だかわかりたくて、たまらなくなりました。いきなりこっちも窓から顔を出そうとしたとき、にわかにその子供が頭を引っ込めて、こっちを見ました。
　それはカムパネルラだったのです。ジョバンニが、
　カムパネルラ、きみは前からここにいたの、と言おうと思ったとき、カムパネルラが、
「みんなはね、ずいぶん走ったけれども遅れてしまったよ。ザネリもね、ずいぶん走ったけれども追いつかなかった」と言いました。
　ジョバンニは、
（そうだ、ぼくたちはいま、いっしょにさそって出かけたのだ）とおもいながら、
「どこかで待っていようか」と言いました。するとカムパネルラは、
「ザネリはもう帰ったよ。お父さんが迎いにきたんだ」
　カムパネルラは、なぜかそう言いながら、少し顔いろが青ざめて、どこか苦しいというふうでした。するとジョバンニも、なんだかどこかに、何か忘れたものがあるというような、おかしな気持ちがしてだまってしまいました。
　ところがカムパネルラは、窓から外をのぞきながら、もうすっかり元気が直って、勢いよく言いました。
「ああしまった。ぼく、水筒を忘れてきた。スケッチ帳も忘れてきた。けれどかまわない。もうじき白鳥の停車場だから。ぼく、白鳥を見るなら、ほんとうにすきだ。川の遠くを飛んでいたって、ぼくはきっと見える」
　そして、カムパネルラは、まるい板のようになった地図を、しきりにぐるぐるまわして見ていました。まったく、その中に、白くあらわされた天の川の左の岸に沿って一条の鉄道線路が、南へ南へとたどって行くのでした。そしてその地図の立派なことは、夜のようにまっ黒な盤の上に、一々の停車場や三角標、泉水や森が、青や橙や緑や、うつくしい光でちりばめられてありました。
　ジョバンニはなんだかその地図をどこかで見たようにおもいました。
「この地図はどこで買ったの。黒曜石でできてるねえ」
　ジョバンニが言いました。
「銀河ステーションで、もらったんだ。君もらわなかったの」
「ああ、ぼく銀河ステーションを通ったろうか。いまぼくたちのいるとこ、ここだろう」
　ジョバンニは、白鳥と書いてある停車場のしるしの、すぐ北を指しました。
「そうだ。おや、あの河原は月夜だろうか」そっちを見ますと、青白く光る銀河の岸に、銀いろの空のすすきが、もうまるでいちめん、風にさらさらさらさら、ゆられてうごいて、波を立てているのでした。
「月夜でないよ。銀河だから光るんだよ」ジョバンニは言いながら、まるではね上がりたいくらい愉快になって、足をこつこつ鳴らし、窓から顔を出して、高く高く星めぐりの口笛を吹きながら一生けん命延びあがって、その天の川の水を、見きわめようとしましたが、はじめはどうしてもそれが、はっきりしませんでした。けれどもだんだん気をつけて見ると、そのきれいな水は、ガラスよりも水素よりもすきとおって、ときどき眼のかげんか、ちらちら紫いろのこまかな波をたてたり、虹のようにぎらっと光ったりしながら、声もなくどんどん流れて行き、野原にはあっちにもこっちにも、燐光の三角標が、うつくしく立っていたのです。遠いものは小さく、近いものは大きく、遠いものは橙や黄いろではっきりし、近いものは青白く少しかすんで、あるいは三角形、あるいは四辺形、あるいは電や鎖の形、さまざまにならんで、野原いっぱいに光っているのでした。ジョバンニは、まるでどきどきして、頭をやけに振りました。するとほんとうに、そのきれいな野原じゅうの青や橙や、いろいろかがやく三角標も、てんでに息をつくように、ちらちらゆれたり顫えたりしました。
「ぼくはもう、すっかり天の野原に来た」ジョバンニは言いました。
「それに、この汽車石炭をたいていないねえ」ジョバンニが左手をつき出して窓から前の方を見ながら言いました。
「アルコールか電気だろう」カムパネルラが言いました。
　するとちょうど、それに返事するように、どこか遠くの遠くのもやのもやの中から、セロのようなごうごうした声がきこえて来ました。
「ここの汽車は、スティームや電気でうごいていない。ただうごくようにきまっているからうごいているのだ。ごとごと音をたてていると、そうおまえたちは思っているけれども、それはいままで音をたてる汽車にばかりなれているためなのだ」
「あの声、ぼくなんべんもどこかできいた」
「ぼくだって、林の中や川で、何べんも聞いた」
　ごとごとごとごと、その小さなきれいな汽車は、そらのすすきの風にひるがえる中を、天の川の水や、三角点の青じろい微光の中を、どこまでもどこまでもと、走って行くのでした。
「ああ、りんどうの花が咲いている。もうすっかり秋だねえ」カムパネルラが、窓の外を指さして言いました。
　線路のへりになったみじかい芝草の中に、月長石ででも刻まれたような、すばらしい紫のりんどうの花が咲いていました。
「ぼく飛びおりて、あいつをとって、また飛び乗ってみせようか」ジョバンニは胸をおどらせて言いました。
「もうだめだ。あんなにうしろへ行ってしまったから」
　カムパネルラが、そう言ってしまうかしまわないうち、次のりんどうの花が、いっぱいに光って過ぎて行きました。
　と思ったら、もう次から次から、たくさんのきいろな底をもったりんどうの花のコップが、湧くように、雨のように、眼の前を通り、三角標の列は、けむるように燃えるように、いよいよ光って立ったのです。

七　北十字とプリオシン海岸

「おっかさんは、ぼくをゆるしてくださるだろうか」
　いきなり、カムパネルラが、思い切ったというように、少しどもりながら、せきこんで言いました。
　ジョバンニは、
（ああ、そうだ、ぼくのおっかさんは、あの遠い一つのちりのように見える橙いろの三角標のあたりにいらっしゃって、いまぼくのことを考えているんだった）と思いながら、ぼんやりしてだまっていました。
「ぼくはおっかさんが、ほんとうに幸になるなら、どんなことでもする。けれども、いったいどんなことが、おっかさんのいちばんの幸なんだろう」カムパネルラは、なんだか、泣きだしたいのを、一生けん命こらえているようでした。
「きみのおっかさんは、なんにもひどいことないじゃないの」ジョバンニはびっくりして叫びました。
「ぼくわからない。けれども、誰だって、ほんとうにいいことをしたら、いちばん幸なんだねえ。だから、おっかさんは、ぼくをゆるしてくださると思う」カムパネルラは、なにかほんとうに決心しているように見えました。
　にわかに、車のなかが、ぱっと白く明るくなりました。見ると、もうじつに、金剛石や草の露やあらゆる立派さをあつめたような、きらびやかな銀河の河床の上を、水は声もなくかたちもなく流れ、その流れのまん中に、ぼうっと青白く後光の射した一つの島が見えるのでした。その島の平らないただきに、立派な眼もさめるような、白い十字架がたって、それはもう、凍った北極の雲で鋳たといったらいいか、すきっとした金いろの円光をいただいて、しずかに永久に立っているのでした。
「ハレルヤ、ハレルヤ」前からもうしろからも声が起こりました。ふりかえって見ると、車室の中の旅人たちは、みなまっすぐにきもののひだを垂れ、黒いバイブルを胸にあてたり、水晶の数珠をかけたり、どの人もつつましく指を組み合わせて、そっちに祈っているのでした。思わず二人ともまっすぐに立ちあがりました。カムパネルラの頬は、まるで熟した苹果のあかしのようにうつくしくかがやいて見えました。
　そして島と十字架とは、だんだんうしろの方へうつって行きました。
　向こう岸も、青じろくぼうっと光ってけむり、時々、やっぱりすすきが風にひるがえるらしく、さっとその銀いろがけむって、息でもかけたように見え、また、たくさんのりんどうの花が、草をかくれたり出たりするのは、やさしい狐火のように思われました。
　それもほんのちょっとの間、川と汽車との間は、すすきの列でさえぎられ、白鳥の島は、二度ばかり、うしろの方に見えましたが、じきもうずうっと遠く小さく、絵のようになってしまい、またすすきがざわざわ鳴って、とうとうすっかり見えなくなってしまいました。ジョバンニのうしろには、いつから乗っていたのか、せいの高い、黒いかつぎをしたカトリックふうの尼さんが、まんまるな緑の瞳を、じっとまっすぐに落として、まだ何かことばか声かが、そっちから伝わって来るのを、虔んで聞いているというように見えました。旅人たちはしずかに席に戻り、二人も胸いっぱいのかなしみに似た新しい気持ちを、何気なくちがった語で、そっと談し合ったのです。
「もうじき白鳥の停車場だねえ」
「ああ、十一時かっきりには着くんだよ」
　早くも、シグナルの緑の燈と、ぼんやり白い柱とが、ちらっと窓のそとを過ぎ、それから硫黄のほのおのようなくらいぼんやりした転てつ機の前のあかりが窓の下を通り、汽車はだんだんゆるやかになって、まもなくプラットホームの一列の電燈が、うつくしく規則正しくあらわれ、それがだんだん大きくなってひろがって、二人はちょうど白鳥停車場の、大きな時計の前に来てとまりました。
　さわやかな秋の時計の盤面には、青く灼かれたはがねの二本の針が、くっきり十一時を指しました。みんなは、一ぺんにおりて、車室の中はがらんとなってしまいました。
〔二十分停車〕と時計の下に書いてありました。
「ぼくたちも降りて見ようか」ジョバンニが言いました。
「降りよう」二人は一度にはねあがってドアを飛び出して改札口へかけて行きました。ところが改札口には、明るい紫がかった電燈が、一つ点いているばかり、誰もいませんでした。そこらじゅうを見ても、駅長や赤帽らしい人の、影もなかったのです。
　二人は、停車場の前の、水晶細工のように見える銀杏の木に囲まれた、小さな広場に出ました。
　そこから幅の広いみちが、まっすぐに銀河の青光の中へ通っていました。
　さきに降りた人たちは、もうどこへ行ったか一人も見えませんでした。二人がその白い道を、肩をならべて行きますと、二人の影は、ちょうど四方に窓のある室の中の、二本の柱の影のように、また二つの車輪の輻のように幾本も幾本も四方へ出るのでした。そしてまもなく、あの汽車から見えたきれいな河原に来ました。
　カムパネルラは、そのきれいな砂を一つまみ、掌にひろげ、指できしきしさせながら、夢のように言っているのでした。
「この砂はみんな水晶だ。中で小さな火が燃えている」
「そうだ」どこでぼくは、そんなことを習ったろうと思いながら、ジョバンニもぼんやり答えていました。
　河原の礫は、みんなすきとおって、たしかに水晶や黄玉や、またくしゃくしゃの皺曲をあらわしたのや、また稜から霧のような青白い光を出す鋼玉やらでした。ジョバンニは、走ってその渚に行って、水に手をひたしました。けれどもあやしいその銀河の水は、水素よりももっとすきとおっていたのです。それでもたしかに流れていたことは、二人の手首の、水にひたったとこが、少し水銀いろに浮いたように見え、その手首にぶっつかってできた波は、うつくしい燐光をあげて、ちらちらと燃えるように見えたのでもわかりました。
　川上の方を見ると、すすきのいっぱいにはえている崖の下に、白い岩が、まるで運動場のように平らに川に沿って出ているのでした。そこに小さな五、六人の人かげが、何か掘り出すか埋めるかしているらしく、立ったりかがんだり、時々なにかの道具が、ピカッと光ったりしました。
「行ってみよう」二人は、まるで一度に叫んで、そっちの方へ走りました。その白い岩になったところの入口に、〔プリオシン海岸〕という、瀬戸物のつるつるした標札が立って、向こうの渚には、ところどころ、細い鉄の欄干も植えられ、木製のきれいなベンチも置いてありました。
「おや、変なものがあるよ」カムパネルラが、不思議そうに立ちどまって、岩から黒い細長いさきのとがったくるみの実のようなものをひろいました。
「くるみの実だよ。そら、たくさんある。流れて来たんじゃない。岩の中にはいってるんだ」
「大きいね、このくるみ、倍あるね。こいつはすこしもいたんでない」
「早くあすこへ行って見よう。きっと何か掘ってるから」
　二人は、ぎざぎざの黒いくるみの実を持ちながら、またさっきの方へ近よって行きました。左手の渚には、波がやさしい稲妻のように燃えて寄せ、右手の崖には、いちめん銀や貝殻でこさえたようなすすきの穂がゆれたのです。
　だんだん近づいて見ると、一人のせいの高い、ひどい近眼鏡をかけ、長靴をはいた学者らしい人が、手帳に何かせわしそうに書きつけながら、つるはしをふりあげたり、スコップをつかったりしている、三人の助手らしい人たちに夢中でいろいろ指図をしていました。
「そこのその突起をこわさないように、スコップを使いたまえ、スコップを。おっと、も少し遠くから掘って。いけない、いけない、なぜそんな乱暴をするんだ」
　見ると、その白い柔らかな岩の中から、大きな大きな青じろい獣の骨が、横に倒れてつぶれたというふうになって、半分以上掘り出されていました。そして気をつけて見ると、そこらには、蹄の二つある足跡のついた岩が、四角に十ばかり、きれいに切り取られて番号がつけられてありました。
「君たちは参観かね」その大学士らしい人が、眼鏡をきらっとさせて、こっちを見て話しかけました。
「くるみがたくさんあったろう。それはまあ、ざっと百二十万年ぐらい前のくるみだよ。ごく新しい方さ。ここは百二十万年前、第三紀のあとのころは海岸でね、この下からは貝がらも出る。いま川の流れているとこに、そっくり塩水が寄せたり引いたりもしていたのだ。このけものかね、これはボスといってね、おいおい、そこ、つるはしはよしたまえ。ていねいに鑿でやってくれたまえ。ボスといってね、いまの牛の先祖で、昔はたくさんいたのさ」
「標本にするんですか」
「いや、証明するに要るんだ。ぼくらからみると、ここは厚い立派な地層で、百二十万年ぐらい前にできたという証拠もいろいろあがるけれども、ぼくらとちがったやつからみてもやっぱりこんな地層に見えるかどうか、あるいは風か水や、がらんとした空かに見えやしないかということなのだ。わかったかい。けれども、おいおい、そこもスコップではいけない。そのすぐ下に肋骨が埋もれてるはずじゃないか」
　大学士はあわてて走って行きました。
「もう時間だよ。行こう」カムパネルラが地図と腕時計とをくらべながら言いました。
「ああ、ではわたくしどもは失礼いたします」ジョバンニは、ていねいに大学士におじぎしました。
「そうですか。いや、さよなら」大学士は、また忙しそうに、あちこち歩きまわって監督をはじめました。
　二人は、その白い岩の上を、一生けん命汽車におくれないように走りました。そしてほんとうに、風のように走れたのです。息も切れず膝もあつくなりませんでした。
　こんなにしてかけるなら、もう世界じゅうだってかけれると、ジョバンニは思いました。
　そして二人は、前のあの河原を通り、改札口の電燈がだんだん大きくなって、まもなく二人は、もとの車室の席にすわっていま行って来た方を、窓から見ていました。

八　鳥を捕る人

「ここへかけてもようございますか」
　がさがさした、けれども親切そうな、大人の声が、二人のうしろで聞こえました。
　それは、茶いろの少しぼろぼろの外套を着て、白い巾でつつんだ荷物を、二つに分けて肩に掛けた、赤髯のせなかのかがんだ人でした。
「ええ、いいんです」ジョバンニは、少し肩をすぼめてあいさつしました。その人は、ひげの中でかすかに微笑いながら荷物をゆっくり網棚にのせました。ジョバンニは、なにかたいへんさびしいようなかなしいような気がして、だまって正面の時計を見ていましたら、ずうっと前の方で、硝子の笛のようなものが鳴りました。汽車はもう、しずかにうごいていたのです。カムパネルラは、車室の天井を、あちこち見ていました。その一つのあかりに黒い甲虫がとまって、その影が大きく天井にうつっていたのです。赤ひげの人は、なにかなつかしそうにわらいながら、ジョバンニやカムパネルラのようすを見ていました。汽車はもうだんだん早くなって、すすきと川と、かわるがわる窓の外から光りました。
　赤ひげの人が、少しおずおずしながら、二人に訊きました。
「あなた方は、どちらへいらっしゃるんですか」
「どこまでも行くんです」ジョバンニは、少しきまり悪そうに答えました。
「それはいいね。この汽車は、じっさい、どこまででも行きますぜ」
「あなたはどこへ行くんです」カムパネルラが、いきなり、喧嘩のようにたずねましたので、ジョバンニは思わずわらいました。すると、向こうの席にいた、とがった帽子をかぶり、大きな鍵を腰に下げた人も、ちらっとこっちを見てわらいましたので、カムパネルラも、つい顔を赤くして笑いだしてしまいました。ところがその人は別におこったでもなく、頬をぴくぴくしながら返事をしました。
「わっしはすぐそこで降ります。わっしは、鳥をつかまえる商売でね」
「何鳥ですか」
「鶴や雁です。さぎも白鳥もです」
「鶴はたくさんいますか」
「いますとも、さっきから鳴いてまさあ。聞かなかったのですか」
「いいえ」
「いまでも聞こえるじゃありませんか。そら、耳をすまして聴いてごらんなさい」
　二人は眼を挙げ、耳をすましました。ごとごと鳴る汽車のひびきと、すすきの風との間から、ころんころんと水の湧くような音が聞こえて来るのでした。
「鶴、どうしてとるんですか」
「鶴ですか、それとも鷺ですか」
「鷺です」ジョバンニは、どっちでもいいと思いながら答えました。
「そいつはな、雑作ない。さぎというものは、みんな天の川の砂が凝って、ぼおっとできるもんですからね、そして始終川へ帰りますからね、川原で待っていて、鷺がみんな、脚をこういうふうにしておりてくるとこを、そいつが地べたへつくかつかないうちに、ぴたっと押えちまうんです。するともう鷺は、かたまって安心して死んじまいます。あとはもう、わかり切ってまさあ。押し葉にするだけです」
「鷺を押し葉にするんですか。標本ですか」
「標本じゃありません。みんなたべるじゃありませんか」
「おかしいねえ」カムパネルラが首をかしげました。
「おかしいも不審もありませんや。そら」その男は立って、網棚から包みをおろして、手ばやくくるくると解きました。
「さあ、ごらんなさい。いまとって来たばかりです」
「ほんとうに鷺だねえ」二人は思わず叫びました。まっ白な、あのさっきの北の十字架のように光る鷺のからだが、十ばかり、少しひらべったくなって、黒い脚をちぢめて、浮彫りのようにならんでいたのです。
「眼をつぶってるね」カムパネルラは、指でそっと、鷺の三日月がたの白いつぶった眼にさわりました。頭の上の槍のような白い毛もちゃんとついていました。
「ね、そうでしょう」鳥捕りは風呂敷を重ねて、またくるくると包んで紐でくくりました。誰がいったいここらで鷺なんぞたべるだろうとジョバンニは思いながら訊きました。
「鷺はおいしいんですか」
「ええ、毎日注文があります。しかし雁の方が、もっと売れます。雁の方がずっと柄がいいし、第一手数がありませんからな。そら」鳥捕りは、また別の方の包みを解きました。すると黄と青じろとまだらになって、なにかのあかりのようにひかる雁が、ちょうどさっきの鷺のように、くちばしをそろえて、少しひらべったくなって、ならんでいました。
「こっちはすぐたべられます。どうです、少しおあがりなさい」鳥捕りは、黄いろの雁の足を、軽くひっぱりました。するとそれは、チョコレートででもできているように、すっときれいにはなれました。
「どうです。すこしたべてごらんなさい」鳥捕りは、それを二つにちぎってわたしました。ジョバンニは、ちょっとたべてみて、
（なんだ、やっぱりこいつはお菓子だ。チョコレートよりも、もっとおいしいけれども、こんな雁が飛んでいるもんか。この男は、どこかそこらの野原の菓子屋だ。けれどもぼくは、このひとをばかにしながら、この人のお菓子をたべているのは、たいへんきのどくだ）とおもいながら、やっぱりぽくぽくそれをたべていました。
「も少しおあがりなさい」鳥捕りがまた包みを出しました。ジョバンニは、もっとたべたかったのですけれども、
「ええ、ありがとう」といって遠慮しましたら、鳥捕りは、こんどは向こうの席の、鍵をもった人に出しました。
「いや、商売ものをもらっちゃすみませんな」その人は、帽子をとりました。
「いいえ、どういたしまして。どうです、今年の渡り鳥の景気は」
「いや、すてきなもんですよ。一昨日の第二限ころなんか、なぜ燈台の灯を、規則以外に間（一時空白）させるかって、あっちからもこっちからも、電話で故障が来ましたが、なあに、こっちがやるんじゃなくて、渡り鳥どもが、まっ黒にかたまって、あかしの前を通るのですからしかたありませんや、わたしぁ、べらぼうめ、そんな苦情は、おれのとこへ持って来たってしかたがねえや、ばさばさのマントを着て脚と口との途方もなく細い大将へやれって、こう言ってやりましたがね、はっは」
　すすきがなくなったために、向こうの野原から、ぱっとあかりが射して来ました。
「鷺の方はなぜ手数なんですか」カムパネルラは、さっきから、訊こうと思っていたのです。
「それはね、鷺をたべるには」鳥捕りは、こっちに向き直りました。「天の川の水あかりに、十日もつるしておくかね、そうでなけぁ、砂に三、四日うずめなけぁいけないんだ。そうすると、水銀がみんな蒸発して、たべられるようになるよ」
「こいつは鳥じゃない。ただのお菓子でしょう」やっぱりおなじことを考えていたとみえて、カムパネルラが、思い切ったというように、尋ねました。鳥捕りは、何かたいへんあわてたふうで、
「そうそう、ここで降りなけぁ」と言いながら、立って荷物をとったと思うと、もう見えなくなっていました。
「どこへ行ったんだろう」二人は顔を見合わせましたら、燈台守は、にやにや笑って、少し伸びあがるようにしながら、二人の横の窓の外をのぞきました。二人もそっちを見ましたら、たったいまの鳥捕りが、黄いろと青じろの、うつくしい燐光を出す、いちめんのかわらははこぐさの上に立って、まじめな顔をして両手をひろげて、じっとそらを見ていたのです。
「あすこへ行ってる。ずいぶん奇体だねえ。きっとまた鳥をつかまえるとこだねえ。汽車が走って行かないうちに、早く鳥がおりるといいな」と言ったとたん、がらんとした桔梗いろの空から、さっき見たような鷺が、まるで雪の降るように、ぎゃあぎゃあ叫びながら、いっぱいに舞いおりて来ました。するとあの鳥捕りは、すっかり注文通りだというようにほくほくして、両足をかっきり六十度に開いて立って、鷺のちぢめて降りて来る黒い脚を両手で片っぱしから押えて、布の袋の中に入れるのでした。すると鷺は、蛍のように、袋の中でしばらく、青くぺかぺか光ったり消えたりしていましたが、おしまいとうとう、みんなぼんやり白くなって、眼をつぶるのでした。ところが、つかまえられる鳥よりは、つかまえられないで無事に天の川の砂の上に降りるものの方が多かったのです。それは見ていると、足が砂へつくや否や、まるで雪の解けるように、縮まってひらべったくなって、まもなく溶鉱炉から出た銅の汁のように、砂や砂利の上にひろがり、しばらくは鳥の形が、砂についているのでしたが、それも二、三度明るくなったり暗くなったりしているうちに、もうすっかりまわりと同じいろになってしまうのでした。
　鳥捕りは、二十疋ばかり、袋に入れてしまうと、急に両手をあげて、兵隊が鉄砲弾にあたって、死ぬときのような形をしました。と思ったら、もうそこに鳥捕りの形はなくなって、かえって、
「ああせいせいした。どうもからだにちょうど合うほど稼いでいるくらい、いいことはありませんな」というききおぼえのある声が、ジョバンニの隣りにしました。見ると鳥捕りは、もうそこでとって来た鷺を、きちんとそろえて、一つずつ重ね直しているのでした。
「どうして、あすこから、いっぺんにここへ来たんですか」ジョバンニが、なんだかあたりまえのような、あたりまえでないような、おかしな気がして問いました。
「どうしてって、来ようとしたから来たんです。ぜんたいあなた方は、どちらからおいでですか」
　ジョバンニは、すぐ返事をしようと思いましたけれども、さあ、ぜんたいどこから来たのか、もうどうしても考えつきませんでした。カムパネルラも、顔をまっ赤にして何か思い出そうとしているのでした。
「ああ、遠くからですね」鳥捕りは、わかったというように雑作なくうなずきました。

九　ジョバンニの切符

「もうここらは白鳥区のおしまいです。ごらんなさい。あれが名高いアルビレオの観測所です」
　窓の外の、まるで花火でいっぱいのような、あまの川のまん中に、黒い大きな建物が四棟ばかり立って、その一つの平屋根の上に、眼もさめるような、青宝玉と黄玉の大きな二つのすきとおった球が、輪になってしずかにくるくるとまわっていました。黄いろのがだんだん向こうへまわって行って、青い小さいのがこっちへ進んで来、まもなく二つのはじは、重なり合って、きれいな緑いろの両面凸レンズのかたちをつくり、それもだんだん、まん中がふくらみだして、とうとう青いのは、すっかりトパーズの正面に来ましたので、緑の中心と黄いろな明るい環とができました。それがまただんだん横へ外れて、前のレンズの形を逆にくり返し、とうとうすっとはなれて、サファイアは向こうへめぐり、黄いろのはこっちへ進み、またちょうどさっきのようなふうになりました。銀河の、かたちもなく音もない水にかこまれて、ほんとうにその黒い測候所が、睡っているように、しずかによこたわったのです。
「あれは、水の速さをはかる器械です。水も……」鳥捕りが言いかけたとき、
「切符を拝見いたします」三人の席の横に、赤い帽子をかぶったせいの高い車掌が、いつかまっすぐに立っていて言いました。鳥捕りは、だまってかくしから、小さな紙きれを出しました。車掌はちょっと見て、すぐ眼をそらして（あなた方のは？）というように、指をうごかしながら、手をジョバンニたちの方へ出しました。
「さあ」ジョバンニは困って、もじもじしていましたら、カムパネルラはわけもないというふうで、小さな鼠いろの切符を出しました。ジョバンニは、すっかりあわててしまって、もしか上着のポケットにでも、はいっていたかとおもいながら、手を入れてみましたら、何か大きなたたんだ紙きれにあたりました。こんなものはいっていたろうかと思って、急いで出してみましたら、それは四つに折ったはがきぐらいの大さの緑いろの紙でした。車掌が手を出しているもんですからなんでもかまわない、やっちまえと思って渡しましたら、車掌はまっすぐに立ち直ってていねいにそれを開いて見ていました。そして読みながら上着のぼたんやなんかしきりに直したりしていましたし燈台看守も下からそれを熱心にのぞいていましたから、ジョバンニはたしかにあれは証明書か何かだったと考えて少し胸が熱くなるような気がしました。
「これは三次空間の方からお持ちになったのですか」車掌がたずねました。
「なんだかわかりません」もう大丈夫だと安心しながらジョバンニはそっちを見あげてくつくつ笑いました。
「よろしゅうございます。南十字へ着きますのは、次の第三時ころになります」車掌は紙をジョバンニに渡して向こうへ行きました。
　カムパネルラは、その紙切れが何だったか待ちかねたというように急いでのぞきこみました。ジョバンニも全く早く見たかったのです。ところがそれはいちめん黒い唐草のような模様の中に、おかしな十ばかりの字を印刷したもので、だまって見ているとなんだかその中へ吸い込まれてしまうような気がするのでした。すると鳥捕りが横からちらっとそれを見てあわてたように言いました。
「おや、こいつはたいしたもんですぜ。こいつはもう、ほんとうの天上へさえ行ける切符だ。天上どこじゃない、どこでもかってにあるける通行券です。こいつをお持ちになれぁ、なるほど、こんな不完全な幻想第四次の銀河鉄道なんか、どこまででも行けるはずでさあ、あなた方たいしたもんですね」
「なんだかわかりません」ジョバンニが赤くなって答えながら、それをまたたたんでかくしに入れました。そしてきまりが悪いのでカムパネルラと二人、また窓の外をながめていましたが、その鳥捕りの時々たいしたもんだというように、ちらちらこっちを見ているのがぼんやりわかりました。
「もうじき鷲の停車場だよ」カムパネルラが向こう岸の、三つならんだ小さな青じろい三角標と、地図とを見くらべて言いました。
　ジョバンニはなんだかわけもわからずに、にわかにとなりの鳥捕りがきのどくでたまらなくなりました。鷺をつかまえてせいせいしたとよろこんだり、白いきれでそれをくるくる包んだり、ひとの切符をびっくりしたように横目で見てあわててほめだしたり、そんなことを一々考えていると、もうその見ず知らずの鳥捕りのために、ジョバンニの持っているものでも食べるものでもなんでもやってしまいたい、もうこの人のほんとうの幸になるなら、自分があの光る天の川の河原に立って百年つづけて立って鳥をとってやってもいいというような気がして、どうしてももう黙っていられなくなりました。ほんとうにあなたのほしいものはいったい何ですかと訊こうとして、それではあんまり出し抜けだから、どうしようかと考えてふり返って見ましたら、そこにはもうあの鳥捕りがいませんでした。網棚の上には白い荷物も見えなかったのです。また窓の外で足をふんばってそらを見上げて鷺を捕るしたくをしているのかと思って、急いでそっちを見ましたが、外はいちめんのうつくしい砂子と白いすすきの波ばかり、あの鳥捕りの広いせなかもとがった帽子も見えませんでした。
「あの人どこへ行ったろう」カムパネルラもぼんやりそう言っていました。
「どこへ行ったろう。いったいどこでまたあうのだろう。僕はどうしても少しあの人に物を言わなかったろう」
「ああ、僕もそう思っているよ」
「僕はあの人が邪魔なような気がしたんだ。だから僕はたいへんつらい」ジョバンニはこんなへんてこな気もちは、ほんとうにはじめてだし、こんなこと今まで言ったこともないと思いました。
「なんだか苹果のにおいがする。僕いま苹果のことを考えたためだろうか」カムパネルラが不思議そうにあたりを見まわしました。
「ほんとうに苹果のにおいだよ。それから野茨のにおいもする」
　ジョバンニもそこらを見ましたがやっぱりそれは窓からでもはいって来るらしいのでした。いま秋だから野茨の花のにおいのするはずはないとジョバンニは思いました。
　そしたらにわかにそこに、つやつやした黒い髪の六つばかりの男の子が赤いジャケツのぼたんもかけず、ひどくびっくりしたような顔をして、がたがたふるえてはだしで立っていました。隣りには黒い洋服をきちんと着たせいの高い青年がいっぱいに風に吹かれているけやきの木のような姿勢で、男の子の手をしっかりひいて立っていました。
「あら、ここどこでしょう。まあ、きれいだわ」青年のうしろに、もひとり、十二ばかりの眼の茶いろな可愛らしい女の子が、黒い外套を着て青年の腕にすがって不思議そうに窓の外を見ているのでした。
「ああ、ここはランカシャイヤだ。いや、コンネクテカット州だ。いや、ああ、ぼくたちはそらへ来たのだ。わたしたちは天へ行くのです。ごらんなさい。あのしるしは天上のしるしです。もうなんにもこわいことありません。わたくしたちは神さまに召されているのです」黒服の青年はよろこびにかがやいてその女の子に言いました。けれどもなぜかまた額に深く皺を刻んで、それにたいへんつかれているらしく、無理に笑いながら男の子をジョバンニのとなりにすわらせました。それから女の子にやさしくカムパネルラのとなりの席を指さしました。女の子はすなおにそこへすわって、きちんと両手を組み合わせました。
「ぼく、おおねえさんのとこへ行くんだよう」腰掛けたばかりの男の子は顔を変にして燈台看守の向こうの席にすわったばかりの青年に言いました。青年はなんとも言えず悲しそうな顔をして、じっとその子の、ちぢれたぬれた頭を見ました。女の子は、いきなり両手を顔にあててしくしく泣いてしまいました。
「お父さんやきくよねえさんはまだいろいろお仕事があるのです。けれどももうすぐあとからいらっしゃいます。それよりも、おっかさんはどんなに永く待っていらっしゃったでしょう。わたしの大事なタダシはいまどんな歌をうたっているだろう、雪の降る朝にみんなと手をつないで、ぐるぐるにわとこのやぶをまわってあそんでいるだろうかと考えたり、ほんとうに待って心配していらっしゃるんですから、早く行って、おっかさんにお目にかかりましょうね」
「うん、だけど僕、船に乗らなけぁよかったなあ」
「ええ、けれど、ごらんなさい、そら、どうです、あの立派な川、ね、あすこはあの夏じゅう、ツィンクル、ツィンクル、リトル、スターをうたってやすむとき、いつも窓からぼんやり白く見えていたでしょう。あすこですよ。ね、きれいでしょう、あんなに光っています」
　泣いていた姉もハンケチで眼をふいて外を見ました。青年は教えるようにそっと姉弟にまた言いました。
「わたしたちはもう、なんにもかなしいことないのです。わたしたちはこんないいとこを旅して、じき神さまのとこへ行きます。そこならもう、ほんとうに明るくてにおいがよくて立派な人たちでいっぱいです。そしてわたしたちの代わりにボートへ乗れた人たちは、きっとみんな助けられて、心配して待っているめいめいのお父さんやお母さんや自分のお家へやら行くのです。さあ、もうじきですから元気を出しておもしろくうたって行きましょう」青年は男の子のぬれたような黒い髪をなで、みんなを慰めながら、自分もだんだん顔いろがかがやいてきました。
「あなた方はどちらからいらっしゃったのですか。どうなすったのですか」
　さっきの燈台看守がやっと少しわかったように青年にたずねました。青年はかすかにわらいました。
「いえ、氷山にぶっつかって船が沈みましてね、わたしたちはこちらのお父さんが急な用で二か月前、一足さきに本国へお帰りになったので、あとから発ったのです。私は大学へはいっていて、家庭教師にやとわれていたのです。ところがちょうど十二日目、今日か昨日のあたりです、船が氷山にぶっつかって一ぺんに傾きもう沈みかけました。月のあかりはどこかぼんやりありましたが、霧が非常に深かったのです。ところがボートは左舷の方半分はもうだめになっていましたから、とてもみんなは乗り切らないのです。もうそのうちにも船は沈みますし、私は必死となって、どうか小さな人たちを乗せてくださいと叫びました。近くの人たちはすぐみちを開いて、そして子供たちのために祈ってくれました。けれどもそこからボートまでのところには、まだまだ小さな子どもたちや親たちやなんかいて、とても押しのける勇気がなかったのです。それでもわたくしはどうしてもこの方たちをお助けするのが私の義務だと思いましたから前にいる子供らを押しのけようとしました。けれどもまた、そんなにして助けてあげるよりはこのまま神の御前にみんなで行く方が、ほんとうにこの方たちの幸福だとも思いました。それからまた、その神にそむく罪はわたくしひとりでしょってぜひとも助けてあげようと思いました。けれども、どうしても見ているとそれができないのでした。子どもらばかりのボートの中へはなしてやって、お母さんが狂気のようにキスを送りお父さんがかなしいのをじっとこらえてまっすぐに立っているなど、とてももう腸もちぎれるようでした。そのうち船はもうずんずん沈みますから、私たちはかたまって、もうすっかり覚悟して、この人たち二人を抱いて、浮かべるだけは浮かぼうと船の沈むのを待っていました。誰が投げたかライフヴイが一つ飛んで来ましたけれどもすべってずうっと向こうへ行ってしまいました。私は一生けん命で甲板の格子になったとこをはなして、三人それにしっかりとりつきました。どこからともなく三〇六番の声があがりました。たちまちみんなはいろいろな国語で一ぺんにそれをうたいました。そのときにわかに大きな音がして私たちは水に落ち、もう渦にはいったと思いながらしっかりこの人たちをだいて、それからぼうっとしたと思ったらもうここへ来ていたのです。この方たちのお母さんは一昨年没くなられました。ええ、ボートはきっと助かったにちがいありません、なにせよほど熟練な水夫たちが漕いで、すばやく船からはなれていましたから」
　そこらから小さな嘆息やいのりの声が聞こえジョバンニもカムパネルラもいままで忘れていたいろいろのことをぼんやり思い出して眼が熱くなりました。
（ああ、その大きな海はパシフィックというのではなかったろうか。その氷山の流れる北のはての海で、小さな船に乗って、風や凍りつく潮水や、はげしい寒さとたたかって、たれかが一生けんめいはたらいている。ぼくはそのひとにほんとうにきのどくでそしてすまないような気がする。ぼくはそのひとのさいわいのためにいったいどうしたらいいのだろう）
　ジョバンニは首をたれて、すっかりふさぎ込んでしまいました。
「なにがしあわせかわからないです。ほんとうにどんなつらいことでもそれがただしいみちを進む中でのできごとなら、峠の上りも下りもみんなほんとうの幸福に近づく一あしずつですから」
　燈台守がなぐさめていました。
「ああそうです。ただいちばんのさいわいに至るためにいろいろのかなしみもみんなおぼしめしです」
　青年が祈るようにそう答えました。
　そしてあの姉弟はもうつかれてめいめいぐったり席によりかかって睡っていました。さっきのあのはだしだった足にはいつか白い柔らかな靴をはいていたのです。
　ごとごとごとごと汽車はきらびやかな燐光の川の岸を進みました。向こうの方の窓を見ると、野原はまるで幻燈のようでした。百も千もの大小さまざまの三角標、その大きなものの上には赤い点々をうった測量旗も見え、野原のはてはそれらがいちめん、たくさんたくさん集まってぼおっと青白い霧のよう、そこからか、またはもっと向こうからか、ときどきさまざまの形のぼんやりした狼煙のようなものが、かわるがわるきれいな桔梗いろのそらにうちあげられるのでした。じつにそのすきとおった奇麗な風は、ばらのにおいでいっぱいでした。
「いかがですか。こういう苹果はおはじめてでしょう」向こうの席の燈台看守がいつか黄金と紅でうつくしくいろどられた大きな苹果を落とさないように両手で膝の上にかかえていました。
「おや、どっから来たのですか。立派ですねえ。ここらではこんな苹果ができるのですか」青年はほんとうにびっくりしたらしく、燈台看守の両手にかかえられた一もりの苹果を、眼を細くしたり首をまげたりしながら、われを忘れてながめていました。
「いや、まあおとりください。どうか、まあおとりください」
　青年は一つとってジョバンニたちの方をちょっと見ました。
「さあ、向こうの坊ちゃんがた。いかがですか。おとりください」
　ジョバンニは坊ちゃんといわれたので、すこししゃくにさわってだまっていましたが、カムパネルラは、
「ありがとう」と言いました。
　すると青年は自分でとって一つずつ二人に送ってよこしましたので、ジョバンニも立って、ありがとうと言いました。
　燈台看守はやっと両腕があいたので、こんどは自分で一つずつ睡っている姉弟の膝にそっと置きました。
「どうもありがとう。どこでできるのですか。こんな立派な苹果は」
　青年はつくづく見ながら言いました。
「この辺ではもちろん農業はいたしますけれどもたいていひとりでにいいものができるような約束になっております。農業だってそんなにほねはおれはしません。たいてい自分の望む種子さえ播けばひとりでにどんどんできます。米だってパシフィック辺のように殻もないし十倍も大きくてにおいもいいのです。けれどもあなたがたのいらっしゃる方なら農業はもうありません。苹果だってお菓子だって、かすが少しもありませんから、みんなそのひとそのひとによってちがった、わずかのいいかおりになって毛あなからちらけてしまうのです」
　にわかに男の子がばっちり眼をあいて言いました。
「ああぼくいまお母さんの夢をみていたよ。お母さんがね、立派な戸棚や本のあるとこにいてね、ぼくの方を見て手をだしてにこにこにこにこわらったよ。ぼく、おっかさん。りんごをひろってきてあげましょうか、と言ったら眼がさめちゃった。ああここ、さっきの汽車のなかだねえ」
「その苹果がそこにあります。このおじさんにいただいたのですよ」青年が言いました。
「ありがとうおじさん。おや、かおるねえさんまだねてるねえ、ぼくおこしてやろう。ねえさん。ごらん、りんごをもらったよ。おきてごらん」
　姉はわらって眼をさまし、まぶしそうに両手を眼にあてて、それから苹果を見ました。
　男の子はまるでパイをたべるように、もうそれをたべていました。またせっかくむいたそのきれいな皮も、くるくるコルク抜きのような形になって床へ落ちるまでの間にはすうっと、灰いろに光って蒸発してしまうのでした。
　二人はりんごをたいせつにポケットにしまいました。
　川下の向こう岸に青く茂った大きな林が見え、その枝には熟してまっ赤に光るまるい実がいっぱい、その林のまん中に高い高い三角標が立って、森の中からはオーケストラベルやジロフォンにまじってなんとも言えずきれいな音いろが、とけるように浸みるように風につれて流れて来るのでした。
　青年はぞくっとしてからだをふるうようにしました。
　だまってその譜を聞いていると、そこらにいちめん黄いろや、うすい緑の明るい野原か敷物かがひろがり、またまっ白な蝋のような露が太陽の面をかすめて行くように思われました。
「まあ、あの烏」カムパネルラのとなりの、かおると呼ばれた女の子が叫びました。
「からすでない。みんなかささぎだ」カムパネルラがまた何気なくしかるように叫びましたので、ジョバンニはまた思わず笑い、女の子はきまり悪そうにしました。まったく河原の青じろいあかりの上に、黒い鳥がたくさんたくさんいっぱいに列になってとまってじっと川の微光を受けているのでした。
「かささぎですねえ、頭のうしろのとこに毛がぴんと延びてますから」青年はとりなすように言いました。
　向こうの青い森の中の三角標はすっかり汽車の正面に来ました。そのとき汽車のずうっとうしろの方から、あの聞きなれた三〇六番の讃美歌のふしが聞こえてきました。よほどの人数で合唱しているらしいのでした。青年はさっと顔いろが青ざめ、たって一ぺんそっちへ行きそうにしましたが思いかえしてまたすわりました。かおる子はハンケチを顔にあててしまいました。
　ジョバンニまでなんだか鼻が変になりました。けれどもいつともなく誰ともなくその歌は歌い出されだんだんはっきり強くなりました。思わずジョバンニもカムパネルラもいっしょにうたいだしたのです。
　そして青い橄欖の森が、見えない天の川の向こうにさめざめと光りながらだんだんうしろの方へ行ってしまい、そこから流れて来るあやしい楽器の音も、もう汽車のひびきや風の音にすりへらされてずうっとかすかになりました。
「あ、孔雀がいるよ。あ、孔雀がいるよ」
「あの森琴の宿でしょう。あたしきっとあの森の中にむかしの大きなオーケストラの人たちが集まっていらっしゃると思うわ、まわりには青い孔雀やなんかたくさんいると思うわ」
「ええ、たくさんいたわ」女の子がこたえました。
　ジョバンニはその小さく小さくなっていまはもう一つの緑いろの貝ぼたんのように見える森の上にさっさっと青じろく時々光ってその孔雀がはねをひろげたりとじたりする光の反射を見ました。
「そうだ、孔雀の声だってさっき聞こえた」カムパネルラが女の子に言いました。
「ええ、三十疋ぐらいはたしかにいたわ」女の子が答えました。
　ジョバンニはにわかになんとも言えずかなしい気がして思わず、
「カムパネルラ、ここからはねおりて遊んで行こうよ」とこわい顔をして言おうとしたくらいでした。
　ところがそのときジョバンニは川下の遠くの方に不思議なものを見ました。それはたしかになにか黒いつるつるした細長いもので、あの見えない天の川の水の上に飛び出してちょっと弓のようなかたちに進んで、また水の中にかくれたようでした。おかしいと思ってまたよく気をつけていましたら、こんどはずっと近くでまたそんなことがあったらしいのでした。そのうちもうあっちでもこっちでも、その黒いつるつるした変なものが水から飛び出して、まるく飛んでまた頭から水へくぐるのがたくさん見えてきました。みんな魚のように川上へのぼるらしいのでした。
「まあ、なんでしょう。たあちゃん。ごらんなさい。まあたくさんだわね。なんでしょうあれ」
　睡そうに眼をこすっていた男の子はびっくりしたように立ちあがりました。
「なんだろう」青年も立ちあがりました。
「まあ、おかしな魚だわ、なんでしょうあれ」
「海豚です」カムパネルラがそっちを見ながら答えました。
「海豚だなんてあたしはじめてだわ。けどここ海じゃないんでしょう」
「いるかは海にいるときまっていない」あの不思議な低い声がまたどこからかしました。
　ほんとうにそのいるかのかたちのおかしいことは、二つのひれをちょうど両手をさげて不動の姿勢をとったようなふうにして水の中から飛び出して来て、うやうやしく頭を下にして不動の姿勢のまままた水の中へくぐって行くのでした。見えない天の川の水もそのときはゆらゆらと青い焔のように波をあげるのでした。
「いるかお魚でしょうか」女の子がカムパネルラにはなしかけました。男の子はぐったりつかれたように席にもたれて睡っていました。
「いるか、魚じゃありません。くじらと同じようなけだものです」カムパネルラが答えました。
「あなたくじら見たことあって」
「僕あります。くじら、頭と黒いしっぽだけ見えます。潮を吹くとちょうど本にあるようになります」
「くじらなら大きいわねえ」
「くじら大きいです。子供だっているかぐらいあります」
「そうよ、あたしアラビアンナイトで見たわ」姉は細い銀いろの指輪をいじりながらおもしろそうにはなししていました。
（カムパネルラ、僕もう行っちまうぞ。僕なんか鯨だって見たことないや）
　ジョバンニはまるでたまらないほどいらいらしながら、それでも堅く、唇を噛んでこらえて窓の外を見ていました。その窓の外には海豚のかたちももう見えなくなって川は二つにわかれました。そのまっくらな島のまん中に高い高いやぐらが一つ組まれて、その上に一人の寛い服を着て赤い帽子をかぶった男が立っていました。そして両手に赤と青の旗をもってそらを見上げて信号しているのでした。
　ジョバンニが見ている間その人はしきりに赤い旗をふっていましたが、にわかに赤旗をおろしてうしろにかくすようにし、青い旗を高く高くあげてまるでオーケストラの指揮者のようにはげしく振りました。すると空中にざあっと雨のような音がして、何かまっくらなものが、いくかたまりもいくかたまりも鉄砲丸のように川の向こうの方へ飛んで行くのでした。ジョバンニは思わず窓からからだを半分出して、そっちを見あげました。美しい美しい桔梗いろのがらんとした空の下を、実に何万という小さな鳥どもが、幾組も幾組もめいめいせわしくせわしく鳴いて通って行くのでした。
「鳥が飛んで行くな」ジョバンニが窓の外で言いました。
「どら」カムパネルラもそらを見ました。
